                            <h2 class="centrar-texto brexia-color encabezados-brexia ">El cáncer y los tratamientos contra el cáncer tal vez causen desnutrición</h2>
                            <p>El cáncer y los tratamientos contra el cáncer a veces afectan el gusto, el olfato, el apetito y la capacidad de comer suficiente o de absorber los nutrientes de los alimentos. Es posible que esto cause desnutrición, una afección
                                por falta de nutrientes esenciales. El abuso de bebidas alcohólicas y la obesidad aumentan el riesgo de desnutrición.</p>
                            <p>La desnutrición hace que el paciente se sienta débil, cansado e incapaz de combatir infecciones o de completar el tratamiento contra el cáncer. La desnutrición suele empeorar cuando el cáncer crece o se disemina.</p>
                            <p>Es importante consumir la cantidad adecuada de proteínas y calorías para curarse, combatir infecciones y tener suficiente energía.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">La anorexia y la caquexia son causas comunes de desnutrición en los pacientes de cáncer</h2>
                            <p>La anorexia es la pérdida del apetito o deseo de comer. Es un síntoma común en los pacientes de cáncer. La anorexia a veces se presenta al comienzo de la enfermedad o después, cuando el cáncer crece o se disemina. Algunos pacientes
                                ya tienen anorexia cuando el cáncer se diagnostica. Casi todos los pacientes de cáncer avanzado tendrán anorexia. La anorexia es la causa más común de desnutrición en los pacientes de cáncer.</p>

