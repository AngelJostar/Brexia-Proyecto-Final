                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Es posible que el cáncer y los tratamientos contra el cáncer produzcan efectos secundarios que influyen en la alimentación</h2>
                            <p>Para muchos pacientes, los efectos del cáncer y los tratamientos contra el cáncer hacen que sea difícil comer bien. Los siguientes tratamientos contra el cáncer afectan la nutrición:</p>
                            <ul class="secciones-enumeradas">
                                <li>Quimioterapia</li>
                                <li>Terapia con hormonas</li>
                                <li>Radioterapia</li>
                                <li>Cirugía</li>
                                <li>Inmunoterapia</li>
                                <li>Trasplante de células madre</li>
