                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Manejo del Dolor en Cáncer</h2>
                        <div class="font-16 fontW-400">
                            <p>El manejo del dolor es una rama de la medicina enfocada en reducir el dolor y mejorar la calidad de vida a través de un enfoque integral de atención. El manejo del dolor es particularmente importante para los pacientes con
                                cáncer, considerando que uno de cada tres pacientes continúa experimentando dolor después del tratamiento.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/equipo-multidisciplinario/clinica_del_dolor-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Si tiene dolor, la gravedad y la prevalencia de su dolor pueden depender de muchos factores, incluido el tipo y la etapa de su cáncer, su plan de tratamiento y su tolerancia al dolor. Puede experimentar dolor por el cáncer
                                en sí mismo, o como un efecto secundario del tratamiento contra el cáncer. Las personas con cáncer avanzado tienen más probabilidades de tener dolor intenso. El dolor por cáncer puede ser el resultado de:</p>
                            <ul class="secciones-enumeradas">
                                <li>Un tumor que ejerce presión sobre tejidos, huesos, nervios u órganos.</li>
                                <li>Mala circulación sanguínea porque el cáncer ha bloqueado los vasos sanguíneos.</li>
                                <li>Bloqueo de un órgano o tubo en el cuerpo, como los riñones o la vejiga.</li>
                                <li>Efectos secundarios de la cirugía, radioterapia, quimioterapia u otros tratamientos contra el cáncer.</li>
                                <li>Metástasis o células cancerosas que se han diseminado a otros lugares del cuerpo.</li>
                                <li>Dolor que es completamente independiente del cáncer o el tratamiento del cáncer.</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">El control del dolor mejora la calidad de vida</h2>
                            <p>El control del dolor puede mejorar la calidad de vida en cualquier etapa del cáncer, por lo que controlar el dolor es nuestra prioridad. Nuestro equipo de manejo del dolor utiliza una variedad de modalidades para tratar y controlar
                                el dolor, que incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Medicamentos recetados</li>
                                <li>Bombas de dolor implantadas</li>
                                <li>Terapias de bloqueo nervioso</li>
                                <li>Terapia física</li>
                                <li>Acupuntura</li>
                                <li>Terapia de Masajes</li>
                                <li>Técnicas de relajación e imágenes guiadas</li>
                            </ul>
                            <p>En la mayoría de los pacientes con cáncer el dolor se logra controlar. Aunque el dolor del cáncer casi nunca se controla por completo, hay formas de aliviar el dolor en la mayoría de los pacientes.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Cada paciente necesita un plan personal para controlar el dolor del cáncer</h2>
                            <p>El diagnóstico, el estadio del cáncer, la respuesta al dolor y las cosas que le gustan o no a cada persona son diferentes. Por esta razón, cada paciente necesita un plan personal para controlar el dolor del cáncer. Usted, su
                                familia y su equipo de atención de la salud pueden trabajar juntos para controlar el dolor.</p>
