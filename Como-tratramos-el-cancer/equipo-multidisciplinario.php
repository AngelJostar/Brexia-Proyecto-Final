                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué son los cuidados paliativos?</h2>
                        <div class="font-16 fontW-400">
                            <p>Los cuidados paliativos son los cuidados para mejorar la calidad de vida de los pacientes que tienen una enfermedad grave o que pone la vida en peligro, como el cáncer. Los cuidados paliativos son un planteamiento de los cuidados
                                que responde a la persona en su totalidad, no solo a su enfermedad. El objetivo de los cuidados paliativos es evitar o tratar, lo más pronto posible, los síntomas y los efectos secundarios de una enfermedad y de su tratamiento,
                                y los problemas psicológicos, sociales y espirituales correspondientes. Los cuidados paliativos se llaman también cuidado de alivio, cuidados médicos de apoyo y control de síntomas.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/equipo-multidisciplinario/cuidados_palitativos-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué problemas se tratan con los cuidados paliativos?</h2>
                            <p>Los efectos físicos y emocionales del cáncer y su tratamiento pueden ser muy diferentes de una persona a otra. Los cuidados paliativos pueden responder a una gama muy amplia de asuntos, al integrar las necesidades específicas
                                de un individuo dentro de la atención. Un especialista en cuidados paliativos tendrá en cuenta los asuntos siguientes para cada paciente:</p>
                            <ul class="secciones-enumeradas">
                                <li>Físicos: Los síntomas físicos comunes incluyen el dolor, el cansancio, falta de apetito, náuseas, vómitos, falta de respiración e insomnio.</li>
                                <li>Emocionales y de superación: Los especialistas en cuidados paliativos pueden proveer recursos para ayudar a los pacientes y a las familias a manejar las emociones que se presentan con un diagnóstico y tratamiento de cáncer.
                                    La depresión, la ansiedad y el temor son solo algunas de las preocupaciones que pueden tratarse por medio de cuidados paliativos.</li>
                                <li>Espirituales: Con un diagnóstico de cáncer, los pacientes y las familias buscan con frecuencia más profundamente un significado a sus vidas. Algunas personas se dan cuenta de que la enfermedad les acerca más a sus creencias
                                    espirituales o de fe, mientras que otras batallan en entender por qué ocurre el cáncer a ellas. Un experto en cuidados paliativos puede ayudar a la gente a explorar sus creencias y valores para que puedan encontrar
                                    un sentido de paz o lleguen al punto de aceptación que sea apropiado para su situación.</li>
                                <li>Necesidad de quien cuida al enfermo: Los miembros de la familia son una parte importante de la atención para el cáncer. Como el paciente, ellos tienen necesidades que cambian. Es común que los miembros de la familia se
                                    sientan exhaustos por las responsabilidades adicionales asignadas a ellos. Para muchas personas resulta difícil cuidar a un pariente enfermo mientras tratan de cumplir con otras obligaciones, como su trabajo, las tareas
                                    domésticas y cuidar a otros miembros de la familia. La incertidumbre de cómo ayudar a su ser querido con las situaciones médicas, el apoyo social inadecuado y las emociones como las preocupaciones y el temor pueden
                                    añadirse también a la tensión de quien cuida al enfermo.</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Cuándo se usan los cuidados paliativos en la atención del cáncer?</h2>
                            <p>Los cuidados paliativos pueden proveerse a lo largo del tratamiento de cáncer, desde el diagnóstico hasta el final de la vida, Cuando una persona recibe cuidados paliativos, ella puede seguir recibiendo también tratamiento
                                para el cáncer.</p>

