                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Es importante que los pacientes de cáncer tengan una buena nutrición</h2>
                            <p>La nutrición es un proceso por el que el cuerpo incorpora alimentos y los usa para crecer, mantenerse sano y reemplazar los tejidos. La buena nutrición es importante para la buena salud. Comer alimentos adecuados antes, durante
                                y después del tratamiento del cáncer puede ayudar a que el paciente se sienta mejor y esté más fuerte. Una alimentación saludable incluye comer y beber cantidades suficientes de alimentos y líquidos que contengan los nutrientes
                                importantes que el cuerpo necesita (vitaminas, minerales, proteínas, carbohidratos, grasas y agua).</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Un especialista en nutrición es un integrante importante del equipo de atención de la salud</h2>
                            <p>Un nutriólogo forma parte del equipo de profesionales de la salud que colabora durante el tratamiento y la recuperación del cáncer. El especialista en nutrición trabajará con los pacientes, los familiares y el resto del equipo
                                médico para planificar la alimentación del paciente durante el tratamiento del cáncer y después de este.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Efectos secundarios controlados por el nutriólogo</h2>
                            <ul class="secciones-enumeradas">
