                        <div class="font-16 fontW-400">
                            <p>Muchos pacientes con cáncer experimentan síntomas gastrointestinales. Un nutriólogo trabaja con nuestros pacientes para ayudar a restaurar la salud digestiva y prevenir la desnutrición y las deficiencias de nutrientes durante
                                el tratamiento. El objetivo es ayudarlo a mantenerse fuerte y nutrido, para que pueda continuar con su tratamiento contra el cáncer.</p>
                            <p>Cada paciente tiene la opción de ser atendido por un nutriólogo.</p>
                            <p>El nutriólogo trabajará con su equipo de atención para controlar su estado nutricional desde el principio hasta el final de su tratamiento contra el cáncer, haciendo las modificaciones necesarias para ayudar a abordar los efectos
                                secundarios y prevenir las interrupciones del tratamiento antes de que surjan.</p>
                            <p>Su nutriólogo se comunicará regularmente con los oncólogos y otros miembros del equipo oncológico del paciente. Trabajar juntos en estrecha proximidad permite un enfoque integral.</p>
