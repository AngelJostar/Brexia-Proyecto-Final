                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿De qué manera el cáncer afecta la vida familiar?</h2>
                        <div class="font-16 fontW-400">
                            <p>Como cualquier persona con cáncer lo sabe, un diagnóstico de cáncer también afecta a los familiares y amigos. A veces, los complejos cambios en los sentimientos y el estilo de vida como consecuencia del cáncer y su tratamiento
                                pueden volverse tan abrumadores para otras personas en su vida como lo son para usted.</p>
                            <p>Comprender los posibles cambios en la forma de relacionarse con familiares y amigos puede ayudarlo a tomar medidas para forjar relaciones saludables y mutuamente solidarias durante este tiempo de desafíos.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/sentimientos-y-el-cancer/familia_cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">La importancia de la comunicación</h2>
                            <p>La buena comunicación es importante en las relaciones entre las personas con cáncer y aquellos que se preocupan por ellas. La falta de comunicación a menudo provoca aislamiento, frustración y malentendidos. Hablar sobre los
                                sentimientos y las necesidades personales con honestidad, sinceridad y franqueza disminuye el estrés en las relaciones.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Cónyuges y parejas</h2>
                            <p>El cáncer tiene un efecto significativo en los matrimonios y otras relaciones de largo plazo. Después de un diagnóstico de cáncer, ambas personas pueden sentir tristeza, ansiedad, enojo o incluso desesperanza.</p>
                            <p>Los efectos del cáncer varían de una pareja a otra. Para algunas parejas, enfrentar los desafíos del cáncer juntos fortalece su relación. Para otros, el estrés del cáncer puede generar problemas nuevos y empeorar los existentes.</p>
                            <p>Estos son algunos cambios que a menudo ocurren en las relaciones:</p>
                            <ul class="secciones-enumeradas">
                                <li>Roles</li>
                                <li>Responsabilidades</li>
                                <li>Necesidades Físicas</li>
                                <li>Necesidades emocionales</li>
                                <li>Salud Sexual e intimidad</li>
                                <li>Planes Futuros</li>
                            </ul>
                            <p>Hablar abiertamente sobre estos temas y posibles soluciones los ayudará a ambos a sentirse más cómodos con estos cambios.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Amigos y Familiares Adultos</h2>
                            <p>Los efectos del cáncer en sus relaciones con amigos y familiares varían ampliamente, según la cercanía de cada relación. Diferentes familias tienen estilos diferentes de comunicación y de enfrentarse a las dificultades. Considere
                                de qué manera reacciona su familia a una crisis y cómo sus familiares se han enfrentado a otras situaciones difíciles. Esto lo ayudará a planificar su estrategia para comunicar noticias y pedir ayuda.</p>
                            <p>Estas son algunas sugerencias para ayudarlo a adaptarse a los cambios en las relaciones con amigos y familiares:</p>
                            <ul class="secciones-enumeradas">
                                <li>Ponga a 1 persona a cargo de comunicar las últimas noticias médicas</li>
                                <li>Prevea un cambio en las relaciones</li>
                                <li>Tome la iniciativa en la comunicación</li>
                                <li>Deje que la gente lo ayude</li>
                                <li>No deje de participar en actividades sociales</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Niños</h2>
                            <p>Ser un padre o una madre con cáncer presenta desafíos únicos, incluidos los siguientes:</p>
                            <p>Comunicación. Probablemente quiera proteger a sus hijos del miedo y otros sentimientos difíciles. Pero, es importante hablar con sinceridad con ellos acerca de su diagnóstico y tratamiento.</p>
                            <p>Cambios en el comportamiento de los niños. Espere alteraciones en el comportamiento de sus hijos a medida que se adaptan a los cambios como consecuencia del diagnóstico y el tratamiento del cáncer que usted padece. Y tenga
                                paciencia.
                            </p>
                            <p>Inversión de roles. Los hijos adultos pueden cumplir el rol de cuidadores de un padre o madre con cáncer. Este es un cambio que a menudo es difícil tanto para los padres como para los hijos.

                            </p>
                            <p>Equilibrar sus necesidades y las necesidades de sus hijos. Ser padre o madre mientras padece cáncer es, a menudo, agotador física y emocionalmente. Puede continuar cuidando de su familia al mismo tiempo que se cuida usted y
                                enfrenta un diagnóstico de cáncer. Este es el momento de reconsiderar sus horarios, su lista de quehaceres, sus objetivos y expectativas. Acepte la ayuda de los demás y pida ayuda cuando la necesita.</p>
