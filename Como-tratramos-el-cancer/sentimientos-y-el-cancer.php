                    <article id="tab6">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Temor a Efectos Secundarios</h2>
                        <div class="font-16 fontW-400">
                            <p>Cuando se prepara para iniciar el tratamiento para el cáncer, es normal sentir temor a los efectos secundarios relacionados con el tratamiento. Pero no tenga miedo de hablar con su equipo de atención médica sobre los posibles
                                efectos secundarios y sobre cómo manejarlos. Su equipo de atención médica estará enfocado en prevenir y en controlar los efectos secundarios. Hablar con ellos puede ayudarlo a relajarse y a prepararlo para lo que se viene.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/sentimientos-y-el-cancer/miedo-cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Miedos frecuentes sobre los efectos secundarios</h2>
                            <p>Algunos de los miedos más frecuentes incluyen los siguientes:</p>
                            <ul class="secciones-enumeradas">
                                <li>Pérdida del control y/o no saber qué esperar</li>
                                <li>Molestia, dolor, náuseas o cansancio</li>
                                <li>No poder realizar las actividades diarias, como ir a trabajar, realizar tareas del hogar y asistir a eventos sociales</li>
                                <li>El aspecto cambia, como la pérdida del cabello o las cicatrices</li>
                                <li> Problemas sexuales o dificultad para quedar embarazada después del tratamiento</li>
                                <li>Ansiedad sobre el tratamiento o procedimiento</li>
                            </ul>
                            <p>Hable con su familia y con sus seres queridos sobre sus expectativas y preocupaciones. Su apoyo puede aliviar sus miedos sobre experimentar efectos secundarios del tratamiento.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Sobrellevar sus miedos</h2>
                            <p>Recuerde que los objetivos a largo plazo del tratamiento son ayudarlo, no lastimarlo. Muchos tratamientos contra el cáncer que se usan actualmente son menos intensos y toman menos tiempo que tratamientos anteriores. Y muchos
                                de los efectos secundarios que puede experimentar son temporales y desaparecerán de muchas semanas a meses después de finalizar su tratamiento.</p>
                            <p>A menudo, puede controlar los efectos secundarios con medicación. Y muchos efectos secundarios se pueden prevenir antes de comenzar el tratamiento. Hable con su equipo de atención médica sobre cómo controlar los efectos secundarios
                                frecuentes para cada tratamiento. Los siguientes consejos pueden ayudarlo a sobrellevar el miedo a los efectos secundarios relacionados con el tratamiento:</p>
                            <ul class="secciones-enumeradas">
