                    <article id="tab5">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Entender el duelo y la pérdida</h2>
                        <div class="font-16 fontW-400">
                            <p>Cuando una persona pierde a alguien cercano, es natural pasar por un duelo. Este proceso lleva tiempo e involucra diferentes emociones y comportamientos. Las personas con cáncer y sus familias también pueden atravesar un duelo
                                por otras pérdidas relacionadas al cáncer. Estas pueden incluir la pérdida de un seno, la pérdida de la fertilidad o la pérdida de la independencia.</p>
                            <p>Los términos “duelo”, “luto” y “pesar” tienen pequeñas diferencias de significado:</p>
                            <p>El duelo es la respuesta emocional de una persona ante la experiencia de una pérdida. El luto es el proceso de adaptarse a la vida después de una pérdida. Se ve influenciado por la sociedad, cultura y religión de la persona.
                                El pesar es el estado de haber experimentado una pérdida.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/sentimientos-y-el-cancer/duelo_en_cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Entender el duelo y la pérdida</h2>
                            <p>Las reacciones ante una pérdida se llaman reacciones del duelo. Varían mucho de persona en persona e incluso en la misma persona a lo largo del tiempo. Las reacciones frecuentes del duelo incluyen sentimientos, pensamientos,
                                sensaciones físicas y comportamientos complicados.</p>
                            <p>Sentimientos. Las personas que experimentaron una pérdida pueden tener una gran variedad de sentimientos. Estos pueden incluir conmoción, bloqueo, tristeza, negación, desesperanza, ansiedad, enojo, culpa, soledad, depresión,
                                impotencia, alivio y anhelo. Una persona en duelo puede comenzar a llorar después de escuchar una canción o comentario que los haga pensar en la persona que falleció. O la persona puede no saber qué desencadenó su llanto.</p>
                            <p>Pensamientos. Algunos patrones de pensamiento frecuentes incluyen rechazo, confusión, problemas para concentrarse, preocupaciones y alucinaciones.</p>
                            <p>Sensaciones físicas. El duelo puede causar muchas sensaciones físicas. Estas incluyen opresión o peso en el pecho o garganta, náuseas o malestar estomacal, mareos, dolores de cabeza, entumecimiento físico, músculos debilitados
                                o tensionados y fatiga. También puede hacerlo vulnerable a las enfermedades.</p>
                            <p>Comportamientos. Una persona en duelo puede tener problemas para dormirse o permanecer dormido. También puede perder la energía para realizar actividades agradables. La persona puede perder interés en la comida o en socializar.
                                Una persona en duelo puede volverse más irritable o agresiva. Otros comportamientos frecuentes incluyen inquietud y actividad excesiva.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Experimentar el Duelo</h2>
                            <p>Cada persona experimenta el duelo de una forma diferente. Generalmente, una persona siente el duelo en olas o ciclos. Esto significa que hay períodos de sentimientos dolorosos e intensos que aparecen y desaparecen. Las personas
                                pueden sentir que están progresando en el duelo cuando sienten menos dolor de forma temporal. Pero después de un tiempo, pueden volver a atravesar el duelo. Tales cambios en el duelo pueden ocurrir en fechas importantes,
                                como fiestas o cumpleaños. A lo largo del tiempo, algunas personas experimentan estos ciclos de duelo con menor frecuencia mientras se adaptan a la pérdida.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Tareas de Luto</h2>
                            <p>Existen diferentes teorías sobre cómo las personas se adaptan a la pérdida. Un modelo altamente aceptado describe 4 tareas del luto:</p>
                            <ul class="secciones-enumeradas">
                                <li>Aceptar la realidad de la pérdida</li>
                                <li>Experimentar el dolor del duelo</li>
                                <li>Adaptarse a la vida sin la presencia física de la persona</li>
                                <li>Encontrar nuevas maneras de mantener la conexión con la persona que falleciós</li>
                            </ul>
                        </div>
