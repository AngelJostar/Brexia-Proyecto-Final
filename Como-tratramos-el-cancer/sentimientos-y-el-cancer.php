                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Ansiedad</h2>
                        <div class="font-16 fontW-400">
                            <p>La ansiedad puede describirse como una sensación de nerviosismo, tensión o preocupación. Es una emoción normal que alerta al cuerpo para que responda ante una amenaza. Sin embargo, la ansiedad intensa y prolongada es un trastorno
                                que puede interferir en sus relaciones y actividades cotidianas.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/sentimientos-y-el-cancer/ansiedad_cancer_de_mama-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Muchas personas con cáncer experimentan síntomas de ansiedad. Un diagnóstico de cáncer puede generar estos sentimientos:</p>
                            <ul class="secciones-enumeradas">
                                <li>Temor a los efectos secundarios del tratamiento o relacionados con este</li>
                                <li>Temor a una recaída del cáncer o a su diseminación después del tratamiento</li>
                                <li>Incertidumbre</li>
                                <li>Preocupación por perder la independencia</li>
                                <li>Inquietud sobre cambios en las relaciones</li>
                                <li>Temor a la muerte</li>
                            </ul>
                            <p>Es posible que la ansiedad dificulte afrontar el tratamiento del cáncer. También puede interferir en su capacidad de tomar decisiones sobre su atención. En consecuencia, la identificación y el tratamiento de la ansiedad son
                                aspectos importantes del tratamiento del cáncer.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas de ansiedad aguda</h2>
                            <p>Es posible que con frecuencia tenga breves períodos de los síntomas enumerados a continuación. Un ataque de pánico se produce cuando una persona tiene todos estos síntomas a la vez:</p>
                            <ul class="secciones-enumeradas">
                                <li>Una sensación de miedo o temor intensos.</li>
                                <li>Una sensación de desapego de usted mismo o de su entorno</li>
                                <li>Palpitaciones o latidos cardíacos acelerados</li>
                                <li>Presión arterial alta</li>
                                <li>Dolor torácico</li>
                                <li>Falta de aliento</li>
                                <li>Una sensación de ahogo</li>
                                <li>Sudoración</li>
                                <li>Escalofríos</li>
                                <li>Mareos o desvanecimiento</li>
                                <li>Temblores</li>
                                <li>Náuseas, diarrea, acidez estomacal o cambios en el apetito</li>
                                <li>Dolor abdominal</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas de ansiedad crónica</h2>
                            <p>La ansiedad crónica puede incluir episodios de ansiedad aguda, junto con uno o más de los siguientes síntomas. Generalmente, duran más tiempo:</p>
                            <ul class="secciones-enumeradas">
                                <li>Preocupación excesiva</li>
                                <li>Nerviosismo</li>
                                <li>Tensión muscular</li>
                                <li>Insomnio, que es la imposibilidad de conciliar el sueño o de permanecer dormido</li>
                                <li>Irritabilidad</li>
                                <li>Fatiga</li>
                                <li>Dificultad para concentrarse</li>
                                <li>Indecisión, que es la dificultad para tomar decisiones</li>
                            </ul>
                            <p>Es importante que informe a su médico o a otro integrante de su equipo de atención médica si experimenta alguno de estos síntomas. Algunos pueden ser efectos secundarios del cáncer o del tratamiento del cáncer. Por ejemplo,
                                fatiga, insomnio y dificultad para concentrarse son efectos secundarios frecuentes del cáncer y de su tratamiento.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Tipos de Tratamiento contra la Ansiedad</h2>
                            <ol class="secciones-enumeradas">
                                <li>Técnicas de relajación Pueden usarse técnicas de relajación solas o para complementar otros tipos de tratamiento. Pueden implementarse algunos de los siguientes métodos con un poco de orientación: Respiración profunda,
                                    Relajación muscular progresiva, Meditación, Biorretroalimentación, Yoga.</li>
                                <li>Tratamiento psicológico Brindan herramientas para mejorar las habilidades para sobrellevar la enfermedad, desarrollar un sistema de apoyo y modificar los pensamientos negativos. Las opciones incluyen la terapia individual,
                                    de pareja o familiar, y la terapia de grupo.</li>
                                <li>Medicamentos: Si sus síntomas de ansiedad son de moderados a graves, es posible que los medicamentos lo beneficien. Existen distintos tipos de medicamentos disponibles. Su médico elegirá el medicamento más apropiado.</li>
                            </ol>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Seguimiento Integral</h2>
                            <p>Tras una derivación a un profesional de la salud mental, su oncólogo probablemente querrá hablar con usted sobre la efectividad del tratamiento y sobre sus efectos secundarios. Usted y su médico pueden abordar estas opciones
                                con anterioridad, si es necesario.</p>
