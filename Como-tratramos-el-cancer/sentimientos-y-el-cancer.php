                                mejorando las consecuencias médicas de la enfermedad en el organismo. Y a nivel emocional disminuyendo las sensaciones de aislamiento, indefensión y abandono, o reduciendo la ansiedad y la tristeza.</p>
                            <p>De igual modo, es muy importante para tu bienestar psicológico, que dispongas de una información clara y correcta sobre la enfermedad y sus consecuencias emocionales.</p>
