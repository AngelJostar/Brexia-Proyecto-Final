                            <p>Otro sistema de estadificación que se usa para todos los tipos de cáncer agrupa el cáncer en una categoría principal de cinco existentes. Este sistema de estadificación lo usan con más frecuencia los registros de cáncer que los doctores. Pero, usted puede oír a su doctor o a su enfermera que describen su cáncer en una de las formas siguientes:</p>
                            <ul class="secciones-enumeradas">
                                <li>In situ: Hay células anormales presentes pero no se han diseminado a tejido cercano</li>
                                <li>Localizado: El cáncer se ha limitado al lugar en donde empezó, sin indicios de haberse diseminado</li>
