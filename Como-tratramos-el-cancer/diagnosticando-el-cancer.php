                            <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Cuál es la diferencia entre cura y remisión?</h2>
                            <p>La cura significa que no hay rastros de cáncer después del tratamiento y que el cáncer nunca volverá.</p>
                            <p>La remisión significa que los signos y síntomas del cáncer se han reducido. La remisión puede ser parcial o completa. En una remisión completa, todos los signos y síntomas del cáncer han desaparecido.</p>
                            <p>Si usted presenta una remisión completa por 5 años o más, es posible que algunos médicos digan que se ha curado. Pero algunas células cancerosas pueden todavía permanecer en su cuerpo durante muchos años después del tratamiento. Estas células pueden causar que un día vuelva a tener cáncer. La mayoría de los tipos de cáncer generalmente vuelven dentro de los primeros 5 años después del tratamiento. Pero también existe la posibilidad de que el cáncer vuelva más allá de ese período. Por esta razón, los médicos no pueden decirle con seguridad que se ha curado. Lo más que pueden decir es que no hay signos de cáncer en ese momento.</p>
                            <p>Debido a que es posible que el cáncer vuelva a aparecer, el médico le hará controles durante muchos años y ordenará exámenes para detectar signos de si el cáncer ha regresado. También se evaluará si hay signos de efectos secundarios tardíos de los tratamientos que usted recibió contra el cáncer.</p>
