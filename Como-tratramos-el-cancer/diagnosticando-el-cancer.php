                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Biopsia para diagnosticar Cáncer</h2>
                        <div class="font-16 fontW-400">
                            <p>En la mayoría de los casos, los doctores deben hacer biopsias para diagnosticar el cáncer. Una biopsia es un procedimiento en el que el doctor extrae una muestra de tejido. Un patólogo observa el tejido al microscopio y realiza otras pruebas para ver si el tejido es canceroso. El patólogo describe los hallazgos en un informe de patología, el cual contiene los detalles del diagnóstico. Los informes de patología cumplen una función importante en el diagnóstico de cáncer y ayudan a decidir entre las opciones de tratamiento.</p>
                            <p>Las muestras para biopsia pueden obtenerse de varias maneras:</p>
                            <p>Con una aguja: El doctor usa una aguja para extraer tejido o fluido. Este método se usa para realizar aspiración de médula ósea, punciónes lumbares y algunas biopsias de seno, de próstata y de hígado.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/diagnosticando-el-cancer/05Biopsia_cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Con endoscopia: El doctor usa una sonda iluminada, delgada, denominada endoscopio para examinar áreas del interior del cuerpo. El endoscopio se inserta a través de las aberturas naturales del cuerpo, como la boca o el ano. Si durante el examen el doctor observa que hay tejido anormal, lo extirpa por el endoscopio junto con algo de tejido normal circundante. Sos ejemplos de biopsia con endoscopia son la colonoscopia y la broncoscopia.</p>
                            <p>Con cirugía: El cirujano extirpa el área con células anormales durante una operación quirúrgica. La cirugía puede ser de escisión o de incisión.  Si la biopsia es de escisión, el cirujano extirpa toda el área de células anormales. Generalmente, se extirpa también algo del tejido normal alrededor de estas células.Si la biopsia es de incisión, el cirujano extirpa solo una parte del área de células anormales.</p>
