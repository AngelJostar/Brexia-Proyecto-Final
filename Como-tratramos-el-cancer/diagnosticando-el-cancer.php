                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Pruebas con imágenes en cáncer</h2>
                        <div class="font-16 fontW-400">
                            <p>Las pruebas con imágenes reproducen áreas internas del cuerpo que ayudan al doctor a detectar la presencia de un tumor. Estas imágenes se pueden hacer de diferentes formas:</p>
                            <p>Tomografía computarizada (TC): La exploración con tomografía computarizada usa una máquina de rayos x conectada a una computadora para tomar una serie de imágenes de los órganos desde diferentes ángulos. En ocasiones debe administrarse un colorante u otro material de contraste antes de realizarse la prueba. Durante la exploración con tomografía computarizada, el paciente debe permanecer inmóvil acostado en una camilla que se desliza dentro de un escáner con forma de dona. La máquina de tomografía computarizada se mueve a su alrededor, tomando imágenes.</p>
                            
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/diagnosticando-el-cancer/06Pruebas_de_imagen_Cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Resonancia magnética: La resonancia magnética usa un potente imán y ondas de radio para tomar imágenes del cuerpo en secciones. Estas secciones se usan para crear imágenes detalladas del interior del cuerpo, en las que se pueden distinguir los tejidos sanos de los enfermos. Cuando se realiza una resonancia magnética, el paciente debe permanecer inmóvil acostado en una camilla que se introduce en una cámara larga y redondeada. La máquina de resonancia magnética produce sonidos rítmicos y golpeteo fuerte. En ocasiones, es necesario inyectar un colorante especial en una vena antes o durante la prueba de resonancia magnética. Este colorante, que se dice medio de contraste, puede hacer que los tumores se destaquen más brillantes en las imágenes.</p>
                            <p>Tomografía nuclear: La tomografía nuclear usa material radioactivo para tomar imágenes del interior del cuerpo. Este tipo de exploración puede llamarse también gammagrafía. Antes de esta exploración, usted recibe una inyección de una pequeña cantidad de material radioactivo, al que a veces se le llama marcador radioactivo. Este material fluye por el torrente sanguíneo y se acumula en algunos huesos u órganos.  Durante la exploración, el paciente debe mantenerse inmóvil en una camilla mientras la máquina, llamada escáner, detecta y mide la radioactividad del cuerpo, y así crea imágenes de huesos u órganos en la pantalla de una computadora o en una filmación. Después de la exploración, el material radioactivo en el cuerpo perderá su radioactividad con el pasar del tiempo. Es también posible que el cuerpo lo elimine por la orina o las heces.</p>
                            <p>Gammagrafía ósea: La gammagrafía ósea es un tipo de tomografía nuclear que estudia la presencia de áreas dañadas o anormales en los huesos. Puede usarse para diagnosticar cáncer de hueso u otro cáncer que se haya diseminado a los huesos (llamado también tumor metastático de hueso).</p>
                            <p>Tomografía por emisión de positrones (TEP): La tomografía por emisión de positrones es un tipo de tomografía nuclear que produce imágenes en 3-D detalladas del interior del cuerpo donde se absorbe la glucosa. Debido a que las células cancerosas a menudo usan más glucosa que las células normales, las imágenes se pueden usar para encontrar el cáncer en el cuerpo. Antes del escanograma, el paciente recibe una inyección de un marcador radioactivo llamado glucosa radioactiva. Durante el escanograma, el paciente debe permanecer inmóvil en una camilla que se mueve hacia adelante y hacia atrás en el interior de un escáner.</p>
                            <p>Ecografía: La ecografía usa ondas de sonido de alta energía que las personas no pueden escuchar. Las ondas de sonido "hacen eco" en los tejidos dentro del cuerpo. Una computadora usa el eco producido para generar imágenes de áreas del interior del cuerpo. A esta técnica se le llama ultrasonido o sonograma.</p>
                            <p>Rayos x: Los rayos x usan dosis bajas de radiación para generar imágenes del interior del cuerpo. Un auxiliar técnico de rayos x coloca al paciente en posición y dirige el haz de rayos x a la parte pertinente del cuerpo. Mientras se toman las imágenes, el paciente debe permanecer inmóvil y quizá deba dejar de respirar por uno o dos segundos.</p>
