                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cáncer</h2>
                        <div class="font-16 fontW-400">
                            <p>El estadio se refiere a la extensión de su cáncer, es decir, qué tan grande es el tumor y si se ha extendido. Cuando el doctor conoce el estadio de su cáncer, le ayudará a:</p>
                            <ul class="secciones-enumeradas">
                                <li>Entender la gravedad de su cáncer y la posibilidad de que usted sobreviva</li>
                                <li>Hacer el mejor plan de tratamiento para usted</li>
                            </ul>
                            <p>Al referirse a un cáncer, los doctores mencionan siempre el estadio asignado al cáncer al momento del diagnóstico, aun cuando empeore o se extienda. Cualquier información nueva de cambios en el cáncer con el tiempo se añade a la designación original del estadio. De esta forma, el estadio no cambia, aun cuando el cáncer pueda cambiar.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/diagnosticando-el-cancer/07estadios_cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Cómo se determina el estadio?</h2>
                            <p>Para conocer el estadio de su enfermedad, su doctor puede ordenar rayos X, análisis de laboratorio y otros exámenes o procedimientos. Vea la sección sobre “Diagnóstico en general” para saber más de estas pruebas.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Sistemas que describen el estadio</h2>
                            <p>Hay muchos sistemas de estadificación. Algunos, como el sistema TNM de estadificación, se usan para muchos tipos de cáncer. Otros son específicos para un tipo determinado de cáncer. La mayoría de los sistemas de estadificación incluyen información acerca de:</p>
                            <ul class="secciones-enumeradas">
                                <li>La ubicación del tumor en el cuerpo</li>
                                <li>El tipo de células</li>
                                <li>El tamaño del tumor</li>
                                <li>Si el cáncer se ha diseminado a los ganglios linfáticos cercanos</li>
                                <li>Si el cáncer se ha diseminado a una parte diferente del cuerpo</li>
                                <li>El grado del tumor, el cual se refiere al aspecto anormal de las células y de la probabilidad de que el tumor crezca y se disemine</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">El sistema TNM de estadificación</h2>
                            <p>El sistema TNM es el sistema de estadificación de cáncer de mayor uso. La mayoría de los hospitales y centros médicos usan el sistema TNM como método principal en sus informes de cáncer. Es probable que usted vea su cáncer descrito por este sistema de estadificación en su informe de patología, a menos que tenga un cáncer para el cual se usa un sistema diferente.</p>
                            <p>En el sistema TNM:</p>
                            <ul class="secciones-enumeradas">
                                <li>La T se refiere al tamaño y extensión del tumor principal. El tumor principal se llama de ordinario el tumor primario.</li>
                                <li>La N se refiere a la extensión de cáncer que se ha diseminado a los ganglios (o nódulos) linfáticos cercanos.</li>
                                <li>La M se refiere a si el cáncer se ha metastatizado; es decir, si ha tenido metástasis. Esto significa que el cáncer se ha diseminado desde el tumor primario a otras partes del cuerpo.</li>
                            </ul>
                            <p>Cuando su cáncer se describe por el sistema TNM, habrá números después de cada letra que dan más detalles acerca del cáncer. Lo que sigue explica el significado de letras y números:</p>
                            <p>Tumor primario (T) </p>
                            <ul class="secciones-enumeradas">
                                <li>TX: No puede medirse un tumor primario</li>
                                <li>T0: No puede encontrarse un tumor primario</li>
                                <li>T1, T2, T3, T4: Se refiere al tamaño y/o extensión del tumor principal. En cuanto más grande es el número después de la T, mayor es el tumor o tanto más ha crecido en los tejidos cercanos. Las T pueden dividirse todavía más para proveer más detalle, como T3a y T3b.</li>
                            </ul>
                            <p>Ganglios linfáticos regionales (N)</p>
                            <ul class="secciones-enumeradas">
                                <li>NX: No puede medirse el cáncer en los ganglios linfáticos cercanos</li>
                                <li>N0: No hay cáncer en los ganglios linfáticos cercanos</li>
                                <li>N1, N2, N3: Se refiere al número y ubicación de los ganglios linfáticos que tienen cáncer. En cuanto más grande es el número después de la N, más son los ganglios linfáticos que tienen cáncer.</li>
                            </ul>
                            <p>Metástasis distante (M)</p>
                            <ul class="secciones-enumeradas">
                               <li>MX: No puede medirse la metástasis</li>
                               <li>M0: El cáncer no se ha diseminado a otras partes del cuerpo</li>
                               <li>M1: El cáncer se ha diseminado a otras partes del cuerpo</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Otras formas de describir el estadio</h2>
