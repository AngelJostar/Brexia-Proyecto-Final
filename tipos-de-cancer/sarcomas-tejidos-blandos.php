                    <article id="tab1">
                        <h2 class="centrar-texto brexia-color encabezados-brexia paddingTop-2">¿Qué son los Sarcomas de Tejido Blando?</h2>
                        <div class="font-16 fontW-400">
                            <p>Los sarcomas de tejido blando definen a un grupo de tumores malignos que se forman cuando las células anormales crecen sin control en los tejidos blandos y los tejidos conectivos.</p>
                            <p>Los tejidos blandos pueden encontrarse en cualquier parte del cuerpo y comprenden músculos, tendones, grasa, vasos sanguíneos y linfáticos, nervios y revestimientos de las articulaciones (tejido sinovial).</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/sarcomas-de-tejido-blando/Tejido-blando-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>El tipo de sarcoma depende de la clase de células en las que se origina. Los tejidos conectivos comprenden todo el tejido que sostiene, conecta o separa a diferentes tejidos en el organismo. Precisamente por este motivo, puede
                                encontrarse en la estructura de órganos en el cuerpo (como, por ejemplo, en el útero). Los sarcomas de tejido blando, por lo tanto, pueden crecer prácticamente en cualquier zona, pero son más comunes en los brazos y las
                                piernas (50%), seguidos por el tronco y el abdomen (40%) y la cabeza y el cuello (10%).</p>
                        </div>
                    </article>
                    <article id="tab2">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo de los Sarcomas de Tejido Blando</h2>
                        <div class="font-16 fontW-400">
                            <p>Algunos factores de riesgo comunes para el sarcoma de tejidos blandos incluyen:</p>
                            <p>Edad: si bien el sarcoma de tejidos blandos puede ocurrir en personas de cualquier edad, se encuentra con mayor frecuencia en adultos mayores de 50 años.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/sarcomas-de-tejido-blando/Factores-de-Riesgo-de-los-Sarcomas-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Condiciones hereditarias: algunas condiciones genéticas hereditarias raras se consideran factores de riesgo para el sarcoma de tejido blando. Incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Poliposis adenomatosa familiar (PAF).</li>
                                <li>Retinoblastoma.</li>
                                <li>Neurofibromatosis tipo 1 (enfermedad de von Recklinghausen o NF1).</li>
                                <li>Síndrome de Li-Fraumeni.</li>
                            </ul>
                            <p>Exposición química: la exposición al cloruro de vinilo y la dioxina puede aumentar el riesgo de desarrollar sarcomas de tejidos blandos. El cloruro de vinilo se utiliza para fabricar tuberías de cloruro de polivinilo (PVC),
                                tapicería de automóviles, utensilios de cocina de plástico y revestimientos de alambre. Las dioxinas son un grupo de químicos que se forman durante la quema de desechos domésticos e industriales.</p>
                            <p>Radioterapia: en raras ocasiones, la radioterapia externa para tratar cualquier forma de cáncer puede conducir al desarrollo de sarcoma de tejidos blandos más adelante en el área tratada. Esto puede ocurrir entre cinco y 30
                                años después del tratamiento inicial.</p>
                            <p>Sistema inmunitario debilitado: las personas con VIH a veces desarrollan sarcoma de Kaposi, un tipo de sarcoma de tejido blando que se desarrolla a partir de las células que recubren los vasos sanguíneos o los vasos linfáticos.
                                Este tipo raro de sarcoma se trata de manera diferente a otros tipos más comunes de sarcoma de tejidos blandos.</p>
                        </div>
                    </article>
                    <article id="tab3">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas de los Sarcomas de Tejido Blando</h2>
                        <div class="font-16 fontW-400">
                            <p>Al igual que muchas formas de cáncer, los síntomas del sarcoma de tejido blando pueden aparecer en una etapa más avanzada de la enfermedad, o es posible que no se note ningún síntoma en absoluto. Los síntomas, si están presentes,
                                también varían según el tipo de sarcoma y su ubicación.</p>
                            <p>Los síntomas de sarcoma de tejidos blandos incluyen:</p>
                            <p>Un bulto o masa es el signo de sarcoma de tejido blando más común. El bulto se formará en el área donde está creciendo el tumor, y puede ir acompañado de algo de dolor si está presionando un nervio o músculo. Incluso si el
                                bulto no es doloroso, si continúa creciendo o si se encuentra en lo profundo de una extremidad o cavidad corporal, consulte a su médico.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/sarcomas-de-tejido-blando/SintomasTejido-Blando-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>La hinchazón incómoda es otro signo de sarcoma de tejido blando, especialmente cuando se encuentra en los brazos y las piernas.</p>
                            <p>La movilidad limitada puede ser un síntoma de sarcoma de tejidos blandos. Algunos tumores pueden restringir el movimiento, como los que se encuentran en la cadera, la rodilla, el hombro o las manos.</p>
                            <p>Las lesiones cutáneas pueden sufrir cuando un tumor de sarcoma atraviesa la piel.</p>
                            <p>Otros síntomas pueden ser signos de sarcoma de tejidos blandos, porque un tumor de sarcoma puede formarse en casi cualquier parte del cuerpo y, por lo tanto, puede afectar una variedad de órganos. Por ejemplo, los sarcomas
                                en el abdomen pueden causar dolor abdominal, vómitos o estreñimiento, mientras que los sarcomas en el útero pueden causar sangrado vaginal y / o dolor abdominal. Con los tumores del estroma gastrointestinal (GIST), puede
                                sentirse lleno después de comer solo comidas muy pequeñas, o puede vomitar sangre o tener deposiciones oscuras.</p>
                        </div>
                    </article>
                    <article id="tab4">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico de los Sarcomas de Tejido Blando</h2>
                        <div class="font-16 fontW-400">
                            <p>Desafortunadamente, los sarcomas pueden ser asintomáticos durante mucho tiempo y los síntomas dependen de la parte del cuerpo que se vea afectada. Puede sospecharse la presencia de sarcomas cuando aparece un bulto en una pierna,
                                en un brazo o en el tronco.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/sarcomas-de-tejido-blando/Cancer-de-Tejido-Blando-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Lo exámenes radiológicos son obligatorios para determinar el alcance de un sarcoma de tejido blando y para establecer la presencia o ausencia de metástasis distantes.</p>
                            <p>Deberá obtenerse una muestra del tumor (biopsia) para llevar a cabo un análisis en el laboratorio para confirmar el diagnóstico y conocer más detalles sobre el tipo de sarcoma.</p>
                        </div>
                    </article>
                    <article id="tab5">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento de los Sarcomas de Tejido Blando</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar los sarcomas de tejidos blandos son:</p>
                            <p>Para los sarcomas localizados que están confinados en su sitio primario y no se han extendido a los tejidos cercanos ni a otras áreas del cuerpo, los tratamientos son:</p>
                            <ol class="secciones-enumeradas">
                                <li>La extirpación del tumor por medio de cirugía es el tratamiento estándar.</li>
                                <li>La radioterapia y la quimioterapia tanto separadamente como combinadas después de la cirugía, a veces pueden usarse para aumentar las posibilidades de cura definitiva o reducir el riesgo de que el tumor vuelva a aparecer.</li>
                                <li>La radioterapia puede usarse antes de la cirugía para reducir el tamaño del tumor y permitir que sea extirpado completamente.
                                </li>
                            </ol>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/sarcomas-de-tejido-blando/Tratamiento-cancer-tejido-blando-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Para los sarcomas avanzados que se han diseminado desde donde aparecieron en primer lugar a otras partes del cuerpo (esto se conoce como cáncer metastásico o avanzado). Los tratamientos son:</p>
                            <ol class="secciones-enumeradas">
                                <li>El enfoque de tratamiento principal es el uso de quimioterapia y terapia dirigida molecular. La elección de los medicamentos dependerá principalmente de las condiciones clínicas del paciente y del tipo de sarcoma.</li>
                                <li>La radioterapia, tanto durante la quimioterapia como después de la misma, podría usarse para aliviar los síntomas y controlar las metástasis.</li>
                                <li>La cirugía podría usarse para aliviar los síntomas o para curar el cáncer en algunos casos específicos.</li>
                            </ol>
                            <p>Seguimiento</p>
                            <ol class="secciones-enumeradas">
                                <li>Las citas del seguimiento incluyen exámenes físicos, análisis de sangre y exámenes radiológicos; todos estos tipos de pruebas se realizarán durante varios años.</li>
                                <li>El seguimiento tras el tratamiento para el sarcoma de tejido blando de grado alto o intermedio será más intensivo que el seguimiento para el sarcoma de grado bajo.</li>
                            </ol>

                        </div>
