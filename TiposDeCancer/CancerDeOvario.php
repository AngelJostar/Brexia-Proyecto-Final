                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué subtipos de cáncer epitelial de ovario hay?</h2>
                        <div class="font-16 fontW-400">
                            <p>Los cuatro principales subtipos histológicos de cáncer epitelial de ovario son:</p>
                            <ol class="secciones-enumeradas">
                                <li>Carcinoma seroso: éste es el subtipo más común y representa alrededor del 80% de los cánceres de ovario avanzados. Estos cánceres se subdividen a su vez en tumores de grado alto y tumores de grado bajo; los tumores de gradobajo
                                    representan aproximadamente el 10% de los carcinomas serosos, tienden a aparecer en mujeres más jóvenes y tienen un mejor pronóstico.</li>
                                <li>Mucinoso: este subtipo representa el 7-14% de todos los cánceres epiteliales de ovario primarios. El pronóstico para este subtipo es muy bueno si se diagnostica en una etapa temprana.</li>
                                <li>Endometrioide: este subtipo es el responsable del cáncer de ovario en alrededor del 10% de las mujeres que lo tienen. Normalmente se trata tumores de grado bajo que se diagnostican temprano.</li>
                                <li>Cánceres de células claras: alrededor del 5% de las mujeres con cáncer de ovario tiene este subtipo, aunque el porcentaje varía dependiendo de en qué parte del mundo se encuentren. El pronóstico para este subtipo es bastante
                                    bueno si se diagnostica temprano.</li>
                            </ol>
