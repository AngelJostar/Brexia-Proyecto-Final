                    <article id="tab3">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Ovario</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento del cáncer de ovario varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer están capacitados y tienen experiencia
                                en los tratamientos disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar cáncer de ovario son:</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-ovario/Tratamiento_cancer_de_ovario-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <ul class="secciones-enumeradas">
                                <li>Cirugía: es la piedra angular del manejo del cáncer epitelial de ovario en sus primeras etapas. El cáncer de ovario epitelial avanzado o de alto riesgo se trata principalmente con cirugía y quimioterapia aunque en algunos
                                    casos específicos también se utilizan terapias dirigidas.</li>
                                <li>Quimioterapia: Uso de medicamentos contra el cáncer para destruir las células cancerosas. La quimioterapia puede administrarse sola o en combinación con otros tratamientos.</li>
                                <li>Terapia dirigida: Fármacos más recientes que actúan bloqueando las señales que indican a las células cancerosas que crezcan o interfiriendo con su capacidad para obtener los nutrientes necesarios para su crecimiento.</li>
                            </ul>
                            <p>El cáncer de ovario se “estadifica” de acuerdo con el tamaño del tumor, el compromiso de los ganglios linfáticos y el hecho de que se haya diseminado a otras partes del cuerpo fuera de la cavidad abdominal. Esta información
                                se utiliza para ayudar a decidir el mejor tratamiento.</p>
                            <p>Estadio inicial del cáncer epitelial de ovario</p>
                            <ul class="secciones-enumeradas">
                                <li>Será muy frecuente que a las mujeres con la enfermedad en estadio I que sean consideradas en riesgo intermedio o alto de recidiva del cáncer se les administre quimioterapia después de la cirugía</li>
                            </ul>
                            <p>Cáncer epitelial de ovario localmente avanzado y metastásico</p>
                            <ul class="secciones-enumeradas">
                                <li>Todas las mujeres cuyo cáncer epitelial de ovario haya sido clasificado como cáncer en estadio II, III o IV deben recibir quimioterapia después de la cirugía. El tratamiento estándar es un régimen de dos medicamentos.</li>
                                <li>Para algunas mujeres a las que se les haya diagnosticado recientemente cáncer epitelial de ovario en estadio III B, III C o IV, a la quimioterapia estándar puede añadirse una terapia dirigida.</li>
                            </ul>
                            <p>Cáncer de ovario epitelial recurrente</p>
                            <ul class="secciones-enumeradas">
                                <li>Este tipo de cáncer se tratará con quimioterapia y los fármacos y régimen específicos que se usen dependerán de la rapidez con la que el cáncer haya regresado y de su sensibilidad al tratamiento.</li>
                                <li>La terapia dirigida puede administrarse en combinación con una quimioterapia doble o una quimioterapia de un solo agente a mujeres que hayan sufrido una recidiva, dependiendo de lo sensible que fuera el tumor a los tratamientos
                                    previos.
                                </li>
                                <li>Existen opciones de distintos tipos de terapia dirigida llamada si el cáncer dio positivo en la mutación del gen BRCA1 o BRCA2 y respondió a la quimioterapia basada en platino. Esto se hace para ayudar a mantener la respuesta
                                    durante tanto tiempo como sea posible.</li>
                            </ul>
                            <p>Seguimiento después del tratamiento</p>
                            <ul class="secciones-enumeradas">
                                <li>En cada visita, su médico la examinará y también podrá hacer un examen pélvico, solicitar análisis de sangre y/o solicitar una tomografía computarizada o una tomografía por emisión de positrones (TEP) para ver si el cáncer
                                    ha regresado y, de ser así, cuál es el mejor tratamiento.</li>
                            </ul>
                        </div>
