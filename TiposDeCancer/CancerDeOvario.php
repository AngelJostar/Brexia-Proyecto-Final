                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cáncer de Ovario</h2>
                        <div class="font-16 fontW-400">
                            <p>La estatificación del cáncer se utiliza para describir su tamaño y posición y si se ha diseminado desde donde comenzó. Para los cánceres de ovario, el sistema utilizado se denomina “sistema de estadificación FIGO” y el cáncer
                                se estadifica mediante el examen del tejido extraído durante una operación. Este procedimiento se denomina estadificación quirúrgica, y significa que los médicos a menudo no pueden saber con certeza en qué estadio se encuentra
                                el cáncer hasta después de haber realizado la cirugía.</p>
                            <p>El cáncer se estadifica utilizando una secuencia de letras y números. En el sistema de estadificación FIGO existen cuatro estadios designados con números romanos del I al IV. En general, cuanto más bajo sea el nivel, mejor
                                será el pronóstico. La estadificación toma en consideración:</p>
                            <ul class="secciones-enumeradas">
                                <li>La dimensión del cáncer, o el tamaño del tumor (T)</li>
                                <li>Si el cáncer se ha diseminado a los ganglios linfáticos (N)</li>
                                <li>Si se ha diseminado a sitios distantes, lo que se conoce como “‘metástasis” (M)</li>
                            </ul>
                            <p>En el caso del cáncer epitelial de ovario, la estadificación se realiza durante la cirugía. Antes de la cirugía, el uso de imágenes de exploración a través de TC o IRM es esencial para permitir al cirujano planear la operación
                                de la mejor manera posible. Durante la cirugía, se toman muestras del tumor y se envían al laboratorio para realizar las pruebas de subtipo histológico y determinar el subtipo de cáncer epitelial de ovario.</p>
                            <p>La siguiente tabla describe los diferentes estadios del cáncer de ovario, incluyendo al cáncer epitelial de ovario.</p>
