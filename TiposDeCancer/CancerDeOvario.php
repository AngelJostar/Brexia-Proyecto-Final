                    <article id="tab2">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer Epitelial de Ovario</h2>
                        <div class="font-16 fontW-400">
                            <p>Es más probable que a una mujer se le diagnostique cáncer epitelial de ovario cuando el cáncer ya está en estado avanzado, porque las etapas tempranas de la enfermedad normalmente no presentan síntomas. La paciente puede haber
                                notado hinchazón y malestar abdominal o, en algunos casos, puede que perciba la presencia de ganglios linfáticos inflamados en las ingles, en las axilas o en el cuello, justo encima de la clavícula.</p>
                            <p>El diagnóstico definitivo sólo puede efectuarse después de la cirugía, pero las investigaciones iniciales comienzan con un examen físico, una ecografía abdominal y unos análisis de sangre, seguidos de una tomografía computarizada
                                (TC) para planificar la cirugía.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-ovario/Diagnostic_cancer_de_ovario-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Las diferentes técnicas de obtención de imagen utilizadas para las mujeres que se sospecha que puedan padecer cáncer de ovario se explican a continuación:</p>
                            <ol class="secciones-enumeradas">
                                <li>Ecografía: Una ecografía realizada con un instrumento especial que se inserta en su vaginale dará al médico la posibilidad de examinar el tamaño, la forma y otras características específicas de sus ovarios que se sabe que
                                    están asociadas con el cáncer epitelial de ovario.</li>
                                <li>Tomografía computarizada (TC): Se trata de un tipo de “radiografía tridimensional” que el equipo de especialistas puede utilizar para determinar la extensión del cáncer y para planificar la cirugía cuando ésta resulte apropiada.
                                    Es un procedimiento indoloro que requiere de 10 a 30 minutos.</li>
                                <li>Radiografía de tórax: Una radiografía de tórax es una alternativa a la tomografía computarizada que el especialista puede utilizar para comprobar si se ha producido una diseminación del cáncer epitelial de ovario a los
                                    pulmones y la cavidad torácica.</li>
                                <li>Imágenes por resonancia magnética (IRM): Aunque no forma parte de las investigaciones de rutina, la resonancia magnética puede utilizarse en lugar de la tomografía computarizada para planificar la cirugía. La IRM utiliza
                                    potentes campos magnéticos y ondas de radio para producir imágenes detalladas del interior del cuerpo. El escáner de IRM es un tubo grande, similar a un escáner de TC, que contiene unos potentes imanes. La paciente
                                    permanece tumbada en el interior del tubo durante el examen, que dura de 15 a 90 minutos.</li>

                            </ol>
                        </div>
