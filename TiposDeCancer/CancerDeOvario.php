                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Cuáles son los síntomas?</h2>
                        <div class="font-16 fontW-400">
                            <p>En sus primeros estadios, el cáncer epitelial de ovario puede tener pocos o ningún síntoma, lo cual dificulta el diagnóstico. Los síntomas se observan más comúnmente en una fase avanzada de la enfermedad y pueden incluir, en
                                todos los estadios:</p>
                            <ul class="secciones-enumeradas">
                                <li>Dolor abdominal o pélvico.</li>
                                <li>Sangrado vaginal.</li>
                                <li>Estreñimiento.</li>
                                <li>Abdomen distendido.</li>
                                <li>Sensación de cansancio extremo.</li>
                                <li>Necesidad frecuente de orinar.</li>
                            </ul>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-ovario/sintomas_cancer_de_ovario-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>En el cáncer epitelial de ovario avanzado:</p>
                            <ul class="secciones-enumeradas">
                                <li>Aumento de la circunferencia abdominal (las faldas o pantalones pueden quedar más estrechos).</li>
                                <li>Hinchazón abdominal.</li>
                                <li>Sensación de nausea.</li>
                                <li>Pérdida del apetito.</li>
                                <li>Indigestión.</li>
                                <li>Sentirse llena poco después de comenzar a comer.</li>
                                <li>Dificultad para respirar.</li>
                            </ul>
                            <p>Deberá ver a su médico si experimenta cualquiera de estos síntomas. Sin embargo, es importante recordar que estos síntomas son comunes en las personas que no tienen cáncer epitelial de ovario y que también pueden ser causados
                                por otras enfermedades.</p>
                        </div>
