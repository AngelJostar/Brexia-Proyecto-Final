                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Ovario</h2>
                        <div class="font-16 fontW-400">
                            <p>Si bien es posible que no se conozca la causa exacta del cáncer de ovario, varios factores pueden aumentar el riesgo de desarrollar la enfermedad. La probabilidad de desarrollar la enfermedad puede ser mayor si una mujer tiene
                                uno o más factores de riesgo ovárico. Un factor de riesgo clave es la edad. La mayoría de las mujeres que desarrollan cáncer de ovario son diagnosticadas después de la menopausia, a la edad de 55 años o más, aunque las
                                pacientes de 40 y 50 años también han sido diagnosticadas con la enfermedad.</p>
                            <p>Si tiene un historial familiar fuerte de cáncer de mama u ovario, también puede tener un mayor riesgo. Hable con su médico sobre el asesoramiento genético, las pruebas genéticas y otros pasos que puede seguir para controlar
                                o reducir su riesgo de cáncer de ovario, como la cirugía preventiva.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-ovario/factores_cancer_de_ovario-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Cuáles son los síntomas?</h2>
                            <ol class="secciones-enumeradas">
                                <li>General</li>
                                <ol type="a">
                                    <li>Edad: dos tercios de las mujeres diagnosticadas con cáncer de ovario tienen 55 años o más.</li>
                                </ol>
                                <li>Genética</li>
                                <ol type="a">
                                    <li>Antecedentes familiares: las mujeres con una madre, hermana, abuela o tía que han tenido cáncer de ovario tienen un mayor riesgo de desarrollar la enfermedad.</li>
                                    <li>Mutaciones genéticas: algunas mujeres que desarrollan cáncer de ovario tienen una mutación hereditaria en uno de los dos genes llamados gen de cáncer de seno 1 (BRCA1) y gen de cáncer de seno 2 (BRCA2). Las mujeres
                                        con la mutación BRCA1 tienen un riesgo de 35 a 70 por ciento más alto de cáncer de ovario. Las mujeres con la mutación BRCA2 tienen un riesgo de 10 a 30 por ciento más alto. Sin embargo, la gran mayoría de las mujeres
                                        diagnosticadas con cáncer de ovario no tienen ninguna mutación. Si le preocupa este factor de riesgo de cáncer de ovario, hable con su obstetra ginecólogo o con un oncólogo ginecológico para hacerse la prueba de
                                        ambas mutaciones BRCA.</li>
                                    <li>Síndrome de Lynch y síndrome de Peutz-Jeghers: las mujeres que tienen estos trastornos genéticos hereditarios tienen un mayor riesgo de desarrollar cáncer de ovario. El síndrome de Lynch se caracteriza por un mayor
                                        riesgo de cáncer del tracto digestivo, el tracto ginecológico y otros órganos. El síndrome de Peutz-Jeghers indica un mayor riesgo de desarrollar pólipos en el tracto digestivo y varios tipos de cáncer, incluidos
                                        los de mama, colon, recto, páncreas, estómago, testículos, ovarios, pulmones y cuello uterino.</li>
                                </ol>
                                <li>Condiciones previas</li>
                                <ol type="a">
                                    <li>Cáncer de mama, colorrectal o endometrial: las mujeres que han sido diagnosticadas con estos cánceres tienen un mayor riesgo de desarrollar cáncer de ovario.</li>
                                </ol>
                                <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Cuáles son los síntomas?</h2>
                                <ol class="secciones-enumeradas">
                                    <li>Estado de maternidad: las mujeres que han dado a luz al menos un hijo, especialmente antes de los 30 años, tienen un riesgo menor de desarrollar la enfermedad. Cuantos más hijos tiene una mujer, más disminuye su riesgo
                                        de cáncer de ovario. Las mujeres que amamantan reducen aún más su riesgo.</li>
                                    <li>Control de la natalidad: las mujeres que han usado anticonceptivos orales durante al menos tres meses tienen un menor riesgo de cáncer de ovario. El riesgo es menor cuanto más tiempo se toman los anticonceptivos. El
                                        riesgo más bajo continúa durante muchos años después de suspender los anticonceptivos.</li>
                                    <li>Cirugía ginecológica: una ligadura de trompas (atando las trompas de Falopio) o una histerectomía (extirpando el útero pero no los ovarios) reduce el riesgo de desarrollar cáncer de ovario.</li>
                                </ol>
                            </ol>
