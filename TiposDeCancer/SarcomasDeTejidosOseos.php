                    <article id="tab3">
                        <h2 class="centrar-texto brexia-color encabezados-brexia">Síntomas de los Sarcomas Óseos</h2>
                        <div class="font-16 fontW-400">
                            <p> Los tumores pueden ocurrir en cualquier hueso del cuerpo. El cáncer de hueso, también conocido como osteosarcoma, ocurre con mayor frecuencia en los huesos largos de los brazos y las piernas. Muchos síntomas de cáncer de hueso también pueden ser causados
                            ​​por afecciones como artritis, osteoporosis o lesiones. Si experimenta uno o más de estos síntomas, debe discutirlos con su médico para identificar la causa y recibir el tratamiento correcto, si es necesario.</p>
                            <div class="imagen-80 centrar-imagen">
                                <img src="/img/sarcomas-oseos/Sintomas-Sarcomas.webp" alt="Cancer de prostata">
                            </div>
                            <p>Los posibles síntomas de cáncer de hueso incluyen:</p>
                            <p>Dolor óseo: el dolor es el signo más común de cáncer de hueso y puede volverse más notorio a medida que el tumor crece. El dolor óseo puede causar un dolor sordo o profundo en un hueso o región ósea (por ejemplo, espalda, pelvis,
                                piernas, costillas, brazos). Al principio, el dolor solo puede ocurrir por la noche o cuando está activo. Sin embargo, a medida que se desarrolla el cáncer, el dolor puede volverse más persistente. Otras afecciones, como
                                la osteoporosis o la artritis, también pueden causar dolor en los huesos o las articulaciones.</p>
                            <p>Hinchazón: el área donde se localiza el dolor puede comenzar a mostrar signos de hinchazón, o puede haber un bulto o masa.</p>
                            <p>Fracturas: las células cancerosas pueden debilitar el hueso y esto a veces puede provocar una fractura. La ruptura puede ocurrir en un área del hueso que previamente había estado adolorida o dolorosa por un período de tiempo.</p>
                            <p>Disminución de la movilidad: en algunos casos, si la ubicación del tumor está cerca de una articulación, puede hacer que los movimientos normales sean difíciles o dolorosos.</p>
                            <p>Otros síntomas: la pérdida involuntaria de peso y la fatiga que acompaña al dolor óseo pueden ser un signo de cáncer de hueso. Otros síntomas, como dificultad para respirar, pueden desarrollarse si el cáncer se ha diseminado
                                a otros órganos, como los pulmones.</p>
                        </div>
                    </article>
                    <article id="tab4">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico de los Sarcomas Óseos</h2>
                        <div class="font-16 fontW-400">
                            <p>Desafortunadamente, los sarcomas óseos pueden ser asintomáticos durante mucho tiempo y los síntomas dependen de la parte del cuerpo que se vea afectada. El dolor óseo es el síntoma más comúnmente referido en el momento del
                                diagnóstico. A veces es posible sentir una masa o hinchazón ubicada profundamente en el hueso implicado y ocasionalmente puede producirse una fractura/ruptura.</p>

                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/sarcomas-oseos/Diagnostico_cancer_de_huesos-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>En las pruebas radiológicas, que usan diferentes tipos de energía para crear imágenes del interior del cuerpo, las imágenes determinan el alcance de un sarcoma óseo y establecen si el cáncer se ha diseminado a otras partes
                                del cuerpo, lo que recibe el nombre de metástasis.</p>
                            <p>Deberá obtenerse una pequeña porción del tumor (biopsia) para llevar a cabo un examen en el laboratorio que permita confirmar el diagnóstico y conocer más detalles sobre el tipo de sarcoma óseo.</p>
                        </div>
                    </article>
                    <article id="tab5">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento de los Sarcomas Óseos</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar a los Sarcomas Óseos se mencionan en esta sección.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/sarcomas-oseos/Tratamiento_-cancer_de_huesos-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Los sarcomas localizados están confinados en su sitio primario y no se han diseminado a los tejidos cercanos ni a otras áreas del cuerpo. Los sarcomas localiazdos se tratan principalmente las siguientes tres maneras.</p>
                            <ol class="secciones-enumeradas">
                                <li>La extirpación del tumor por medio de cirugía es el tratamiento estándar.</li>
                                <li>Radioterapia (el uso de radiación para tratar el cáncer).</li>
                                <li>Quimioterapia (medicamentos que destruyen y/o limitan el crecimiento de las células cancerosas).</li>

                            </ol>
                            <p>La radioterapia y la quimioterapia pueden ser aplicadas por separado o de forma conjunta antes y/o después de la cirugía. A veces pueden usarse para aumentar la probabilidad de curación completa y reducir el riesgo de que el
                                cáncer regrese.</p>
                            <p>Los sarcomas óseos avanzados se han diseminado desde su sitio primario a otras partes del cuerpo. Esto se conoce como tumor metastásico o localmente avanzado y comúnmente se pude tratar de las siguientes tres maneras:</p>
                            <ol class="secciones-enumeradas">
                                <li>El tratamiento principal es el uso de quimioterapia y terapia dirigida molecularmente. La elección de los medicamentos dependerá principalmente de las condiciones clínicas del paciente y del tipo de sarcoma óseo.</li>
                                <li>La radioterapia, tanto durante la quimioterapia como después de la misma, podría usarse para aliviar los síntomas y controlar las metástasis.</li>
                                <li>La cirugía podría usarse para aliviar los síntomas (como el dolor) y podría ser curativa en algunos casos.</li>
                            </ol>
                            <p>Seguimiento</p>
                            <ol class="secciones-enumeradas">
                                <li>Las citas del seguimiento incluyen exámenes físicos, análisis de sangre y exámenes radiológicos; todos estos tipos de pruebas se realizarán durante varios años.</li>
                                <li>Las investigaciones del seguimiento de los sarcomas óseos dependerán de la localización, el tamaño y la agresividad del tumor. La frecuencia del seguimiento dependerá del grado de su sarcoma.</li>
                            </ol>

