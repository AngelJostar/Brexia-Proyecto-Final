                    <article id="tab2">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo de los Sarcomas Óseos</h2>
                        <div class="font-16 fontW-400">
                            <p>Actualmente no está claro por qué se producen los sarcomas óseos. Se han identificado algunos factores de riesgo. Un factor de riesgo aumenta el riesgo de aparición de cáncer, pero no es ni necesario ni suficiente para causar
                                cáncer. Un factor de riesgo no es una causa en sí mismo. Algunas personas con estos factores de riesgo nunca desarrollarán un sarcoma óseo y otras personas sin ninguno de estos factores de riesgo sin embargo pueden desarrollar
                                un sarcoma óseo.</p>
                            <p>Se han identificado algunos factores de riesgo del sarcoma óseo y los principales son los siguientes:</p>
                            <p>Predisposiciones genéticas: las afecciones tanto heredadas como adquiridas pueden estar asociadas con la aparición del sarcoma óseo</p>
                            <ol class="secciones-enumeradas">
                                <li>Síndrome de Li-Fraumeni: es una afección genética heredada debida a la mutación de un gen supresor de tumores (p53), esto es, un gen que ayuda a proteger a las células para que no se vuelvan cancerosas. Los pacientes con
                                    este síndrome poco común son más proclives a desarrollar diversos tipos de cánceres, incluyendo los sarcomas óseos.</li>
                                <li>RB hereditario (retinoblastoma): síndrome familiar en el que todas las células del cuerpo tienen una mutación del gen RB1. Los pacientes suelen desarrollar tumores malignos de la retina en ambos ojos durante la infancia,
                                    y estos niños también tienen un mayor riesgo de padecer sarcomas de huesos o de tejidos blandos, incluyendo el osteosarcoma.</li>
                                <li>Exostosis múltiple hereditaria: (también conocida como osteocondromatosis múltiple) es un trastorno musculoesquelético hereditario poco frecuente que ocasiona estatura corta y deformidades. En esta afección, cada osteocondroma
                                    tiene un riesgo muy pequeño de desarrollarse y convertirse en un sarcoma óseo (la mayoría de las veces un condrosarcoma).</li>
                                <li>Otras afecciones hereditarias poco comunes, como el síndrome de Werner (trastorno hereditario caracterizado por un envejecimiento rápido que empieza en la adolescencia), el síndrome de Rothmund Thomson (trastorno hereditario
                                    que afecta a la piel, los huesos, los ojos, la nariz, el cabello, las uñas, los dientes, los testículos y los ovarios) y el síndrome de Bloom (trastorno caracterizado por la altura, que es inferior a la media, una cara
                                    estrecha con enrojecimiento y exantema, una voz aguda y problemas de fertilidad), también se han vinculado a un mayor riesgo de padecer osteosarcoma.</li>
                            </ol>
                            <p>Enfermedad ósea de Paget: trastorno caracterizado por un crecimiento anormal de las células óseas nuevas. Los huesos afectados son frágiles y deformes y tienen más probabilidades de romperse que los huesos sanos normales. Los
                                sarcomas de hueso (mayormente osteosarcomas) se desarrollan en aproximadamente el 1% de las personas con la enfermedad de Paget, por lo general cuando muchos huesos están afectados. Afecta principalmente a personas mayores
                                de 50 años.</p>
                            <p>Radiación ionizante: la exposición a radiación ionizante, como los rayos X y la radioterapia, puede aumentar el riesgo de aparición de sarcomas óseos incluso en ausencia de otros factores de riesgo. Con poca frecuencia, los
                                sarcomas óseos pueden surgir tras la exposición a radiación administrada para tratar otros cánceres y suelen comenzar en el área del cuerpo que ha sido tratada con radioterapia. El riesgo aumenta con la dosis del tratamiento
                                y disminuye con la edad. El promedio de tiempo entre la exposición a la radiación y el diagnóstico de un sarcoma óseo es de unos 10 años. Sin embargo, la exposición a la radiación es una rara causa de aparición de sarcomas
                                óseos.
                            </p>
                            <p>El riesgo de aparición del osteosarcoma es mayor en niños y adolescentes con Síndrome de Down. Hay otros factores que se sospecha que están asociados con un riesgo aumentado de aparición de los sarcomas óseos, pero las pruebas
                                para dichos factores son inconsistentes.</p>
                        </div>
