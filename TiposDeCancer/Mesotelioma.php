                    <article id="tab7">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Mesotelioma</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar mesotelioma son:</p>
                            <p>En lugar de utilizar un sistema de estadificación, la mayoría de los médicos ofrecen opciones de tratamiento basándose en si el cáncer se puede extraer con cirugía. Si la mayor parte, o tal vez todo el cáncer se puede extraer,
                                se le llama resecable. Si no se puede extraer, y la cirugía no es una opción, se le llama irresecable.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/mesatelioma/Tratamiento_Mesotelioma-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Hay muchas maneras de tratar el mesotelioma.</p>
                            <ul class="secciones-enumeradas">
                                <li>La cirugía y la radiación se usan para tratar el tumor solamente y no afectan el resto del cuerpo. Esto se denomina tratamiento local.</li>
                                <li>Por otro lado, los medicamentos usados en la quimioterapia recorren el cuerpo entero y pueden llegar a todas las células cancerosas en cualquier parte donde se encuentren. A esto se le llama tratamiento sistémico.</li>
                            </ul>
                            <p>Cirugía</p>
                            <p>Si su cáncer es pequeño o no se ha propagado, su médico puede sugerir la cirugía, junto con otros tratamientos. Si se puede hacer la cirugía, ésta provee la mejor probabilidad de cura. También se puede usar para aliviar algunos
                                síntomas causados por el cáncer.</p>
                            <p>Algunas veces, se acumula líquido en el pecho y esto causa problemas respiratorios. Este líquido puede extraerse al introducir un pequeño tubo por el pecho. Una vez se extrae el líquido, se introduce un medicamento en el tubo
                                que ayuda a sellar el espacio y evitar que el líquido se acumule nuevamente.</p>
                            <p>Por lo general, la radiación y/o la quimioterapia se utiliza junto con la cirugía.</p>
                            <p>Tratamiento con radiación</p>
                            <p>La radioterapia usa rayos de alta energía (como rayos X) para destruir las células cancerosas. Para el mesotelioma, la radiación se utiliza más a menudo después de la cirugía para eliminar cualquier célula cancerosa que haya
                                quedado. Además, la radiación se puede usar para aliviar los síntomas, como el dolor, el sangrado y la dificultad para tragar, entre otros problemas. Se administra en pequeñas dosis todos los días durante muchas semanas.</p>
                            <p>Quimioterapia</p>
                            <p>La quimioterapia es el uso de medicamentos para combatir el cáncer. Estos medicamentos se suministran en una vena, ingresan en la sangre y se propagan por todo el cuerpo. La quimioterapia es a menudo el principal tratamiento
                                para el mesotelioma que no se puede extraer con cirugía.</p>
                            <p>La quimioterapia se aplica en ciclos o series de tratamiento. Cada serie de tratamiento es seguida por un periodo de descanso. Este descanso permite que el cuerpo se recupere. La mayoría de las veces, la quimioterapia incluye
                                dos o más medicamentos y, a menudo, el tratamiento dura muchos meses.</p>
                            <p>Otros tratamientos que se pueden usar</p>
                            <p>Medicamentos que detienen el crecimiento de vasos sanguíneos del tumor: para que un tumor canceroso crezca, es necesario que se formen nuevos vasos sanguíneos para que nutran a las células cancerosas en el tumor. Se puede usar
                                un medicamento de terapia dirigida llamado Avastin® con quimioterapia para evitar que se formen nuevos vasos sanguíneos.</p>
                            <p>Medicamentos que ayudan a su sistema inmunitario: los medicamentos de inmunoterapia ayudan a su sistema inmunitario a encontrar y eliminar las células cancerosas. Pueden ayudar a controlar los tumores cuando la quimioterapia
                                no es eficaz.</p>
