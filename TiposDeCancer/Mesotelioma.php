                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Mesotelioma</h2>
                        <div class="font-16 fontW-400">

                            <p>Si bien la causa exacta del mesotelioma puede no ser conocida, varios factores pueden aumentar el riesgo de desarrollar este tipo de cáncer.</p>
                            <p>La exposición al asbesto es un factor de riesgo importante para el mesotelioma. El asbesto es un mineral fibroso natural que tiene muchas aplicaciones industriales útiles debido a su fuerza y resistencia al fuego y al calor,
                                así como a su baja conductividad eléctrica. Ahora se sabe que la exposición al asbesto es dañina y está relacionada con varios tipos de cáncer, más comúnmente el mesotelioma.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/mesatelioma/Factores_de_riesgo_Mesotelioma-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Conocer los factores de riesgo para el mesotelioma puede ayudarlo a tomar medidas preventivas para reducir la probabilidad de desarrollar este tipo de cáncer. Algunos factores de riesgo comunes incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Fumar: aunque fumar solo no es un factor de riesgo para el mesotelioma, algunos piensan que fumar en combinación con la exposición al asbesto puede aumentar el riesgo de mesotelioma de una persona</li>
                                <li>Exposición a Asbesto: la exposición al asbesto es un factor de riesgo importante para el mesotelioma. El riesgo de exposición prolongada al asbesto no disminuye con el tiempo. Más bien, pueden pasar más de 20 años desde
                                    la última exposición antes de que se desarrolle el cáncer. Hable con un médico si usted o un ser querido tiene antecedentes de exposición al asbesto. Hoy en día, el riesgo de exposición para los trabajadores en la industria
                                    manufacturera es mucho menor porque el asbesto, en general, ya no se usa en los Estados Unidos. Aunque el uso de asbesto ha disminuido drásticamente desde fines de la década de 1980, todavía se puede encontrar asbesto
                                    en edificios o productos más antiguos.</li>
                                <li>Exposición a SV40: entre 1955 y 1963, algunas vacunas contra la poliomielitis se infectaron con SV40 (virus simio 40). Hay investigaciones en curso que exploran la posibilidad de que las infecciones por SV40 puedan tener
                                    un efecto en el desarrollo del mesotelioma. Aunque no hay evidencia concluyente, puede haber una superposición en el rango de edad pico de las personas diagnosticadas con mesotelioma (de 50 a 70 años) y el momento de
                                    la exposición a SV40.</li>
                                <li>Exposición a Dióxido de torio: hasta la década de 1950, se inyectó dióxido de torio en el pecho o el abdomen antes de tomar una radiografía para crear contraste en la imagen. Puede haber un vínculo entre el dióxido de torio,
                                    seguido de una alta dosis de radiación y el mesotelioma</li>
                            </ul>
