                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Mesotelioma</h2>
                        <div class="font-16 fontW-400">
                            <p>El médico le hará preguntas sobre su salud y realizará una revisión física general. Si los indicios apuntan hacia un mesotelioma, se llevarán a cabo más pruebas.</p>
                            <p>Las siguientes pruebas son algunas que podría ser necesarias:</p>
                            <p>Radiografía de tórax: a menudo es la primera prueba que se hace en busca de problemas con sus pulmones. Si se detecta algún cambio, usted necesitará más pruebas.</p>
                            <p>Tomografía computarizada: este estudio también se conoce como “CT scan o CAT scan”. Es una clase especial de radiografía que capta imágenes del interior del cuerpo. También se puede usar una tomografía computarizada para ayudar
                                a realizar una biopsia (vea información más adelante).</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/mesatelioma/Diagnostico_Mesotelioma-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>PET scan: en este estudio, se le suministra un tipo de azúcar especial que puede verse en el interior de su cuerpo con una cámara. Si hay cáncer, el azúcar aparece como “puntos radioactivos” indicando donde se encuentra el
                                cáncer. Con este estudio se examina todo el cuerpo. También puede ayudar si el doctor cree que el cáncer se ha propagado, pero no sabe a qué lugar.</p>
                            <p>Imágenes por resonancia magnética: este estudio, conocido en inglés como MRI, usa ondas de radio e imanes potentes en lugar de rayos X para crear imágenes detalladas. Las MRI son muy útiles para examinar los tejidos blandos.</p>
                            <p>Análisis de sangre: las pruebas sanguíneas no se utilizan para descubrir el mesotelioma. Aunque los análisis de sangre pueden darle al médico más información sobre su estado de salud.</p>
                            <p>Biopsias: el doctor extrae un fragmento pequeño de tejido o una pequeña cantidad de líquido que se ha acumulado donde parece estar el cáncer. Luego se analiza para saber si contiene células cancerosas. Una biopsia es la mejor
                                forma de saber con seguridad si usted tiene cáncer. Hay muchos tipos de biopsias, así como formas de realizarlas. Pregunte a su médico qué tipo de biopsia va a requerir. Cada tipo de biopsia tiene ventajas y desventajas.
                                La elección del tipo de biopsia a usar depende de su caso.</p>
                        </div>
