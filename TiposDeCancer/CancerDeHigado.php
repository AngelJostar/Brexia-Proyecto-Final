                        <h2 class="centrar-texto brexia-color font-18 fontW-800 paddingTop-1 paddingBottom-1">¿Qué es el Cáncer de Hígado?</h2>
                        <div class="font-16 fontW-400">
                            <p>El ​cáncer de hígado es un ​tumor que se origina en el tejido del ​hígado (tejido hepático). Dependiendo del tipo de célula cancerosa, existen diversos tipos de ​cáncer de hígado​. El tipo más frecuente es el carcinoma hepatocelular,
                                que constituye el 90 % de todos los cánceres de hígado; comienza en los hepatocitos, las células más importantes del hígado.</p>
                            <p>Existe un tipo de ​cáncer de hígado raro, que se presenta principalmente en pacientes jóvenes, este es llamado carcinoma fibrolamelar. Esta variante es usualmente bien definida en comparación al carcinoma hepatocelular, que
                                crece de manera más invasiva. También se caracteriza por una cicatriz central visible en los exámenes de imagenología.</p>
                            <div class="imagen-80 centrar-imagen">
                                <img src="/img/cancer-de-higado/cancer_de_higado-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>El ​hígado es uno de los órganos más grandes del cuerpo. Tiene dos lóbulos y ocupa la sección superior derecha del abdomen adentro de la caja torácica. Las siguientes son tres de las diversas funciones del hígado.</p>
                            <ul class="secciones-enumeradas">
                                <li>Filtrar sustancias dañinas en la sangre para que puedan ser transportadas desde el cuerpo hasta la materia fecal y la orina.</li>
                                <li>Producir bilis para ayudar a la digestión de las grasas de los alimentos.</li>
                                <li>Almacenar glucógeno (azúcar) que el cuerpo usa para obtener energía.</li>
                            </ul>
                        </div>
                    </article>
                    <article id="tab2">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Hígado</h2>
                        <div class="font-16 fontW-400">
                            <p>Si bien es posible que no se conozca la causa exacta del ​cáncer de hígado​,varios factores pueden aumentar el ​riesgo​ de desarrollar la enfermedad. Debido a que no se han desarrollado pruebas de detección de rutina ampliamente
                                recomendadas para el ​cáncer de hígado​, las personas con antecedentes familiares de la enfermedad u otros ​factores de riesgo ​deben hablar con su ​médico ​sobre los pasos que pueden seguir para controlar o reducir su
                                riesgo. En Brexia recomiendamos hacerse análisis de sangre de alfafetoproteína y ultrasonidos cada seis a 12 meses para personas con alto riesgo de desarrollar ​cáncer de hígado.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-higado/Factor_de_riesgo_cancer_higado-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Los ​factores de riesgo ​para el ​cáncer de hígado ​incluyen:</p>
                            <p>Edad: ​La edad promedio de aparición del cáncer de hígado es de 63 años.</p>
                            <p>Sexo:​ Los hombres tienen más probabilidades de desarrollar cáncer de hígado que las mujeres, en una proporción de 2 a 1.</p>
                            <p>Obesidad: ​Ser obeso puede aumentar las posibilidades de desarrollar cáncer de hígado, probablemente a través del desarrollo de esteatohepatitis no alcohólica (NASH) y eventualmente, cirrosis.</p>
                            <p>Uso excesivo de alcohol: ​El abuso de alcohol es una causa común de cirrosis hepática, lo que aumenta los riesgos de cáncer de hígado de una persona.</p>
                            <p>Fumar:​ El consumo de tabaco puede aumentar el riesgo de desarrollar cáncer de hígado.</p>
                            <p>Esteroides anabólicos:​ Utilizados por los atletas para aumentar la fuerza y ​​lamasa muscular, el uso a largo plazo de esteroides anabólicos (hormonas masculinas) puede aumentar ligeramente el riesgo de desarrollar cáncer
                                de hígado. Los esteroides similares a la cortisona como la hidrocortisona, la dexametasona y la prednisona no conllevan el mismo riesgo.</p>
                            <p>Arsénico:​ La exposición crónica al arsénico natural a través del agua potable (contaminaciones en algunos pozos) aumenta el riesgo de desarrollar algunas formas de cáncer de hígado.</p>
                            <p>Aflatoxinas​: Son sustancias cancerígenas producidas por un hongo que contamina el trigo, el maíz, la soya, el arroz y algunos tipos de nueces. La contaminación generalmente ocurre debido al almacenamiento de los alimentos
                                en un ambiente húmedo y cálido, más común en países más cálidos y tropicales. La exposición a largo plazo a las aflatoxinas es un importante factor de riesgo de cáncer de hígado, especialmente en personas con infecciones
                                de hepatitis B o C.</p>
                            <p>Exposición a ciertas sustancias químicas:​ La exposición al cloruro de vinilo (una sustancia química utilizada en la fabricación de algunos plásticos) y al dióxido de torio (una sustancia química utilizada anteriormente para
                                las pruebas de rayos X) puede aumentar el riesgo de angiosarcoma del hígado.</p>
                            <p>Cirrosis del hígado: ​La cirrosis ocurre cuando las células del hígado se dañan y son reemplazadas por tejido cicatricial. Las personas con cirrosis tienen un mayor riesgo de cáncer de hígado. En la mayoría de los casos, las
                                personas que desarrollan CHC tienen cirrosis subyacente. La cirrosis es causada por varios factores. Además del abuso de alcohol y las infecciones crónicas de hepatitis B o C, la esteatohepatitis no alcohólica (NASH, una
                                enfermedad del hígado graso que a menudo se observa en personas obesas que consumen poco o nada de alcohol), ciertos tipos de enfermedades metabólicas hereditarias y enfermedades autoinmunes pueden causar cirrosis.</p>
                            <p>Enfermedades metabólicas:​ Ciertos tipos de enfermedades metabólicas hereditarias pueden causar cirrosis y aumentar las posibilidades de desarrollar cáncer de hígado. Hemocromatosis genética (un trastorno de sobrecarga de hierro
                                que acumula depósitos de hierro en todo el cuerpo, incluido el hígado), tirosinemia (niveles elevados del aminoácido tirosina), deficiencia de alfa-1-antitripsina, porfiria cutánea tardía (deficiencia en la síntesis de
                                hemo), glucógeno La enfermedad de almacenamiento y la enfermedad de Wilson (niveles elevados de cobre en el hígado) son enfermedades raras que pueden dañar el hígado y aumentar los riesgos de cáncer de hígado de una persona.</p>
                            <p>Diabetes:​ Tener diabetes también puede aumentar el riesgo de desarrollar cáncer de hígado.</p>
                        </div>
