                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Hígado</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de ​tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en ​cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar ​Cáncer de Hígado se mencioanan en esta seccion.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-higado/tratamiento_de_cancer_de_higado-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>La planificación del ​tratamiento implica un equipo multidisciplinar de profesionales médicos. Este encuentro de diferentes especialistas se denomina reunión multidisciplinar, o revisión por el comité de tumores. En esta reunión,
                                se discutirá la planificación del tratamiento de acuerdo a la información pertinente que se indicó antes, por ejemplo si el paciente presenta cirrosis hepática, la extensión de la enfermedad, el patrón de crecimiento del
                                ​tumor​,el funcionamiento hepático, la resecabilidad y el estado general de salud del paciente. También se tienen en cuenta los diversos riesgos de cada tipo de tratamiento. La extensión del tratamiento dependerá del estadio
                                del cáncer​,de las características del ​tumor​y de los riesgos para el paciente.</p>
                            <p>El plan de ​tratamiento depende principalmente del estadio de la enfermedad de acuerdo al sistema Barcelona Clinic Liver Cancer (BCLC).</p>
                            <p>Tratamiento para cáncer en estadio 0 y estadio A de acuerdo a la clasificación por el sistema BCLC.</p>
                            <p>Para estos pacientes con estadio temprano de la enfermedad (estadio 0 y estadio A de acuerdo al sistema BCLC), es posible proponer un tratamiento con intención de erradicar la enfermedad por completo (tratamiento curativo).
                                Este tratamiento puede ser la extirpación quirúrgica, el trasplante de hígado y los métodos de ablación local; y la decisión dependerá principalmente del grado de cirrosis y del tamaño y número de tumores en el hígado.</p>
                            <p>Para estos pacientes, hay varias opciones de tratamiento. La opinión multidisciplinaria* decidirá cuál es la más apropiada, de acuerdo con la información relevante mencionada antes. Las tres opciones más importantes en estos
                                estadios son:</p>
                            <ul>
                                <li>Extirpación del tumor mediante cirugía.</li>
                                <li>Trasplante de hígado.</li>
                                <li>Métodos de ablación locales.</li>
                            </ul>
                            <p>Tratamientos usados para pacientes cuyo tumor se considera no resecable mediante operación quirúrgica y para los pacientes en espera de trasplante de hígado.</p>
                            <p>El tratamiento de ablación y la embolización son dos tipos de procedimientos que pueden ofrecerse como alternativa a la cirugía cuando se considera que el tumor no es resecable o mientras se está a la espera de un trasplante
                                de hígado. Para los carcinomas fibrolamelares considerados irresecables, estas modalidades de tratamiento a nivel local son los únicos que pueden tenerse en cuenta.</p>
                            <p>Tratamiento para cáncer en estadio B de acuerdo a la clasificación BCLC.</p>
                            <p>Para pacientes en estadio intermedio (estadio B de acuerdo al sistema BCLC), el tratamiento consiste en la inyección de un medicamento anticancer y de un espiral o partículas pequeñas degradables directamente en la arteria
                                que irriga el hígado. Este tipo de tratamiento se llama quimiembolización arterial (TACE).</p>
                            <p>Tratamiento para el estadio C de acuerdo a la clasificación BCLC.</p>
                            <p>El tratamiento sistémico con terapia dirigida, inmunoterapia o quimioterapia es el tratamiento recomendado en este estadio.</p>
                            <p>Tratamiento de cáncer en estadio D de acuerdo a la clasificación BCLC.</p>
                            <p>En este estadio el tratamiento recomendado está dirigido a aliviar los síntomas causados por la enfermedad. Para pacientes con cáncer en estadio D de acuerdo a la clasificación BCLC, se ofrece tratamiento de sostén. El objetivo
                                no es curar el cáncer ni prolongar la supervivencia, sino que se intenta reducir los síntomas y hacer que el paciente se sienta lo más cómodo posible. Existen medicamentos efectivos para controlar el dolor, las náuseas
                                y otros síntomas. Es importante informar al médico o a la enfermera de cualquier molestia que pudiera sufrirse para adaptar la medicación según sus necesidades.</p>
