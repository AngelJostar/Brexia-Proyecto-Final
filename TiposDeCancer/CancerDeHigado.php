                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Hígado</h2>
                        <div class="font-16 fontW-400">
                            <p>El ​diagnóstico​ de ​cáncer de hígado ​suele basarse en distintas exploraciones:</p>
                            <ul class="secciones-enumeradas">
                                <li>Examen clínico: ​El médico preguntará sobre los síntomas y molestias, además explorará el abdomen y el resto del cuerpo. Comprobando si el hígado o el bazo aumentaron de tamaño, si hay líquido en el abdomen, si hay una
                                    coloración amarillenta de la piel y los ojos (signo de ictericia) u otros signos que indiquen mal funcionamiento del hígado.</li>
                            </ul>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-higado/diagnostico_cancer_de_higado-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <ul class="secciones-enumeradas">
                                <li>Análisis de sangre: ​Un análisis de sangre puede revelar elevación de las concentraciones de α-fetoproteína (AFP), un marcador tumoral, pero esto únicamente se observa en el 50-75 % de los pacientes con cáncer de hígado.
                                    Por lo tanto, si las concentraciones no están elevadas no implica que no haya ningún tumor, de igual manera, si la concentración de AFP es elevada, no indica necesariamente que haya un tumor.</li>
                                <li>Examen radiológico: ​En primer lugar, se realizará unultrasonido del hígado para evaluar la consistencia del órgano y buscar posibles nódulos. En el 75 % de los casos de cáncer hepático, los tumores son multifocales en
                                    el momento del diagnóstico, es decir, que hay varios nódulos (o tumores) en diferentes partes del hígado. Para obtener una imagen más precisa y poder detectar los nódulos de menor tamaño, pueden realizarse también exploraciones
                                    mediante TC o IRM. Una IRM puede resultar especialmente útil en pacientes que ya tienen nódulos (benignos) a causa de la cirrosis. Estas exploraciones se realizan en ocasiones después de inyección intravenosa de un
                                    líquido de contraste para marcar cualquier nódulo que estuviera presente. La secuencia de pruebas usadas para el diagnóstico del cáncer de hígado dependerá del tamaño de las lesiones y de la presencia de cirrosis observados
                                    tras las exploraciones radiológicas.</li>
                                <li>Examen histopatológico: ​El análisis histopatológico se realiza en una muestra de tejido hepático denominada biopsia. La decisión de realizar una biopsia debe evaluarse con varios especialistas, incluyendo un cirujano especializado
                                    en cirugía de hígado. Es la única manera de evaluar si una lesión observada en una exploración radiológica es benigna o maligna. Una biopsia puede tomarse usando una aguja fina o gruesa que atraviesa la piel en el lado
                                    derecho y se introduce en el hígado para extraer una muestra de tejido hepático. En ocasiones se usa un ultrasonido o una exploración TC al mismo tiempo para asegurarse de que la aguja se introduzca directamente en
                                    lo que se sospecha es un nódulo. Un cirujano puede también tomar una biopsia durante una operación llamada laparoscopia, durante la cual el cirujano introduce una cámara pequeña e instrumento finos por una o más incisiones
                                    de pequeño tamaño en la piel, para ver el interior del abdomen y tomar una biopsia sin tener que hacer una gran incisión en el abdomen. Un especialista, el patólogo examinará la muestra de tejido con un microscopio
                                    en un laboratorio, y puede también realizar otras pruebas para definir el tipo y las características específicos del tumor. Si el patólogo concluye que la biopsia no contiene ninguna célula cancerosa, no podrá concluirse
                                    que el tumor es maligno.</li>
                            </ul>
                            <p>También es posible hacer uso de los resultados de imagenología para establecer el diagnóstico. Se puede evitar una biopsia solamente cuando un examen de imagen específico muestra el típico patrón de vascularización característico
                                del cáncer de hígado. Este examen puede ser un tipo de tomografía (tomografía computarizada multidetector) o un tipo de IRM (resonancia magnética contrastada dinámica).

                            </p>
                        </div>
