                    <article id="tab6">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Endometrio</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de ​tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en ​cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar ​cáncer de endometrio ​se mencionan a continuación.</p>
                            <p>La piedra angular del ​tratamiento es la ​cirugía​. La ​radioterapia y la ​quimioterapia utilizadas tras la ​cirugía se llaman terapias adyuvantes, lo que significa que se utilizan como complemento a la ​cirugía.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-endometrio/Tratamiento_cancer_endometrio-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Para algunas pacientes, hay varias posibilidades de ​tratamiento disponibles y la elección debería analizarse teniendo en cuenta el equilibrio entre los beneficios y los riesgos.</p>
                            <p>Cirugía</p>
                            <p>Para cada paciente se realiza una evaluación preoperatoria. En las pacientes consideradas operables, el objetivo de la cirugía es estadificar la enfermedad y extirpar el útero que contiene el tumor.</p>
                            <p>Terapia adyuvante</p>
                            <p>Una terapia adyuvante es una terapia que se administra como complemento a la cirugía. Se recomienda que la decisión sobre el tratamiento del cáncer de endometrio se fundamente en la discusión de un equipo interdisciplinario
                                de profesionales médicos. Esta reunión de diferentes especialistas es llamada reunión multidisciplinar o revisión del comité de tumores. En esa reunión, se debate la planificación del tratamiento según la información pertinente.</p>
                            <p>Tratamiento de la enfermedad en estadio avanzado</p>
                            <p>El tratamiento del cáncer de endometrio en estadio avanzado suele incluir combinaciones de cirugía, radioterapia y quimioterapia. Estas pacientes pueden recibir tratamiento con radioterapia externa (radiaciones producidas por
                                una fuente externa y luego dirigidas hacia el tumor) y/o radioterapia interna llamada braquiterapia (implica la colocación de una fuente de radiación en la cavidad uterina y/o en la vagina) y, además, mediante tratamiento
                                sistémico, un tratamiento sistémico actúa sobre las células cancerosas en todo el cuerpo y no solo a nivel local. El tratamiento sistémico puede consistir en quimioterapia u hormonoterapia.</p>
