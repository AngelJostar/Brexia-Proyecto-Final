                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Endometrio</h2>
                        <div class="font-16 fontW-400">
                            <p>Los factores de riesgo conocidos para el ​cáncer de endometrio incluyen:</p>
                            <p>Edad: La mayoría de las mujeres diagnosticadas con cáncer de endometrio tienen más de 50 años y han pasado por la menopausia.</p>
                            <p>Mayor número de ciclos menstruales: Las mujeres que han tenido más ciclos menstruales en su vida tienen un mayor riesgo de cáncer de endometrio. Esto incluye a aquellos que comenzaron sus períodos antes de los 12 años y que
                                pasaron por la menopausia después de los 50 años.</p>
                            <img src="/img/cancer-de-endometrio/Factor_de_riesgo-768x512.webp" alt="Brexia_especialistas">
                            <p>Sin embarazos previos: Los investigadores están investigando por qué el embarazo parece reducir el riesgo de cáncer de endometrio. Durante el embarazo, el equilibrio hormonal de una mujer cambia, y su cuerpo produce más progesterona
                                y menos estrógeno. Los ciclos menstruales irregulares y la infertilidad también pueden causar desequilibrios en los niveles de estrógeno y progesterona, lo que puede aumentar el riesgo de cáncer de endometrio.</p>
                            <p>Obesidad: Los tejidos grasos tienden a producir niveles más altos de estrógeno, particularmente después de la menopausia, lo que aumenta el riesgo de cáncer de endometrio para las mujeres mayores con sobrepeso.</p>
                            <p>Síndrome metabólico​: Este síndrome ocurre cuando se desarrolla un conjunto específico de afecciones al mismo tiempo, como exceso de grasa alrededor del abdomen, niveles altos de azúcar en la sangre, presión arterial alta,
                                niveles altos de triglicéridos y niveles bajos de lipoproteínas de alta densidad en la sangre.</p>
                            <p>Hiperplasia endometrial: ​Esta afección ocurre cuando una acumulación de células y estructuras glandulares causa un engrosamiento en el endometrio (el revestimiento del útero). Existen varios tipos de hiperplasia endometrial.
                                La hiperplasia no es cáncer, pero la afección puede aumentar el riesgo de desarrollar cáncer, según el tipo de hiperplasia, si las células del endometrio se han vuelto anormales y otros factores.</p>
                            <p>Otras condiciones:​ Diabetes, tumores de ovario y síndromes.</p>
                            <p>Terapia de reemplazo de estrógenos: Reemplazar el estrógeno sin aumentar los niveles de progesterona después de la menopausia puede aumentar el riesgo de una mujer de desarrollar cáncer de endometrio.</p>
                            <p>Tamoxifeno: Las mujeres que reciben tratamiento con el medicamento para el cáncer de seno tamoxifeno tienen un mayor riesgo de desarrollar esta enfermedad.</p>
                            <p>Factores de riesgo genético: La herencia juega un papel clave en el desarrollo de ciertos cánceres ginecológicos, porque el daño a los genes que involucran el crecimiento celular y la reparación del ADN se acumulan con el tiempo.</p>
