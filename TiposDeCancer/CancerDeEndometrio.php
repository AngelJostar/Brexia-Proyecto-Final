                        <h2 class="centrar-texto brexia-color encabezados-brexia paddingTop-2 ">¿Qué es el Cáncer de Endometrio?</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer de endometrio ​es un tipo de cáncer ​que se forma en el tejido que reviste el útero (órgano pequeño, hueco y con forma de pera situado en la pelvis de una mujer donde se desarrolla el feto). La mayoría de los casos
                                de ​cáncer de endometrio son adenocarcinomas (​cáncer que comienzan en las células que elaboran y liberan mucosidad y otros fluidos).</p>
                            <p>El ​endometrio es el revestimiento del ​útero​, un órgano hueco y muscular de la pelvis de la mujer. El ​útero es el lugar donde crece el feto. En la mayoría de las mujeres no embarazadas, el ​útero mide aproximadamente tres
                                pulgadas de largo. El ​cuello uterino es el extremo inferior más estrecho del ​útero​ que conduce a la vagina.
                            </p>
                            <div class="imagen-80 centrar-imagen">
                                <img src="/img/cancer-de-endometrio/cancer_de_endometrio-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>La mayoría de las mujeres con ​cáncer uterino​, alrededor del 92 por ciento, tienen ​cáncer endometrial​, ​o un ​tumor que se desarrolla en los tejidos del revestimiento ​uterino​, llamado ​endometrio​. El otro tipo primario,
                                el sarcoma uterino, es raro y se forma en los músculos u otros tejidos del ​útero​. Los síntomas del ​cáncer uterino pueden diferir según el tipo de ​cáncer​ que se haya desarrollado.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Nota importante sobre otros tipos de cáncer de Útero</h2>
                            <p>Cáncer de cuello de útero: El cáncer de cuello de útero es un cáncer que se forma en el cuello del útero, a diferencia del cáncer de endometrio que se forma en el cuerpo del útero. El diagnóstico y tratamiento de este tipo
                                de cáncer es diferente al del cáncer de endometrio.</p>
                            <p>Sarcoma uterino: ​El sarcoma uterino es otro tipo de cáncer que se forma en el cuerpo del útero. Se forma en el músculo del ​útero (miometrio) o en otros tejidos del útero. ​Aunque el tratamiento del sarcoma uterino y el del
                                cáncer de endometrio comparten algunas similitudes, la ​información presentada aquí es válida para el cáncer de endometrio, pero no para el sarcoma uterino.</p>
                            <p>Carcinosarcoma uterino: El carcinosarcoma uterino es otro tipo de cáncer que se forma en el cuerpo del útero. Ahora se sabe que el carcinosarcoma puede ser un tipo de cáncer de endometrio agresivo. La información proporcionada
                                sobre el cáncer de endometrio también es válida para el carcinosarcoma uterino.</p>
                        </div>
