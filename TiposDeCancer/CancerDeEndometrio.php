                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadísticas del Cáncer de Endometrio</h2>
                        <div class="font-16 fontW-400">
                            <p>Cuando usted se entera de que tiene ​cáncer de endometrio ​y comienza a buscar opciones de ​tratamiento​, es posible que le preocupe sobre todo su esperanza de vida y su calidad de vida. En ​Brexia ​creemos que tiene derecho
                                a conocer las ​estadísticas ​sobre los resultados del tratamiento del ​cáncer de estómago​, para que pueda tomar decisiones informadas sobre su atención del ​cáncer.</p>
                            <p>Muchos pacientes con cáncer de endometrio consideran que la duración y la calidad de vida son dos de las estadísticas más importantes para la atención del cáncer de endometrio, y valoran una experiencia de tratamiento que sea
