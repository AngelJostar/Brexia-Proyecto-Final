                    <article id="tab5">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Endometrio</h2>
                        <div class="font-16 fontW-400">
                            <p>A diferencia del cáncer de cuello de ​útero​, no se recomienda la realización de una prueba de detección sistemática para el ​cáncer de endometrio.</p>
                            <p>El signo más frecuente del ​cáncer de endometrio es el sangrado vaginal. Después de la menopausia no debería presentarse ningún sangrado vaginal, por lo que su presencia no es normal. Tras la menopausia, la presencia de sangrado
                                vaginal no es normal, por lo que las mujeres deben consultar a su ​médico.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-endometrio/Diagnostico_cancer_endometrio-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Antes de la menopausia, si una mujer presenta sangrado vaginal entre periodos menstruales, o sangrados vaginales abundantes e inusuales durante los periodos menstruales, también debe consultar a su ​médico.</p>
                            <p>El ​cáncer de endometrio no es la única y más frecuente causa de dicho sangrado vaginal y los médicos le harán un examen más exhaustivo.</p>
                            <p>El ​diagnóstico ​del ​cáncer de endometrio​ se basa en los siguientes tres exámenes:</p>
                            <ul class="secciones-enumeradas">
                                <li>Examen clínico. Esto incluye un examen ginecológico para evaluar la localización y el volumen del tumor y si se ha extendido a otros órganos en la pelvis.</li>
                                <li>Examen radiológico. Esto incluye la realización de una ecografía del útero. Se introduce una sonda en la vagina para estar más cerca del útero y examinar mejor. Esto se llama ecografía transvaginal. Durante este examen,
                                    se mide el grosor del endometrio. Si es mayor a a 3 o 4 mm, debe obtenerse una muestra de endometrio (biopsia). Puede requerir realizarse exámenes adicionales como una radiografía torácica, un ultrasonido abdominal
                                    y una tomografía computarizada abdominal para excluir una metástasis. Si se sospecha que el cáncer se ha extendido al cuello del útero, puede solicitarse una resonancia magnética (IRM).</li>
                                <li>Examen histopatológico. Se trata del examen de laboratorio de las células tumorales mediante la disección de una muestra del tumor (biopsia). Este examen de laboratorio lo realiza un patólogo que confirmará el diagnóstico
                                    de cáncer de endometrio y dará más información sobre las características del mismo. La biopsia suele realizarse por histeroscopia, que consiste en introducir un dispositivo especial para obtener la biopsia. Más tarde
                                    se efectuará un segundo examen histopatológico mediante el examen del tumor extirpado por cirugía.</li>
                            </ul>
                        </div>
