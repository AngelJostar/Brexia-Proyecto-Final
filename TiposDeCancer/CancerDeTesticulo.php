                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Testículo</h2>
                        <div class="font-16 fontW-400">
                            <p>Aunque se desconocen las causas exactas del ​cáncer de testículo​, se han identificado ciertos ​factores ​ que pueden aumentar el ​riesgo​ de ​cáncer​​testicular ​de un individuo. Algunos de estos factores de riesgo incluyen:
                            </p>
                            <p>Edad​. Aproximadamente el 80 por ciento de los cánceres testiculares ocurren en hombres adultos menores de 44 años, y más de la mitad ocurren en hombres entre 20 y 34 años.</p>
                            <p>Raza y etnia​. Los hombres caucásicos tienen un riesgo cinco veces mayor de desarrollar cáncer testicular que los hombres negros, y un riesgo tres veces mayor en comparación con los hombres asiáticos o indios americanos. Los
                                hombres hispanos / latinos tienen un riesgo menor que los hombres caucásicos pero un riesgo mayor que los hombres asiático-estadounidenses.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/cancer-de-testiculos/Contenido-Cancer-de-Testiculo_0003_Capa-4-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Antecedentes familiares.​ Aproximadamente el 3 por ciento de los casos de cáncer testicular ocurren en familias. Tener un hermano o padre que haya tenido cáncer testicular puede aumentar ligeramente su riesgo de desarrollar
                                la enfermedad.</p>
                            <p>Criptorquidia (un testículo no descendido)​. Los testículos se desarrollan en el abdomen del feto y se mueven hacia el escroto antes del nacimiento. Aproximadamente el 3 por ciento de los hombres pueden nacer con un testículo
                                que no ha podido descender correctamente. El cáncer testicular es varias veces más probable que ocurra en hombres con criptocordismo, y es más probable que se desarrolle en los testículos no descendidos.</p>
                            <p>Infección por VIH . Algunas investigaciones sugieren que la infección con el virus de inmunodeficiencia humana (VIH) puede aumentar los riesgos de cáncer testicular, especialmente en personas que han desarrollado el síndrome
                                de inmunodeficiencia adquirida (SIDA).</p>
                            <p>Carcinoma in situ.​Esta es una forma no invasiva de cáncer testicular que a veces, pero no siempre, puede progresar a cáncer invasivo. Por lo general, no forma una masa que se pueda sentir ni que produzca ningún síntoma. Se
                                encuentra con mayor frecuencia incidentalmente en hombres que se han sometido a una biopsia del testículo para evaluar otra afección, como la infertilidad.</p>
                            <p>Antecedentes previos de cáncer testicular. Aproximadamente del 3 al 4 por ciento de los hombres que han tenido cáncer en un testículo más tarde desarrollarán cáncer en el otro testículo</p>
