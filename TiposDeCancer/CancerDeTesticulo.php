                    <h2 class="centrar-texto brexia-color font-18 fontW-800 paddingTop-1 paddingBottom-1">¿Qué es el Cáncer de Testículo?</h2>
                        <div class="font-16 fontW-400">
                            <p>El ​cáncer de testículo se forma en las células de los ​testículos​. La mayoría comienza en las células que producen esperma y se conocen como tumores de células germinales. El cáncer de testículo ​afecta principalmente a hombres
                                de entre 15 y 40 años.
                            </p>
                            <P>
                                Hay dos tipos principales de ​cáncer de testículo​: Seminoma y no seminoma. Los seminomas consisten en un tipo de célula cancerosa, mientras que los no seminomas generalmente involucran una mezcla de tipos de células.
                            </P>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/cancer-de-testiculos/Contenido-Cancer-de-Testiculo_0009_Capa-1-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué son los testículos?</h2>
                            <p>Los testículos son dos glándulas en forma de huevo que se encuentran dentro del escroto (bolsa de piel floja que está justo detrás del pene). El cordón espermático mantiene los testículos dentro del escroto, que también contiene
                                el conducto deferente, además de los vasos y nervios de los testículos.</p>
                            <p>Los testículos son las glándulas sexuales masculinas que producen la testosterona y los espermatozoides. Las células germinativas dentro de los testículos producen espermatozoides inmaduros que viajan a través de una red de
                                túbulos (tubos diminutos) y tubos más grandes hasta llegar al epidídimo (tubo largo en espiral al lado de los testículos), donde maduran y se almacenan.</p>

                        </div>
