                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer Cervicouterino</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer cervicouternio en sus etapas tempranas no presenta síntomas. Los síntomas del cáncer de cervicouternio avanzado incluyen sangrado vaginal anormal, dolor pélvico, flujo vaginal y dolor durante las relaciones sexuales.</p>
                            <p>En Brexia, el diagnóstico del cáncer cervicouternio suele basarse en los resultados del examen clínico, de la colposcopia y de la biopsia. Existen investigaciones adicionales que ayudan a determinar cuán avanzado está el cáncer,
                                como, por ejemplo, exámenes bajo anestesia, radiografías y un pielograma intravenoso. También se pueden utilizar técnicas modernas de diagnóstico por imágenes como la tomografía computarizada (TC), la imagenología por resonancia
                                magnética (IRM) y la tomografía por emisión de positrones (TEP).</p>
                            <p>Para detectar y hacer el diagnóstico el cáncer cervicouternio, se utilizan pruebas para examinar el cuello uterino. Se utilizan los siguientes procedimientos:</p>
                            <p>Examen físico y antecedentes de salud: examen del cuerpo para revisar el estado general de salud e identificar cualquier signo de enfermedad, como masas o cualquier otra cosa que parezca anormal. También se toman datos sobre
                                los hábitos de salud, los antecedentes de enfermedades y los tratamientos anteriores.</p>
                            <p>Examen pélvico: examen de la vagina, el cuello uterino, el útero, las trompas de Falopio, los ovarios y el recto. Se introduce un espéculo en la vagina y el médico o el enfermero examinan la vagina y el cuello uterino para
                                detectar signos de enfermedad. Por lo habitual, se realiza una prueba de Pap del cuello uterino. Además, el médico o el enfermero introducen uno o dos dedos cubiertos con guantes lubricados en la vagina y colocan la otra
                                mano sobre la parte baja del abdomen para palpar el tamaño, la forma y la posición del útero y los ovarios. Asimismo, el médico o el enfermero introducen un dedo enguantado y lubricado en el recto para palpar si hay masas
                                o áreas anormales.
                            </p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-cervicouterino/Diagnostico_de_cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Prueba de Pap: procedimiento para recolectar células de la superficie del cuello uterino y la vagina. Se utiliza un trozo de algodón, un cepillo o una espátula pequeña de madera para hacer un raspado suave y extraer una muestra
                                de las células del cuello uterino y la vagina. Las células se examinan al microscopio para determinar si son anormales. Este procedimiento también se llama frotis de Pap.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-cervicouterino/Cancer_diagnostico-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Prueba del virus del papiloma humano (VPH): prueba de laboratorio para identificar el ADN o el ARN de ciertos tipos de infección por el VPH. Se extraen células del cuello uterino y se analiza el ADN o el ARN de estas para determinar
                                si hay una infección por un tipo del VPH relacionado con el cáncer de cuello uterino. Para este análisis, a veces se usa la muestra de células obtenida durante una prueba de Pap. Este análisis también se hace cuando los
                                resultados de una prueba de Pap indican que hay ciertas células anormales en el cuello uterino.</p>
                            <p>Legrado endocervical: procedimiento para extraer células o tejido del canal del cuello uterino mediante una cureta (instrumento en forma de cuchara). Se extraen muestras de tejido y se observan al microscopio para detectar
                                signos de cáncer. A veces este procedimiento se realiza al mismo tiempo que una colposcopia.</p>
                            <p>Colposcopia: procedimiento para el que se usa un colposcopio (un instrumento con aumento y luz) a fin de observar el interior de la vagina y el cuello uterino, y detectar áreas anormales. Es posible extraer muestras de tejido
                                con una cureta (una herramienta en forma de cuchara) o un cepillo para observarlas al microscopio y verificar si hay signos de cáncer.</p>
                            <p>Biopsia: si se encuentran células anormales en una prueba de Pap, a veces el médico hace una biopsia. Se extrae una muestra de tejido del cuello uterino para que un patólogo la observe al microscopio y determine si hay signos
                                de cáncer. En general la biopsia se hace en el consultorio del médico cuando solo se extrae una pequeña cantidad de tejido. Es posible que la biopsia se haga en un hospital si se extrae una muestra de tejido más grande
                                en forma de cono (biopsia de cono).</p>
                            <p>El cáncer de cuello uterino se “estadifica” de acuerdo con el tamaño del tumor, la afectación de los ganglios linfáticos y el hecho de que se haya diseminado a otras partes del cuerpo. Esta información se utiliza para ayudar
                                a decidir el mejor tratamiento.</p>

