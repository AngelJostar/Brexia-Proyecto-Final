                            <p>Los signos y síntomas del cáncer de cuello uterino incluyen el sangrado vaginal y el dolor pélvico.</p>
                            <p>Estos y otros signos y síntomas pueden obedecer a un cáncer de cuello uterino o a otras afecciones. Consulte con su médico si tiene alguno de los siguientes signos o síntomas:</p>
                            <ul class="secciones-enumeradas">
                                <li>Sangrado vaginal (incluso si sangra después de una relación sexual).</li>
                                <li>Secreción vaginal fuera de lo común.</li>
                                <li>Dolor pélvico.</li>
                                <li>Dolor durante las relaciones sexuales.</li>
                            </ul>
                            <P>Para la mayoría de las mujeres con cancer cervicouterino, el síntoma más común es el sangrado o manchado vaginal inusual. Otros síntomas comunes de los tumores uterinos incluyen:</P>
                            <ul class="secciones-enumeradas">
                                <li>Micción frecuente.</li>
                                <li>Dolor en el abdomen.</li>
                                <li>Una masa (bulto o crecimiento) en la vagina.</li>
                                <li>Sentirse lleno en todo momento.</li>
                            </ul>
                            <p>Estos signos y síntomas pueden ser causados ​​por otras afecciones además del cáncer cervicouterino. Si experimenta alguno de ellos, consulte a su médico para determinar su causa.</p>
