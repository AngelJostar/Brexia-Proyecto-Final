                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cáncer Cervicouterino</h2>
                        <div class="font-16 fontW-400">
                            <p>Se utilizan los siguientes estadios para el cáncer de cuello uterino.</p>
                            <p>Estadio I: el cáncer se formó y se encuentra solo en el cuello uterino. El estadio I se divide en los estadios IA y IB, según el tamaño del tumor y la profundidad a la que este se diseminó.</p>
                            <ul class="secciones-enumeradas">
                                <li>El estadio IA se divide en los estadios IA1 y IA2, según la profundidad a la que se disemina el tumor.</li>
                                <ul>
                                    <li>En el estadio IA1, se encuentra muy poco cáncer (solo se observa al microscopio) en el tejido del cuello uterino. El tumor se diseminó a una profundidad de 3 mm o menos.</li>
                                    <li>En el estadio IA2, se encuentra muy poco cáncer (solo se observa al microscopio) en el tejido del cuello uterino. El tumor se diseminó a una profundidad de más de 3 mm, pero no más de 5 mm.</li>
                                </ul>
                                <li>El estadio IB se divide en los estadios IB1, IB2 y IB3, según el tamaño del tumor y la profundidad a la que este se diseminó.</li>
                                <ul>
                                    <li>En el estadio IB1, el tumor mide 2 cm o menos y se diseminó a más de 5 mm de profundidad.</li>
                                    <li>En el estadio IB2, el tumor mide más de 2 cm, pero no más de 4 cm.</li>
                                    <li>En el estadio IB3, el tumor mide más de 4 cm.</li>
                                </ul>
                                <div class="imagen-80 centrar-imagen ">
                                    <img src="/img/cancer-cervicouterino/icacion_cancr_cervicouterino-768x512.webp" alt="Brexia_especialistas">
                                </div>
                                <p>Estadio II: el cáncer se diseminó a los dos tercios superiores de la vagina o al tejido que rodea el útero. El estadio II se divide en los estadios IIA y IIB, según hasta dónde se diseminó el cáncer.</p>
                                <li>En el estadio IIA, el cáncer se diseminó del cuello uterino a los dos tercios superiores de la vagina, pero no se diseminó al tejido que rodea el útero. El estadio IIA se divide en los estadios IIA1 y IIA2, según el tamaño
                                    del tumor.</li>
                                <ul>
                                    <li>En el estadio IIA1, el tumor mide 4 cm o menos.</li>
                                    <li>En el estadio IIA2, el tumor mide más de 4 cm.</li>
                                </ul>
                                <li>En el estadio IIB, el cáncer se diseminó del cuello uterino al tejido que rodea el útero.</li>
                                <p>Estadio III: el cáncer se diseminó al tercio inferior de la vagina o a la pared pélvica, causó problemas de riñón o afectó los ganglios linfáticos. El estadio III se divide en los estadios IIIA, IIIB y IIIC, según hasta
                                    dónde se diseminó el cáncer.</p>
                                <li>En el estadio IIIA, el cáncer se diseminó al tercio inferior de la vagina, pero no se diseminó a la pared pélvica.</li>
                                <li>En el estadio IIIB, se presenta una de las siguientes situaciones:</li>
                                <ul>
                                    <li>El cáncer se diseminó a la pared pélvica.</li>
                                    <li>El tumor se agrandó y obstruye uno o ambos uréteres, o hace que uno o ambos riñones se agranden o no funcionen.
                                    </li>
                                </ul>
                                <li>El estadio IIIC se divide en los estadios IIIC1 y IIIC2, según la forma en que se disemina el cáncer a los ganglios linfáticos.</li>
                                <ul>
                                    <li>En el estadio IIIC1, el cáncer se diseminó a los ganglios linfáticos de la pelvis.</li>
                                    <li>En el estadio IIIC2, el cáncer se diseminó a los ganglios linfáticos del abdomen cercanos a la aorta.</li>
                                </ul>
                                <p>Estadio IV: el cáncer se diseminó fuera de la pelvis, al revestimiento de la vejiga o del recto, o a otras partes del cuerpo.El estadio IV se divide en los estadios IVA y IVB, según hasta dónde se diseminó el cáncer.</p>
                                <li>En el estadio IVA, el cáncer se diseminó a órganos cercanos, como la vejiga o el recto.</li>
                                <li>En el estadio IVB, el cáncer se diseminó a otras partes del cuerpo, como el hígado, los pulmones, los huesos o los ganglios linfáticos lejanos.</li>
                                <p>Cáncer de cuello uterino recidivante: El cáncer de cuello uterino recidivante es cáncer que recidivó (volvió) después de que se trató. El cáncer puede volver al cuello uterino o otras partes del cuerpo.</p>
                                <h2 class="centrar-texto brexia-color encabezados-brexia ">Puntos Importantes de la Estadificación del Cáncer Cervicouterino</h2>
                                <li>Después de que se diagnostica el cáncer de cuello uterino, se realizan pruebas para determinar si las células cancerosas se diseminaron dentro del cuello uterino o a otras partes del cuerpo.</li>
                                <li>Es posible que el cáncer se disemine desde donde comenzó hasta otras partes del cuerpo.</li>
                                <li>Es posible que se formen células anormales en el revestimiento del cuello uterino (carcinoma in situ).</li>
                            </ul>
                        </div>
