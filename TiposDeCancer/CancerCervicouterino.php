                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer Cervicouterino</h2>
                        <div class="font-16 fontW-400">
                            <p>Un factor de riesgo es todo aquello que aumenta la probabilidad de padecer una enfermedad, como el cáncer. Cada tipo de cáncer tiene diferentes factores de riesgo. Los factores de riesgo asociados al cáncer cervicouterino son:</p>
                            <p>Virus del Papiloma Humano: El factor de riesgo más importante del cáncer de cuello uterino es la infección por el virus del papiloma humano.</p>
                            <p>Antecedente sexuales: Ser sexualmente activo a una edad temprana (especialmente los menores de 18 años). Tener muchas parejas sexuales. Tener una pareja que se considera de alto riesgo (alguien con infección por VPH o que tiene
                                muchas parejas sexuales).</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-cervicouterino/riesgo_cancer_cervicouterino-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Tabaquismo: Cuando alguien fuma, tanto el fumador como las personas que le rodean están expuestos a muchas sustancias químicas cancerígenas que afectan a otros órganos, además de los pulmones. Estas sustancias dañinas son absorbidas
                                a través de los pulmones y conducidas al torrente sanguíneo por todo el cuerpo.
                            </p>
                            <p>Tener un sistema inmunitario débil o comprometido: El virus de inmunodeficiencia humana (VIH o HIV, en inglés), el virus que causa el SIDA (AIDS), debilita el sistema inmunitario y ocasiona que las mujeres estén en un mayor
                                riesgo de infecciones por VPH.</p>
                            <p>Infección con clamidia: La clamidia es una clase relativamente común de bacteria que puede infectar el sistema reproductor. Se transmite mediante el contacto sexual. Las mujeres infectadas con clamidia a menudo no presentan
                                síntomas y es posible que no sepan que están infectadas a menos que se les hagan pruebas durante un examen pélvico. La infección con clamidia puede causar inflamación de la pelvis que puede conducir a la infertilidad.</p>
                            <p>Uso prolongado de anticonceptivos orales (píldoras de control natal): Existe evidencia de que el uso de anticonceptivos orales (píldoras de control natal) por períodos prolongados aumenta el riesgo de cáncer de cuello uterino.
                                Los estudios de investigación sugieren que el riesgo de cáncer de cuello uterino aumenta mientras más tiempo una mujer tome las píldoras, pero el riesgo se reduce nuevamente después de suspender las píldoras, y el riesgo
                                regresa a lo normal muchos años después de suspenderlas.</p>
                            <p>Virus del Papiloma Humano: El factor de riesgo más importante del cáncer de cuello uterino es la infección por el virus del papiloma humano.</p>
                            <p>Tener muchos embarazos a término: Las mujeres que han tenido tres o más embarazos a término (completos) tienen un mayor riesgo de padecer cáncer de cuello uterino. Se cree que esto se debe probablemente a una mayor exposición
                                a la infección por VPH con la actividad sexual. Además, algunos estudios han indicado que los cambios hormonales durante el embarazo podrían causar que las mujeres sean más susceptibles a infección con VPH o crecimiento
                                tumoral. También se cree que las mujeres embarazadas podrían tener sistemas inmunitarios más débiles, lo que permite la infección por VPH y crecimiento tumoral.</p>
                            <p>Edad temprana en el primer embarazo a término (completo): Las mujeres que tuvieron su primer embarazo a término a la edad de 20 años o menos son más propensas a llegar a tener cáncer de cuello uterino posteriormente en la vida
                                que las que tuvieron su primer embarazo a los 25 años o después.</p>
                            <p>Situación económica: Muchas mujeres con bajos ingresos no tienen acceso fácil a servicios adecuados de atención a la salud, incluyendo la detección del cáncer de cuello uterino con pruebas de Papanicolaou o de detección del
                                VPH. Esto significa que es posible que no se hagan las pruebas de detección ni reciban tratamiento para precánceres de cuello uterino.</p>
                            <p>Una alimentación con pocas frutas y verduras: Las mujeres con una alimentación que no incluya suficientes frutas, ensaladas y verduras pueden tener un mayor riesgo de cáncer de cuello uterino.</p>
                            <p>Antecedente familiar de cáncer de cuello uterino: El cáncer de cuello uterino puede ocurrir con mayor frecuencia en algunas familias. Si su madre o hermana tuvieron cáncer de cuello uterino, sus probabilidades de desarrollar
                                la enfermedad aumentan en comparación a si nadie en la familia lo ha tenido.</p>
                        </div>

