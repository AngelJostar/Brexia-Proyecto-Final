                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tipos de Cáncer Cervicouterino</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer cervicouterino se clasifica según el aspecto que presenta al observarlo con un microscopio en el laboratorio. Los dos tipos más comunes de cáncer cervicouterino son el carcinoma de células escamosas y el adenocarcinoma.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-cervicouterino/pos_de_cancer_cervicouterino-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <ul class="secciones-enumeradas">
                                <li>La mayoría (9 de 10 casos) del cáncer de cuello uterino son carcinomas de células escamosas. Estos cánceres se desarrollan a partir de células en el exocérvix. Los carcinomas de células escamosas se originan con mayor frecuencia
                                    en la zona de transformación (donde el exocérvix se une al endocérvix).</li>
                                <li>La mayoría de los otros cánceres cervicales son adenocarcinomas que se originan de células glandulares. El adenocarcinoma cervical se origina en las células de las glándulas productoras de mucosidad del endocérvix.</li>
                                <li>El cáncer de cuello uterino que tiene características tanto de los carcinomas de células escamosas como de los adenocarcinomas es menos común. Estos tumores se llaman carcinomas adenoescamosos o carcinomas mixtos.</li>
                            </ul>
                            <p>Aunque casi todo el cáncer cervicouterino son carcinomas de células escamosas o adenocarcinomas, otros tipos de cáncer también se pueden desarrollar en el cuello uterino. Estos otros tipos, como melanoma, sarcoma y linfoma,
                                ocurren con más frecuencia en otras partes del cuerpo. (Solamente los tipos más comunes de cáncer de cuello uterino se presentan en este sitio).</p>
                        </div>
