                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer Cervicouterino</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento del cáncer cervicouterino varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. En Brexia, nuestros expertos en cáncer están capacitados
                                y tienen experiencia en los tratamientos disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes.</p>
                            <p>El tratamiento del cáncer cervicouterino depende del tamaño, la ubicación y el estadio del tumor.</p>
                            <p>Las pacientes deben estar plenamente informadas e involucradas en las decisiones sobre las opciones de tratamiento.</p>
                            <p>La cirugía es la piedra angular del manejo del cáncer cervicouternino en sus primeras etapas. La enfermedad más avanzada puede tratarse con quimiorradioterapia, quimioterapia, radioterapia o terapias dirigidas.</p>
                            <p>Las opciones más comunes para tratar cáncer de cervicouterino (Cuello uterino) son:</p>
                            <p>Neoplasia intraepitelial cervical no invasiva: En la neoplasia intraepitelial cervical (NIC) no invasiva, las células del cuello uterino muestran cambios anormales que pueden progresar hasta convertirse en cáncer de cuello
                                uterino en el futuro. Algunas pacientes con NIC no requieren tratamiento, pero otras se someten a un procedimiento (por ejemplo, a una escisión electroquirúrgica con asa o conización) para eliminar el área de células anormales.</p>
                            <p>Cáncer de cuello uterino en estadio temprano: Las mujeres con enfermedad invasiva en estadio temprano normalmente se someten a cirugía para extirpar el cáncer. Generalmente se les realiza una histerectomía y también puede que
                                se les extirpen algunos ganglios linfáticos pélvicos. Las pacientes que se considera que presentan un alto riesgo de recidiva del cáncer pueden recibir quimiorradioterapia adyuvante después de la cirugía. Para las pacientes
                                que deseen tener hijos en el futuro pueden estar disponibles opciones de cirugía de preservación de la fertilidad (por ejemplo, se puede realizar una traquelectomía en lugar de una histerectomía para preservar el útero).</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-cervicouterino/Tratamiento_de_cancer-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Cáncer de cuello uterino localmente avanzado: La enfermedad localmente avanzada generalmente se trata en quimiorradioterapia. A algunas pacientes se les puede ofrecer quimioterapia neoadyuvante para reducir el tumor, seguida
                                de cirugía.</p>
                            <p>Cáncer de cuello uterino metastásico: La enfermedad metastásica normalmente se trata con quimioterapia en combinación con terapia dirigida. La radioterapia paliativa se puede utilizar para tratar ciertos síntomas que surgen
                                a raíz de las metástasis.</p>
                            <p>Cáncer de cuello uterino recidivante: El tratamiento de la enfermedad recidivante depende de la extensión de la recidiva. Si el tumor vuelve a aparecer como una recidiva en un solo sitio de la pelvis, se puede ofrecer radioterapia
                                o exenteración pélvica. Los tumores recidivantes en órganos distantes se consideran cánceres metastásicos y pueden tratarse con quimioterapia con o sin terapia dirigida.</p>
                            <p>Seguimiento después del tratamiento: La organización de las citas de seguimiento variará según su caso. En cada visita, su médico la examinará y la someterá a un examen pélvico. Puede que también le realicen una tomografía
                                computarizada o una tomografía por emisión de positrones. Los grupos de apoyo pueden ayudar a las pacientes y a sus familias a comprender mejor el cáncer de cuello uterino y a aprender a hacer frente a todos los aspectos
                                de la enfermedad, desde el diagnóstico hasta los efectos físicos y emocionales a largo plazo.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Pubntos importantes del Tratamiento de Cáncer Cervicouterino</h2>

                            <ul class="secciones-enumeradas">
                                <li>Hay diferentes tipos de tratamiento para las pacientes de cáncer de cuello uterino. Se usan cinco tipos de tratamiento estándar:</li>
                                <ul>
                                    <li>Cirugía</li>
                                    <li>Radioterapia</li>
                                    <li>Quimioterapia</li>
                                    <li>Terapia dirigida</li>
                                    <li>Inmunoterapia</li>
                                </ul>
                                <li>Se están probando nuevos tipos de tratamiento en ensayos clínicos.</li>
                                <li>A veces el tratamiento del cáncer de cuello uterino causa efectos secundarios.</li>
                                <li>Los pacientes podrían considerar la participación en un ensayo clínico.</li>
                                <li>A veces se necesitan pruebas de seguimiento.</li>
                            </ul>
