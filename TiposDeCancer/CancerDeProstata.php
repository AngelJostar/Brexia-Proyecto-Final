                                    posiblemente también con terapia adyuvante de privación de andrógenos.</li>
                                <li>Cáncer de Próstata metastasico. La enfermedad metastásica normalmente se tratará con terapia de privación de andrógenos, ya sea sola o en combinación con bloqueadores de la síntesis de testosterona o quimioterapia. Si el
                                    cáncer continúa creciendo a pesar del tratamiento con terapia de privación de andrógenos (también conocido como cáncer de próstata resistente a la castración [CPRC] o cáncer de próstata hormono-refractario [CPHR]),
