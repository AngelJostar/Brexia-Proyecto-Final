                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué causa el cáncer de próstata?</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer de próstata se forma cuando el ADN en las células de la próstata desarrolla mutaciones que pueden desactivar su capacidad para controlar el crecimiento y la división celular. En muchos casos, estas células mutadas
                                mueren o son atacadas por el sistema inmune. Pero algunas células mutadas pueden escapar del sistema inmune y crecer sin control, formando un tumor de próstata.
                            </p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-prostata/factores-de-riesgo_cancer_prostata-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>
                                Si bien puede no conocerse la causa exacta del cáncer de próstata, el riesgo de desarrollar la enfermedad aumenta con la edad. Además, los hombres con antecedentes familiares de cáncer de próstata tienen un mayor riesgo de desarrollar la enfermedad.
                            </p>
                            <p>
                                Algunos factores de riesgo comunes para el cáncer de próstata incluyen:
                            </p>

                            <ol class="secciones-enumeradas">
                                <li>Raza: los estudios muestran que los hombres afroamericanos tienen aproximadamente un 70 por ciento más de probabilidades de desarrollar cáncer de próstata en su vida que los hombres caucásicos o hispanos.</li>
                                <li>Edad: el riesgo de desarrollar cáncer de próstata aumenta con la edad. Mientras que solo uno de cada 10,000 hombres menores de 40 años será diagnosticado con cáncer de próstata, uno de cada 15 hombres de 60 años será diagnosticado
                                    con la enfermedad.</li>
                                <li>Antecedentes familiares: los hombres con un pariente sanguíneo inmediato, como un padre o hermano, que tiene o tuvo cáncer de próstata, tienen el doble de probabilidades de desarrollar la enfermedad. Para los hombres que
                                    tienen otro miembro de la familia diagnosticado con la enfermedad, aumentan las posibilidades de desarrollar cáncer de próstata.</li>
                                <li>Dieta: una dieta rica en grasas saturadas, así como la obesidad, aumenta el riesgo de cáncer de próstata.</li>
                                <li>Niveles altos de testosterona: los hombres que usan terapia de testosterona tienen más probabilidades de desarrollar cáncer de próstata, ya que un aumento en la testosterona estimula el crecimiento de la glándula prostática.
                                </li>
                                <li>Neoplasia intraepitelial prostática (PIN): esta afección puede estar asociada con un mayor riesgo de cáncer de próstata. El PIN es una condición en la cual las células de la glándula prostática se ven anormales cuando se
                                    examinan con un microscopio. No está necesariamente relacionado con ningún síntoma. Casi la mitad de los hombres serán diagnosticados con PIN antes de los 50 años.</li>
                                <li>Cambios en el genoma: se sabe que ciertos genes aumentan los riesgos de cáncer de próstata, como los genes BRCA1 y BRCA2.</li>
                            </ol>
