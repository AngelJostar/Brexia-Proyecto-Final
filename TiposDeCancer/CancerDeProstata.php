                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Próstata</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento del cáncer de próstata varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer están capacitados y tienen experiencia
                                en los tratamientos disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes.</p>
                            <p>El tratamiento del cáncer de próstata dependerá del tamaño, la localización y el estadio del tumor. Los pacientes con cáncer de próstata deben estar plenamente informados e involucrados en las decisiones sobre sus opciones
                                de tratamiento. Para algunos pacientes, especialmente para los hombres mayores con cáncer de próstata de crecimiento lento, puede que someterse a tratamiento no sea apropiado o necesario. Esto se debe a que son más propensos
                                a morir por la edad avanzada o por otras causas distintas al cáncer de próstata y, por lo tanto, los efectos secundarios negativos que se derivarían del tratamiento del cáncer de próstata podrían ser mayores que cualquier
                                beneficio que pudiera obtenerse con respecto a la esperanza de vida.
                            </p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-prostata/Tratamiento_cancer_de_prostata-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Los enfoques de tratamiento del cáncer de próstata incluyen la vigilancia activa (en la que el cáncer se vigila de cerca y seguidamente se trata en cuanto progresa), la cirugía, la radioterapia, la terapia hormonal (por ejemplo,
                                la terapia de privación de andrógenos, los anti andrógenos o los bloqueadores de la síntesis de testosterona) y la quimioterapia. Estos se explican a continuación:
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Vigilancia activa del Cáncer de Próstata</h2>
                            <p>
                                Nueve de cada 10 hombres diagnosticados con cáncer de próstata tienen enfermedad localizada. Esto quiere decir que el cáncer de próstata no se diseminó fuera de la próstata y generalmente no causa síntomas. Debido a que el cáncer de próstata a menudo
                                crece lentamente, la vigilancia activa puede ser la opción de tratamiento preferida para algunos hombres. En la vigilancia activa, el oncólogo monitorea de cerca la enfermedad con pruebas y pospone el tratamiento hasta
                                una fecha posterior.
                            </p>

                            <p>
                                Los hombres mayores con cáncer de próstata tienen más probabilidades de ser candidatos para la vigilancia activa porque no se ha demostrado que tratarlos con cirugía o radiación los ayude a vivir más tiempo. La decisión de controlar el cáncer de próstata
                                en lugar de tratarla se toma entre un paciente y su médico.
                            </p>
                            <p>
                                En general, la vigilancia activa puede ser una opción para pacientes cuyo cáncer de próstata es:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li>No causa síntomas</li>
                                <li>Se espera que crezca lentamente</li>
                                <li>Pequeño y contenido dentro de la próstata</li>
                            </ul>
                            <p>
                                Los pacientes con cáncer de próstata bajo vigilancia activa pueden recibir pruebas de antígeno prostático y biopsias con regularidad cada uno o dos años. Se considerarían otras opciones de tratamiento si los niveles de antígeno prostático de un paciente
                                aumentan rápidamente, si su médico encuentra cambios en su examen rectal o si desarrolla nuevos síntomas.
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Quimioterapia en Cáncer de Próstata</h2>
                            <p>
                                Antes de recibir quimioterapia para el cáncer de próstata, puede recibir medicamentos previos para ayudar a que los síntomas sean más tolerables. Los posibles efectos secundarios de la quimioterapia pueden incluir náuseas y vómitos, pérdida de cabello
                                y llagas en la boca. Su equipo de atención utilizará múltiples medidas para ayudar a reducir o moderar los síntomas relacionados con la quimioterapia.
                            </p>
                            <p>
                                Durante la quimioterapia, su equipo de atención ofrecerá servicios de atención de apoyo para ayudar a aliviar los efectos secundarios. Por ejemplo, nuestros especialistas pueden sugerir suplementos para reducir las náuseas. Además, un terapeuta puede
                                recomendar técnicas para ayudarlo a relajarse y sentirse menos ansioso durante sus tratamientos de quimioterapia contra el cáncer de próstata.
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Terapia Hormonal en Cáncer de Próstata</h2>
                            <p>
                                La terapia hormonal para el cáncer de próstata priva a las células cancerosas de las hormonas masculinas que necesitan para crecer. La terapia hormonal para el cáncer de próstata a menudo se usa en combinación con radiación y otras terapias. Podemos utilizar
                                la terapia hormonal para reducir los tumores avanzados de cáncer de próstata, para que puedan tratarse con radiación.
                            </p>
                            <p>
                                En Brexia utilizamos enfoques innovadores en la terapia hormonal, atacando la enfermedad en múltiples frentes con una combinación de medicamentos. Un régimen común para la terapia del cáncer de próstata utiliza una combinación de dos o más medicamentos
                                para reducir el nivel de testosterona y otras hormonas que pueden alimentar la enfermedad. En algunos casos, la terapia hormonal se puede administrar de forma intermitente para ayudar a reducir los efectos secundarios relacionados
                                con el tratamiento.
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Inmunoterapia en Cáncer de Próstata</h2>
                            <p>
                                La inmunoterapia es un tratamiento prometedor para el cáncer de próstata, que incluye formas avanzadas o recurrentes de la enfermedad. Su equipo de atención en Brexia utiliza una variedad de estrategias inmunes para matar un tumor de cáncer de próstata
                                y prevenir su recurrencia. Este método de tratamiento puede usarse solo o junto con otros tratamientos, como la radioterapia y la terapia hormonal.
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Radioterapia en Cáncer de Próstata</h2>
                            <p>
                                Con los sistemas avanzados de administración de radioterapia, nuestros oncólogos radioterapeutas pueden detectar tumores difíciles de alcanzar en la próstata. Además, nuestros oncólogos radioterapeutas pueden administrar dosis de radiación más altas en
                                las células de cáncer de próstata, al tiempo que reducen la exposición al tejido sano normal.
                            </p>
                            <p>
                                Ofrecemos dos tipos principales de radioterapia para el tratamiento del cáncer de próstata:
                            </p>
                            <ol class="secciones-enumeradas">
                                <li>Radioterapia de haz externo (EBRT): administra altas dosis de radiación a las células de cáncer de próstata desde fuera del cuerpo, utilizando una variedad de tecnologías basadas en máquinas</li>
                                <li>Braquiterapia de alta tasa de dosis (radiación interna): administra altas dosis de radiación de implantes colocados cerca o dentro de los tumores en el cuerpo</li>
                            </ol>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Cirugía de Cáncer de Próstata</h2>
                            <p>
                                Las cirugias para el cáncer de próstata pueden ser una opción para los hombres que califican según su salud general y otros factores. La cirugía está diseñada para extirpar el cáncer de próstata mediante una operación abierta (tradicional) o con un equipo
                                robótico. Un ejemplo de cirugía para el cáncer de próstata es la extirpación de la próstata, llamada prostatectomía. Una de las opciones preferidas para tratar el cáncer de próstata limitado a órganos es la prostatectomía
                                radical. El sistema quirúrgico Da Vinci le permite al cirujano ofrecer este procedimiento utilizando un enfoque mínimamente invasivo.
                            </p>
                            <p>
                                Algunas ventajas de la cirugía de cáncer de próstata pueden incluir:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li>Los pacientes con cáncer localizado pueden no necesitar tratamiento adicional.</li>
                                <li>La biopsia simultánea permite una estadificación más precisa.</li>
                                <li>Los niveles de antígeno prostático posquirúrgicos predicen de manera confiable la recurrencia del cáncer.</li>
                                <li>Menos efectos secundarios intestinales o rectales que con los tratamientos de radiación</li>
                                <li>Menor riesgo de urgencia y frecuencia urinarias que con los tratamientos de radiación.</li>
                            </ul>
                            <p>
                                Algunas desventajas de la cirugía de cáncer de próstata pueden incluir:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li class="">Riesgos relacionados con la cirugía, incluidos los efectos secundarios de la anestesia general.</li>
