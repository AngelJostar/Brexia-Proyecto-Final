                        <h2 class="centrar-texto brexia-color encabezados-brexia paddingTop-2 ">Qué es el Cáncer de Próstata?</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer de próstata se forma en las células de la glándula prostática. Muchos tipos de cáncer de próstata crecen lentamente, siendo poco probable que se diseminen, pero algunos pueden crecer más rápidamente.
                            </p>
                            <P>
                                Se desconocen las causas exactas del cáncer de próstata y en sus primeras etapas no suele presentar síntomas.
                            </P>
                            <P>
                                El cáncer de próstata es el segundo cáncer más común en los hombres de todo el mundo y afecta principalmente a hombres mayores. Más de la mitad del cáncer de próstata se presenta en hombres mayores de 70 años.
                            </P>
                            <P>
                                La próstata es una glándula (glándula prostática) en el aparato reproductor masculino. La próstata está justo debajo de la vejiga (el órgano que recibe y expulsa la orina) y delante del recto (la parte inferior del intestino). Es casi del tamaño de una
                                nuez y rodea parte de la uretra (el tubo que vacía la orina de la vejiga). La glándula prostática elabora un líquido que es parte del semen.
                            </P>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/cancer-de-prostata/Cancer_de_prostata-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>
                                El cáncer de próstata en su etapa inicial normalmente no presenta síntomas. Los síntomas que pueden aparecer a medida que el cáncer progresa, como el aumento de la frecuencia con que se orina y la dificultad o la urgencia para orinar, suelen estar causados
                                por la presión que el cáncer ejerce sobre la uretra.
                            </p>
                            <p>
                                El cáncer de próstata se “estadifica” de acuerdo con el tamaño del tumor, la afectación de los ganglios linfáticos y el hecho de que se haya diseminado a los huesos o a otras partes del cuerpo. Esta información se utiliza para ayudar a decidir el mejor
                                tratamiento.
                            </p>
                        </div>
