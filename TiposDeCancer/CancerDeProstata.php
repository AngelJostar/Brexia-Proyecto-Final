                                        o braquiterapia.</li>
                                    <li class="">Los pacientes con enfermedad de riesgo intermedio podrán recibir vigilancia activa, prostatectomía radical o radioterapia con o sin terapia de privación de andrógenos neoadyuvante y concurrente.</li>
                                    <li class="">Los pacientes con enfermedad de alto riesgo podrán ser tratados con prostatectomía radical con linfadenectomía pélvica (extirpación de los ganglios linfáticos pélvicos) o radioterapia en la próstata y los ganglios linfáticos
