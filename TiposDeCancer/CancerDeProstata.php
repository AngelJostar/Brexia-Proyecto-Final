                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Próstata</h2>
                        <div class="font-16 fontW-400">
                            <p>El diagnóstico del cáncer de próstata generalmente se basa en los resultados del examen clínico de la próstata y de un análisis de sangre en el que se verifican los niveles de una proteína llamada antígeno prostático específico
                                (PSA, por sus siglas en ingles), así como en los resultados de una biopsia.</p>

                            <p>Estudios adicionales pueden ayudar a determinar lo avanzado que esta el cáncer de próstata. Por ejemplo, se pueden usar unas exploraciones denominadas imágenes por resonancia magnética (IRM), tomografía por emisión de positrones
                                (PET) y tomografía computarizada (TC) para ver hasta donde se ha diseminado el cáncer. Los ganglios linfáticos en la pelvis también se pueden extirpar y examinar para verificar si contienen células cancerosas.
                            </p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-prostata/Cancer_de_prostata-768x512.webp" alt="cp">
                            </div>
                            <p>Los tipos de pruebas que puede someterse a un diagnóstico de cáncer de próstata incluyen las que se enumeran a continuación:</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Antígeno Prostático en Cáncer de Próstata</h2>
                            <p>El antígeno prostático específico (PSA) es una proteína que se encuentra en las células de la próstata que ayuda a mantener el semen licuado. La mayoría de los casos de cáncer de próstata se desarrollan en estas células, por
                                lo que un recuento elevado de antígeno prostático puede ser un signo de cáncer de próstata.</p>
                            <p>Para la mayoría de los hombres, un nivel de antígeno prostático de 4 nanogramos por mililitro (ng / ml) de sangre es normal. Los médicos pueden establecer un nivel basal de antígeno prostático más bajo para los hombres con
                                mayor riesgo, como los afroamericanos y aquellos con antecedentes familiares de cáncer de próstata.</p>
                            <p>No todos los hombres con niveles altos de antígeno prostático tienen cáncer de próstata. Los niveles altos de antígeno prostático también pueden ser causados ​​por una infección del tracto urinario, prostatitis o hiperplasia
                                prostática benigna, todas las cuales son afecciones no cancerosas. Pero:</p>
                            <ul class="secciones-enumeradas">
                                <li class="">Los hombres con un nivel de antígeno prostático entre 4 y 10 tienen un 25 por ciento de tener cáncer de próstata</li>
                                <li class="">Los hombres con un nivel de antígeno prostático superior a 10 tienen una probabilidad del 50 por ciento de tener cáncer de próstata.</li>
                                <li class="">Los hombres con un nivel bajo de antígeno prostático aún pueden desarrollar cáncer de próstata. Las pruebas de antígeno prostático no son una indicación de cuán agresivo puede ser el cáncer de próstata. Muchos tipos de
                                    cáncer de próstata crecen lentamente y no requieren tratamiento inmediato.</li>
                            </ul>
                            <p>El antígeno prostático se puede medir con un análisis de sangre que a menudo se realiza junto con un examen rectal digital, que le permite a su médico sentir la próstata en busca de anomalías en la forma, el tamaño y la textura.
                                Los hombres sanos con un riesgo bajo o promedio de desarrollar cáncer de próstata deben hablar con su médico acerca de los exámenes de detección a los 50 años. Los hombres con un riesgo más alto deben hablar con su médico
                                acerca de los exámenes de detección a los 40 o 45 años.
                            </p>
                            <p>
                                Recomendaciones en cuanto a la periodicidad de los examenes de antígeno prostático en hombres según su edad:</p>
                            </p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-prostata/Diagnostico_cancer_prostata-768x372.webp" alt="cp">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Biopsia de Cáncer de Próstata</h2>
                            <p>
                                Durante una biopsia de cáncer de próstata, su médico guía una pequeña sonda en el recto y extrae una pequeña muestra de tejido de la próstata para un examen más detallado. Este tejido le permite al médico estadificar la enfermedad y determinar un plan
                                de tratamiento apropiado.
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Examen de tacto rectal en Cáncer de Próstata</h2>
                            <p>Un examen de tacto rectal suele ser el primer paso para determinar la salud de la próstata y, a menudo, se realiza como parte de un examen de rutina anual para detectar cáncer de próstata.
                            </p>
                            <p>
                                Debido a la proximidad anatómica de la próstata con el recto hace que sea fácilmente accesible a través del orificio anal.
                            </p>
                            <p>
                                El tacto rectal es una exploración simple y sencilla de realizar para la que no se necesita ningún tipo de tecnología específica. Se lleva a cabo en la misma consulta del médico. Consiste en la introducción de un dedo del explorador, protegido por un
                                guante y lubricado, a través del ano. Permite palpar la superficie de la próstata situada en la parte anterior del recto.
                            </p>
                            <p>
                                Se trata de una maniobra que puede resultarte algo desagradable o incómoda, pero en absoluto dolorosa. Es una prueba de corta duración, que permite obtener una valiosa información clínica al urólogo.
                            </p>
                            <p>
                                Las alteraciones que se pueden detectar mediante un tacto rectal y que nos pueden hacer sospechar la presencia de un cáncer de próstata son:
                            </p>

                            <ul class="secciones-enumeradas">
                                <li class="">Nódulos o irregularidades en la superficie</li>
                                <li class="">Aumento de la consistencia en una porción de la próstata o de manera difusa.</li>
                                <li class="">Alteración en los bordes de la glándula</li>
                            </ul>
                            <p>
                                Aunque la palpación de la próstata sea normal no excluye la presencia de un posible foco de cáncer, tan sólo expresa que no es palpable. Existen tipos de cáncer de próstata que bien por su pequeño tamaño o bien porque se hallan situados en zonas internas
                                no palpables resultan indetectables mediante una exploración de este tipo.
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Pruebas de imagen en Cáncer de Próstata</h2>
                            <p>
                                Se pueden usar varias pruebas de imágenes para determinar el tipo, la etapa y la progresión de su cáncer de próstata, que incluyen:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li class="">Exploración ósea: el cáncer de próstata puede hacer metástasis a los huesos. Si experimenta dolor óseo o los análisis de sangre revelan niveles elevados de calcio, su oncólogo radiólogo puede realizar un escaneo óseo para
                                    detectar si el cáncer de próstata se ha diseminado a los huesos.</li>
                                <li class="">Tomografía computarizada: una tomografía computarizada revela el flujo sanguíneo y la anatomía de los tejidos dentro y alrededor de la próstata, lo que permite el diagnóstico y el control del crecimiento tumoral.</li>
                                <li class="">MRI: la resonancia magnética (MRI) puede ser especialmente útil para la próstata, debido al mayor contraste de los tejidos blandos.</li>
                                <li class="">PET / CT: una PET / CT es útil para el cáncer de próstata porque los escáneres pueden revelar células cancerosas antes de que haya tumores o cambios estructurales. Esto es importante para detectar la enfermedad temprano.
                                    Podemos utilizar el escáner GE Discovery ™ PET / CT 600, un escáner CT de cuatro dimensiones que produce imágenes detalladas de rayos X de estructuras transversales de las estructuras internas del cuerpo. También permite
                                    a los radiólogos planificar el tratamiento de acuerdo con los patrones de respiración de los pacientes.</li>
                                <li class="">Ultrasonido: esta tecnología se puede utilizar para controlar el tamaño de la próstata, la respuesta tumoral o la actividad en otros tejidos. Al detectar el cáncer de próstata, su médico también puede recomendarle una ecografía
                                    transrectal. Durante este examen, se inserta una pequeña sonda en el recto para que las ondas de sonido puedan proporcionar una imagen de la próstata y medir su tamaño. Las imágenes revelarán tumores, calcificaciones
                                    y agrandamiento de la próstata.</li>
                            </ul>

