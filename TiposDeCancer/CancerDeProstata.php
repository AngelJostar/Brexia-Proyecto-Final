                        <div class="font-16 fontW-400">
                            <p>En la mayoría de los casos, los síntomas del cáncer de próstata no se expresan en las primeras etapas de la enfermedad. Los síntomas del cáncer de próstata pueden ser diferentes para cada hombre, y cualquiera de estos síntomas
                                puede ser causado por otras afecciones que no sean cáncer de próstata. Por esto es muy importante hacerse los exámenes de rutina como se debe.</p>
