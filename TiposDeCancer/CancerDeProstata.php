                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas tempranos del cáncer de próstata</h2>
                            <p>Debido a la proximidad de la glándula prostática a la vejiga y la uretra, el cáncer de próstata puede estar acompañado de una variedad de síntomas urinarios, especialmente en las primeras etapas. Dependiendo de su tamaño y
                                ubicación, un tumor puede presionar y contraer la uretra, inhibiendo el flujo de orina. Algunos síntomas tempranos de cáncer de próstata incluyen:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li>Ardor o dolor al orinar</li>
                                <li>Dificultad para orinar o problemas para comenzar y detenerse mientras orina</li>
                                <li>Necesidades más frecuentes de orinar por la noche.</li>
                                <li>Pérdida del control de la vejiga.</li>
                                <li>Disminución del flujo o la velocidad del flujo de orina.</li>
                                <li>Sangre en orina (hematuria)</li>
                                <li>Sangre en semen</li>
                                <li>Dificultad para tener una erección (disfunción eréctil)</li>
                                <li>Eyaculacion dolorosa</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas avanzados del cáncer de próstata</h2>
                            <P>El cáncer de próstata puede diseminarse (hacer metástasis) y formar tumores en órganos o huesos cercanos. Si el cáncer de próstata se propaga a la columna vertebral, puede presionar los nervios espinales. Los signos de cáncer
                                de próstata metastásico pueden incluir:</P>
                            <ul class="secciones-enumeradas">
                                <li>Hinchazón en las piernas o el área pélvica.</li>
                                <li>Entumecimiento o dolor en las caderas, piernas o pies.</li>
                                <li>Dolor en los huesos que no desaparece o provoca fracturas.</li>
                            </ul>
                            <p>Los síntomas del cáncer de próstata a menudo difieren de un paciente a otro. El primer síntoma más común de cáncer de próstata recurrente es un aumento en el nivel de antígeno prostático en la sangre, lo que hace que las pruebas
                                regulares de antígeno prostático sean aún más importantes para medir el progreso del tratamiento y verificar si hay signos de recurrencia. Es importante informar a su médico sobre nuevos signos o síntomas.
                            </p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas del cáncer de próstata recurrente</h2>
                            <P>El cáncer de próstata que reaparece después del tratamiento se considera recurrente. Cuando regresa al área alrededor de la próstata, la enfermedad se clasifica como una recurrencia local. Si el cáncer de próstata se encuentra
                                en otra parte del cuerpo, el cáncer recurrente se considera metastásico. Si el cáncer hace metástasis (o se disemina) fuera de la próstata, lo más probable es que se desarrolle primero en los ganglios linfáticos cercanos.
                                El cáncer de próstata metastásico se disemina con mayor frecuencia al hígado, los huesos y los pulmones.
                            </P>
                            <p>
                                Después del tratamiento inicial para el cáncer de próstata, se espera que los niveles de antígeno prostático disminuyan drásticamente. El primer signo de cáncer de próstata recurrente puede ser un aumento en el nivel de antígeno prostático. Otros síntomas
                                de cáncer recurrente pueden depender de si el cáncer se diseminó y dónde. Los síntomas incluyen:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li>Sangre en la orina</li>
                                <li>Dificultad para orinar</li>
                                <li>Dolor de espalda baja</li>
                                <li>Fatiga</li>
                                <li>Respiración dificultosa</li>
                                <li>Ictericia</li>
