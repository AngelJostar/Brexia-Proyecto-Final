                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas del Cáncer de mama</h2>
                        <div class="font-16 fontW-400">
                            <p>Los signos de advertencia del cáncer de mama pueden ser distintos en cada mujer.</p>
                            <p>Algunas personas no tienen ningún tipo de signos o síntomas. Sin embargo, si resulta posible que las mujeres con la enfermedad presenten cambios en las mamas o síntomas.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-mama/sintomas-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Muchas veces la causa de estos síntomas puede ser otra afección médica diferente que no sea cáncer. Algunas señales de advertencia del cáncer de mama y que deben de hacer buscra una valoración con el especialista incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Dolor en cualquier parte de la mama</li>
                                <li>Un bulto que se palpa como un nudo firme o un engrosamiento de la mama o debajo del brazo. Es importante palpar la misma zona de la otra mama para asegurarse de que el cambio no sea parte del tejido mamario sano de esa
                                    área.
                                </li>
                                <li>Cualquier cambio en el tamaño, grosor o la forma de la mama.</li>
                                <li>Que una de las mamas se vea distinta a la otra.</li>
                                <li>Secreción del pezón que se produce de forma repentina, contiene sangre o se produce solo en una mama.</li>
                                <li>Cambios físicos, como irritación, hundimientos, pezón invertido hacia dentro o una llaga en la zona del pezón.</li>
                                <li>Irritación de la piel o cambios en esta, como rugosidades, hoyuelos, escamosidad o pliegues nuevos.</li>
                                <li>Mamas enrojecidas, tibias e hinchadas, con o sin erupción cutánea con rugosidad que se asemeja a la piel de una naranja, llamada “piel de naranja”.</li>
                                <li>Dolor en la mama, particularmente, dolor en la mama que no desaparece. El dolor generalmente no es un síntoma de cáncer de mama, pero debe comunicarse al médico.</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué significa si encuentro un bulto en mis mamas?</h2>
                            <p>Muchas afecciones pueden causar bultos en las mamas, entre ellas el cáncer. Sin embargo, la mayoría de los bultos en las mamas son causados por otras afecciones médicas. Las dos causas más comunes de bultos en las mamas son
                                la enfermedad fibroquística y los quistes. La enfermedad fibroquística produce cambios en las mamas que no son cancerosos y que pueden causar bultos, sensibilidad al tacto y dolor. Los quistes son pequeños sacos llenos
                                de líquido que pueden formarse en las mamas</p>
