                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tipos de Cáncer de Mama</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer de mama se puede dividir en varios tipos en función de las características de las células de la mama a partir de las cuales se desarrolla y también, en función del grado de extensión de la enfermedad y de su evolución
                                a lo largo del tiempo.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-mama/tipos-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Clasificación Morfológica</h2>
                            <p>La mayoría de estos tumores se originan en el tejido glandular de la mama, que está formado por los lobulillos, glándulas donde se produce la leche, y los conductos galactóforos, que son los conductos encargados de transportarla
                                hasta el pezón durante la lactancia materna. Estos cánceres son de origen epitelial y se denominan carcinomas.</p>
                            <p>Los tres tipos de tumores más frecuentes son:</p>
                            <ul class="secciones-enumeradas">
                                <li>Carcinoma ductal invasivo o infiltrante (CDI)</li>
                                <li>Carcinoma lobulillar invasivo o infiltrante (CLI)</li>
                                <li>Carcinoma ductal “in situ” o carcinoma intraductal (CDIS)</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Otros Tipos de Cáncer de Mama</h2>
                            <p>A continuación vamos a hablar de otros tipos de cáncer de mama desde el punto de vista molecular que se diagnostican con menos frecuencia:</p>
                            <ul class="secciones-enumeradas">
                                <li>Carcinoma inflamatorio de mama</li>
                                <li>Carcinoma lobulillar “in situ” (CLIS)</li>
                                <li>La enfermedad de Paget de la mama</li>
