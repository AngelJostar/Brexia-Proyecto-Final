                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico de Cáncer de Mama</h2>
                        <div class="font-16 fontW-400">
                            <p>Los médicos requerirán de realizar distintas pruebas para diagnosticar el cáncer de mama. También realizarán pruebas para determinar si el cáncer se ha determinado a una parte del cuerpo mas allá de la mama o los ganglios linfáticos.
                                Cuando esto sucede, se lo denomina metástasis.</p>
                            <p>Los médicos especialistas también requerirán hacer pruebas para determinar qué tratamientos funcionaran mejor. En la mayor parte de los casos de sospecha de cáncer de mama, una biopsia es la única manera segura para que el
                                médico determine el subtipo especificico de cáncer.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-mama/diagnostico-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Los diferentes métodos en el proceso de Diagnosticar Cáncer de Mama son las siguientes:</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Examen Clínico de las Mamas</h2>
                            <p>Un examen clínico de las mamas es un examen hecho por un médico o una enfermera que usa sus manos para detectar bultos u otros cambios.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Mastográfia</h2>
                            <p>La mamografía es una radiografía de la mama. Las mamografías suelen utilizarse para detectar cáncer de mama. Si se detecta una anomalía en una mamografía de detección, el médico puede recomendar una mamografía de diagnóstico
                                para evaluar con mayor detalle esa anomalía.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Ultrasonido Mamario</h2>
                            <p>El Ultrasonido utiliza ondas sonoras para producir imágenes de estructuras profundas del cuerpo. La ecografía puede usarse para determinar si un nuevo nódulo mamario es una masa sólida o un quiste lleno de líquido.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Imagen por resonancia magnética de las mamas</h2>
                            <p>En la resonancia magnética de las mamas se utilizan imanes y ondas de radio para tomar fotografías de las mamas. La resonancia magnética se utiliza junto con las mamografías para examinar a las mujeres que tienen un riesgo
                                alto de presentar cáncer de mama. No se usa en las mujeres que tienen un riesgo promedio porque la resonancia magnética de las mamas puede tener un resultado anormal aun cuando no haya cáncer.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Biopsia</h2>
                            <p>La biopsia es la única forma definitiva de hacer un diagnóstico de cáncer de mama. En la biopsia, el médico usa una aguja especializada guiada por</p>
                            <p>radiografía u otra prueba de diagnóstico por imágenes para extraer un núcleo de tejido del área sospechosa. Con frecuencia, se deja un pequeño marcador metálico en el sitio dentro de la mama para poder identificar el área con
                                mayor facilidad en pruebas de diagnóstico por imágenes futuras.</p>
                            <p>Las muestras de la biopsia se envían a un laboratorio para que los expertos las analicen y determinen si las células son cancerosas. Una muestra de biopsia también se analiza para determinar el tipo de células involucradas
                                en el cáncer de mama, la agresividad (grado) del cáncer, y si las células cancerosas tienen receptores hormonales u otros receptores que puedan influir en las opciones de tratamiento.</p>
                            <p>La American Cancer Society ha elaborado las recomendaciones siguientes para la detección selectiva del cáncer de mama.</p>
                            <ol class="secciones-enumeradas">{
                                <li>Las mamografías deben realizarse anualmente a partir de los 40 años.</li>
                                <li>Exploración clínica de la mama por parte de un profesional sanitario aproximadamente cada 3 años en las mujeres desde los 20 a los 40 años yanualmente por encima de los 40.</li>
                                <li>Las mujeres deben practicar una autoevaluación de sus mamas y comunicar cualquier cambio a su médico.</li>
                                <li>Las mujeres con riesgo elevado de cáncer de mama basándose en ciertos factores, como aquellas con mutación genética, deben someterse a una mamografía y a una resonancia magnética (RM) cada año.</li>
                            </ol>
