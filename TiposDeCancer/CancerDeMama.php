                                los cánceres de mama se detectan en mujeres de 50 años o más.</p>
                            <p>Algunas mujeres tienen cáncer de mama aunque no tengan ningún otro factor de riesgo conocido. Tener un factor de riesgo no significa que la persona tendrá la enfermedad, y no todos los factores de riesgo tienen el mismo efecto.
                                La mayoría de las mujeres tienen algunos factores de riesgo, pero la mayoría de las mujeres no tienen cáncer de mama.</p>
