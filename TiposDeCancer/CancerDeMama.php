                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cáncer de Mama</h2>
                        <div class="font-16 fontW-400">
                            <p>Si se diagnostica cáncer de mama, se hacen otras pruebas para averiguar si las células cancerosas se han diseminado dentro de la mama o a otras partes del cuerpo. Este proceso se llama estadificación.</p>
                            <p>El estadio del cáncer de mama se determina por la localización del cáncer, es decir, si está únicamente en la mama o si se encuentra en los ganglios linfáticos debajo del brazo o si se ha diseminado fuera de la mama. El tipo
                                y estadio del cáncer de mama les indica a los médicos cuál es el tipo de tratamiento que usted necesita.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-mama/estadificacion-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>En el caso del cáncer de mama, se habla de cinco estadios (del 0 al IV), el estadio 0 corresponde a aquellos casos en los que se diagnostica un carcinoma in situ (no invasivo) y el resto de los cuatro estadios corresponden
                                al cáncer de mama invasivo o infiltrante, siendo el estadio en el que la enfermedad está en una fase más inicial y el estadio IV en el que la enfermedad ya se ha extendido a otros órganos del cuerpo. En general, el pronóstico
                                de los pacientes empeora a medida que el estadio es más avanzado, lo que supone que las tasas de curación son mayores cuando el cáncer es diagnosticado en estadios más iniciales.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cancer de Mama</h2>
                            <p>El sistema utilizado para la estadificación del cáncer de mama es el sistema TNM. Las letras TNM significan:</p>
                            <ol class="secciones-enumeradas">
                                <li>1) T (de tumor). Tamaño del tumor primario localizado en la mama, pudiendo presentar valores diferentes según la medida más grande del tumor:</li>
                                <ul>
                                    <li>T0:no hay evidencia del tumor.</li>
                                    <li>Tis: cánceres no invasivos.</li>
                                    <li>T1:igual o inferior a 2 cm.</li>
                                    <li>T2:entre 2 y 5 cm.</li>
                                    <li>T3:mayor de 5 cm.</li>
                                    <li>T4:el tumor se ha extendido a la piel de la mama.</li>
                                </ul>
                                <li>2) N (de node, ganglio en inglés). Grado de afectación de los ganglios linfáticos que están próximos a la mama, pudiendo presentar valores diferentes en función del número y la localización de los ganglios linfáticos a
                                    los que se ha extendido el tumor:</li>
                                <ul>
                                    <li>N0: el cáncer de mama no se ha extendido a ningún ganglio linfático.</li>
                                    <li>N1: extensión a ganglios linfáticos en la axila (de 1 a 3 ganglios) y/o a ganglios linfáticos situados cerca del esternón (cadena mamaria interna).</li>
                                    <li>N2: extensión a ganglios linfáticos en la axila (de 4 a 9 ganglios) o a ganglios linfáticos situados cerca del esternón (cadena mamaria interna).</li>
                                    <li>N3: extensión a ganglios linfáticos en la axila (10 o más); o ganglios linfáticos situados por debajo de la clavícula (infraclaviculares) o por encima de la clavícula (supraclaviculares); o ganglios linfáticos en la
                                        axila y cerca del esternón (cadena mamaria interna).</li>
                                </ul>
                                <li>3) M (de metástasis). Extensión a otras partes del cuerpo, fuera de la mama y de los ganglios linfáticos próximos a ella (metástasis a distancia):</li>
                                <ul>
                                    <li>M0: no hay evidencia de la presencia de metástasis a distancia.</li>
                                    <li>M1: se detectan metástasis a distancia mediante examen físico o pruebas radiológicas.</li>
                                </ul>
                                <h2 class="centrar-texto brexia-color encabezados-brexia ">Determinación de Estadio</h2>
