                            <p>Estar familiarizada con cómo se ven sus mamas y cómo se sienten al tacto puede ayudarla a notar síntomas como bultos, dolores o cambios en el tamaño que puedan causar preocupación. Esto puede incluir cambios detectados durante
                                un autoexamen de las mamas. </p>
                            <p>La autoexploración de la mama (AEM), que consiste en autoexaminarse las mamas de forma regular, puede ser una buena forma de detectar el cáncer de mama en un estadio temprano, cuando puede tratarse con mejores resultados. No
                                todos los casos de cáncer pueden detectarse de esta forma, pero es un paso importante que puedes y debes implementar por tu cuenta.</p>
                            <p>Son pocas las mujeres que realmente quieren someterse a una autoexploración de la mama (AEM), y para muchas la experiencia es frustrante: es posible que sientas cosas y no sepas qué significan. No obstante, cuanto más examines
                                tus mamas, más aprenderás acerca de ellas y más fácil te resultará detectar posibles anomalías.</p>
                            <p>En caso de tener alguna duda o inquietud es indispensable acudir al Médico para que un especialista realice la valoración adecuada. El médico examinará ambas mamas y los ganglios linfáticos de las axilas para verificar si existen
                                nódulos u otras anomalías.</p>
