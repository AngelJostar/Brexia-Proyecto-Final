                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Melanoma (cáncer de piel)</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar melanoma (cáncer de piel) son:</p>
                            <p>La cirugía es el tratamiento principal para la amplia mayoría de los pacientes. Es también el único tratamiento que se administra a pacientes con melanomas en estadios 0 o I, y a la mayoría de los que presentan melanomas en
                                estadio II.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/melanoma/Tratamiento_cancer_de_piel-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Otras opciones de tratamiento incluyen radioterapia, inmunoterapia y las terapias dirigidas, bien por sí mismas o combinadas. La extensión del tratamiento dependerá del estadio del cáncer, de las características del tumor y
                                de los riesgos para el paciente.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Melanoma (cáncer de piel) según su progresión</h2>
                            <p>Plan de tratamiento para melanomas in situ (estadio 0)</p>
                            <p>Un melanoma in situ se limita a la epidermis y no se ha expandido más allá. El tratamiento consiste en la extirpación del tumor. Después de confirmar el diagnóstico mediante biopsia, se realiza la escisión del tumor, llamada
                                escisión amplia, para obtener unos márgenes de seguridad apropiados para un tumor maligno. A veces, la biopsia que se realiza para diagnosticar el melanoma pudo extirpa todo el tumor con los márgenes de seguridad apropiados.
                                En este caso la biopsia se llama biopsia excisional y no se necesita más tratamiento.</p>
                            <p>Plan de tratamiento para melanomas en estadios I a III</p>
                            <p>Los melanomas en estadios I y II no se han extendido a los ganglios linfáticos, pero los de estadio III sí. El tratamiento más importante consiste en la extirpación completa del melanoma y de los ganglios linfáticos a los que
                                pueda haberse extendido. Cuando las exploraciones clínicas y radiológicas no muestran diseminación del cáncer a los ganglios linfáticos o cuando dicha diseminación no es clara, suele necesitarse y realizarse un procedimiento
                                denominado biopsia del ganglio linfático centinela durante la misma intervención quirúrgica. La disección extensa de los ganglios linfáticos de las regiones circundantes se realiza en aquellos pacientes en los que se sospecha,
                                de acuerdo con las exploraciones clínicas o radiológicas, extensión del tumor a los ganglios linfáticos.</p>
                            <p>Terapia adyuvante Un tratamiento adyuvante es un tratamiento que se administra además de la operación quirúrgica. En algunos casos, no resulta posible eliminar el tumor completo ni todas las metástasis locorregionales mediante
                                un procedimiento quirúrgico. En tales casos, otros tratamientos pueden ayudar a eliminar las células cancerosas que todavía quedan a nivel local. Esto puede hacerse mediante radioterapia o mediante la aplicación de tratamiento
                                sistémico dependiendo del caso en concreto.</p>
                            <p>Plan de tratamiento para el melanoma en estadio IV</p>
                            <p>Los melanomas en estadio IV se han extendido bien a partes de la piel o ganglios linfáticos más allá de los ganglios linfáticos regionales (por ejemplo, se han extendido a la piel del abdomen en el caso de un melanoma inicial
                                en la pierna) o a órganos distantes, como hígado, pulmón o cerebro.</p>
                            <p>Las opciones de tratamiento para pacientes con melanoma en estadio IV dependen del número y tipo de metástasis y en presencia o ausencia de la mutación BRAF. Se recomienda que la decisión de tratamiento se base en una discusión
                                de un equipo interdisciplinar de profesionales médicos. Este encuentro de diferentes especialistas se denomina opinión multidisciplinar o revisión por el comité de tumores. En esa reunión se discutirá la planificación del
                                tratamiento de acuerdo con la información relevante sobre el paciente, la extensión del cáncer y los tratamientos anteriores. El paciente debe poder acceder a la decisión escrita del comité de tumores.</p>
