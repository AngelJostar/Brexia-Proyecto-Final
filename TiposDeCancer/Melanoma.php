                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Melanoma (cáncer de piel)</h2>
                        <div class="font-16 fontW-400">
                            <p>El diagnóstico de melanoma se basa en las exploraciones que se indican a continuación:</p>
                            <ol class="secciones-enumeradas">
                                <li>Exploración médica El médico plantea preguntas al paciente, especialmente en relación con los posibles factores de riesgo y sobre la evolución del lunar o los lunares sospechosos. También se realiza una exploración de los
                                    lunares sospechosos y del resto de la piel. Un lunar sospechoso presenta las características ‘ABCDE’:
                                    <ul class="secciones-enumeradas">
                                        <li>Asimetría en la forma.</li>
                                        <li>Bordes irregulares o poco definidos.</li>
                                        <li>Color cambiante, que varía dependiendo de las zonas.</li>
                                        <li>Dinámica de su evolución.</li>
                                        <li>Evolución en el tiempo.</li>
                                    </ul>
                                    <p>No todos los melanomas presentan las cuatro características. Existen melanomas cuyo color no cambia, no presentan áreas oscuras, de hecho parecen espinillas de color rojizo. Además de ello, el médico también palpa los
                                        ganglios linfáticos en la ingle, la axila, el cuello... dependiendo de la localización de los lunares sospechosos.</p>
                                </li>
                                <li>Dermoscopia Consiste en utilizar un pequeño dispositivo denominado dermoscopio o dermatoscopio que ilumina y amplía los puntos de la piel para una exploración más precisa. Aunque no siempre sea necesaria la exploración
                                    con un dermoscopio, mejora la exactitud del diagnóstico cuando la realiza un médico con experiencia formado en el uso de esta técnica.
                                </li>
                                <li>Exploración histopatológica Tras la extirpación del lunar completo La exploración histopatológica es la exploración en laboratorio de las células tumorales mediante la disección del tumor. Esto confirmará el diagnóstico
                                    de melanoma. El tumor debe extirparse por completo y, a continuación, enviarse al laboratorio. Esto se denomina biopsia de piel y lo hace un médico de forma manual. En primer lugar, se inyecta anestesia local en la
                                    zona que va a extirparse. A continuación, se extirpa el lunar sospechoso asegurándose de que se retira también parte del tejido normal alrededor y debajo del tumor. Es muy importante que tanto la extirpación del lunar
                                    como la exploración en laboratorio la realicen profesionales con experiencia en melanomas.</li>
                            </ol>
