                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué es el melanoma (cáncer de piel)?</h2>
                        <div class="font-16 fontW-400">
                            <p>Un melanoma es una forma de cáncer que comienza en los melanocitos, unas células que elaboran la melanina (el pigmento que da la coloración de la piel). Puede comenzar en un lunar (melanoma de piel), pero también puede aparecer
                                en otros tejidos pigmentados, como los ojos o los intestinos.</p>
                            <p>La piel es el órgano más grande del cuerpo. Protege contra el calor, la luz solar, las lesiones y las infecciones. La piel también ayuda a controlar la temperatura y almacena agua, grasa y vitamina D. La piel tiene varias capas,
                                pero las dos capas principales son la epidermis (capa superficial o externa) y la dermis (capa profunda o interna). El cáncer de piel comienza en la epidermis, que se compone de tres tipos de células:</p>

                            <ul class="secciones-enumeradas">
                                <li>Células escamosas: células delgadas y planas que forman la capa superficial de la epidermis.</li>
                                <li>Células basales: células redondas ubicadas debajo de las células escamosas.</li>
                                <li>Melanocitos: células que producen melanina y se encuentran en la parte profunda de la epidermis. La melanina es el pigmento que le da el color natural a la piel. Cuando la piel se expone al sol, los melanocitos producen
                                    más pigmento y oscurecen la piel.</li>
                            </ul>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/melanoma/Contenido-Melanoma-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>El cáncer de piel se presenta en cualquier lugar del cuerpo, pero es más común en la piel que se suele exponer al sol, como la cara, el cuello y las manos.</p>

                        </div>
