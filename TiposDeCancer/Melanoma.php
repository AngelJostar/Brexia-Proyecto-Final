                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Melanoma (cáncer de piel)</h2>
                        <div class="font-16 fontW-400">
                            <p>No está claro por qué se produce el melanoma, aunque se han identificado algunos factores de riesgo. Un factor de riesgo aumenta la probabilidad de aparición de cáncer, pero no es suficiente ni necesario para que aparezca,
                                y no es una causa en sí mismo. Algunas personas con estos factores de riesgo nunca presentan melanoma y algunas personas sin ningún factor de riesgo terminan sufriendo melanoma. Los principales factores de riesgo de melanom
                                a son.</p>
                            <p>Tipo de piel: Las personas con piel clara presentan un riesgo más elevado de aparición de melanoma que las personas con piel más oscura; el máximo riesgo lo presentan las personas pelirrojas con pecas. El melanoma es, de hecho,
                                muy raro en personas de ascendencia africana o asiática. Cuando se da, suele ser un tipo especial de melanoma denominado melanoma lentiginoso acral que aparece en las palmas de las manos, las plantas de los pies o debajo
                                de las uñas.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/melanoma/Brexia_Melanoma-1-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Nevus o nevo (lunar): Nevo es el término médico para ‘lunar’. La mayoría de los lunares nunca se convertirán en malignos, pero la presencia de una gran cantidad (más de cien) o de lunares poco usuales indica un aumento del
                                riesgo de aparición de melanoma en la persona.</p>
                            <ul class="secciones-enumeradas">
                                <li>Presentar múltiples nevos comunes aumenta el riesgo de aparición de melanoma: el riesgo aumenta con la cifra de nevos y es especialmente elevado cuando esta supera los cien.</li>
                                <li>Presentar tres o más nevos atípicos aumenta el riesgo de aparición de melanoma. Se define como ‘nevo atípico’ el que presenta al menos tres de las características del ‘ABCD’: Asimetría en la forma, Bordes irregulares o
                                    poco definidos, Color que varía dependiendo de las zonas, Dinámica de su evolución en el tiempo, en lo que se refiere a forma, color y tamaño.</li>
                                <li>Los nevos congénitos son los que tenemos de nacimiento. Los lunares congénitos de gran tamaño (mayores de 5 cm) presentan riesgo de convertirse en melanoma. Las personas con nevos congénitos de gran tamaño deben someterse
                                    a seguimiento de forma periódica.</li>
                            </ul>
                            <p>Exposición al sol: La exposición natural a la radiación ultravioleta (UV) emitida por el sol es un importante factor de riesgo para el melanoma. La exposición intermitente al sol, habitualmente con propósito recreativo, aumenta
                                el riesgo de aparición de melanoma. Las quemaduras solares aumentan el riesgo de aparición de melanoma, especialmente si se producen durante la infancia. El uso de protección solar puede reducir el riesgo de desarrollar
                                melanoma. Debe aplicarse junto con otras medidas sencillas, como evitar el sol entre las 11 de la mañana y las 3 de la tarde, o cubrirse con ropa, sombrero y gafas de sol durante la exposición al sol.</p>
                            <p>Uso de camas solares: La exposición a la luz UV artificial para obtener un bronceado aumenta el riesgo de aparición de melanoma, especialmente cuando se utilizan las camas solares antes de los 30 años.</p>
                            <p>Antecedentes de melanoma: Haber sufrido melanoma aumenta el riesgo de padecer otro en un lugar diferente. También tener un familiar de primer grado (padres, hermanos e hijos) con melanoma aumenta el riesgo de presentar esa
                                afección.
                            </p>
                            <p>Sexo: En América del Norte, Oceanía e Israel los hombres presentan un riesgo de aparición de melanoma más elevado, mientras que en Europa el riesgo es ligeramente mayor en las mujeres.</p>
                            <p>Inmunodepresión: Las personas con inmunidad deprimida presentan mayor riesgo de aparición de melanoma. La inmunidad puede disminuir por una enfermedad (como el Sida) o a causa de los fármacos que se administran después de un
                                transplante de órganos.</p>
                            <p>Xerodermia pigmentosa (xerodermia de Kaposi o melanosis lenticular progresiva): Es una enfermedad rara hereditaria en la que se sufre un trastorno de la habilidad de reparar los daños causados por la luz ultravioleta. El riesgo
                                de aparición de todo tipo de cáncer de piel, incluido el melanoma, es extremadamente alto para estas personas.</p>
                            <p>Se sospecha que otros factores, tales como la exposición a los pesticidas o la enfermedad de Parkinson, están relacionados con un aumento del riesgo de melanoma, pero las pruebas y las razones de esa asociación continúan siendo
                                poco claras.</p>
