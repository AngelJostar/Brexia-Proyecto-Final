                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas del Melanoma (cáncer de piel)</h2>
                        <div class="font-16 fontW-400">
                            <p>Debido a que muchos melanomas se desarrollan en la piel donde se pueden ver, tienen una buena posibilidad de ser detectados temprano. El examen regular de la piel para detectar cualquier crecimiento nuevo o inusual, o cambios
                                en lunares existentes, es crítico. Si encuentra algo sospechoso, debe discutirlo con su médico de atención primaria, un dermatólogo (médico de la piel) o un profesional de la salud calificado para diagnosticar el melanoma.</p>
                            <p>La mayoría de los lunares son inofensivos. Un lunar normal generalmente tiene un color uniforme (marrón, negro o tostado) y tiene menos de 6 mm de diámetro (aproximadamente del tamaño de un borrador de lápiz). Pueden ser planas
                                o elevadas, y generalmente no cambian con el tiempo.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/melanoma/Sintomas_de_cancer_de_piel-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>El primer síntoma de melanoma suele ser una nueva mancha en la piel o un cambio en el tamaño, la forma o el color de un lunar existente. El método ABCDE puede ayudarlo a determinar si un crecimiento anormal de la piel puede
                                ser melanoma:</p>
                            <ul class="secciones-enumeradas">
                                <li>Asimetría: el lunar tiene una forma irregular.</li>
                                <li>Borde: el borde no es liso, sino irregular o con muescas.</li>
                                <li>Color: el lunar tiene sombras irregulares o manchas oscuras.</li>
                                <li>Diámetro: el punto es más grande que el tamaño de un borrador de lápiz.</li>
                                <li>Evolución o elevación: el punto está cambiando de tamaño, forma o textura.</li>
                            </ul>
                            <p>La única forma de asegurarse de si un lunar es melanoma es que un médico lo examine.</p>
                            <p>Otros síntomas de melanoma pueden incluir:</p>
                            <ul class="secciones-enumeradas">
                                <li>Llagas que no sanan.</li>
                                <li>Pigmento, enrojecimiento o hinchazón que se extiende fuera del borde de una mancha hacia la piel circundante.</li>
                                <li>Picazón, ternura o dolor.</li>
                                <li>Cambios en la textura o escamas, exudación o sangrado de un lunar existente.</li>
                                <li>Visión borrosa o pérdida parcial de la vista, o manchas oscuras en el iris.</li>
                            </ul>
                            <p>Debido a que los síntomas del cáncer varían, y no todos los melanomas se desarrollan a partir de lunares, es importante hablar con su médico sobre crecimientos de piel nuevos o inusuales.</p>
                            <p>Aunque muchos melanomas se desarrollan en áreas expuestas al sol, también pueden desarrollarse en áreas que generalmente están ocultas del sol. Además de examinar las piernas, el tronco, los brazos, la cara y el cuello, es
                                importante observar las áreas entre los dedos de los pies, debajo de las uñas y las uñas de los pies, en las palmas de las manos, las plantas de los pies, los genitales e incluso los ojos.</p>
                        </div>
