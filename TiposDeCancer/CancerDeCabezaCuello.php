                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas del Cáncer de Cabeza y Cuello</h2>
                        <div class="font-16 fontW-400">
                            <p>Las orejas, la nariz y la garganta son las áreas más comunes afectadas por el cáncer de cabeza y cuello. Los síntomas de este cáncer pueden depender de dónde se desarrolla el cáncer y cómo se propaga.</p>
                            <p>Por ejemplo, los tumores en la laringe o la faringe pueden descubrirse como un bulto en la garganta. El cáncer en la boca puede causar llagas en la boca o hinchazón de la mandíbula.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cabeza-y-cuello/sintomas_cancer_cabeza-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Además de los signos físicos del cáncer de cabeza y cuello, estos tumores a menudo causan síntomas similares a las condiciones menos graves, como el resfriado común. Los cambios en la voz, dolores de cabeza, dolor de garganta
                                o tos pueden ser síntomas de cáncer de garganta. El dolor o el zumbido en los oídos también pueden acompañar a ciertos tipos de cáncer de cabeza y cuello.</p>
                            <p>Algunos síntomas comunes del cáncer de cabeza y cuello incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Un bulto en la nariz, el cuello o la garganta, con o sin dolor.</li>
                                <li>Dolor de garganta persistente.</li>
                                <li>Problemas para tragar (disfagia).</li>
                                <li>Pérdida de peso inexplicable.</li>
                                <li>Tos frecuente.</li>
                                <li>Cambio en la voz o ronquera.</li>
                                <li>Dolor de oído o dificultad para oír.</li>
                                <li>Dolores de cabeza Un parche rojo o blanco en la boca.</li>
                                <li>Mal aliento que no se explica por la higiene.</li>
                                <li>Obstrucción nasal o congestión persistente.</li>
                                <li>Sangrado nasal frecuente o secreción inusual.</li>
                                <li>Dificultad para respirar.</li>
                            </ul>
