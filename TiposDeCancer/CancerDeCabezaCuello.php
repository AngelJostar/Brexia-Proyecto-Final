                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Cabeza y Cuello</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer de cabeza y cuello están capacitados y tienen experiencia
                                en los tratamientos disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar cáncer de cabeza y cuello según su estadio son:</p>
                            <ul class="secciones-enumeradas">
                                <li>Los estadios I y II del cáncer de cabeza y cuello se denominan cánceres localizados o de etapa inicial, puesto que tienen un diámetro inferior a los 4 cm y no se han diseminado a ningún ganglio linfático.</li>
                                <ul>
                                    <li>La radioterapia y la cirugía tienen una efectividad similar para tratar el tumor.</li>
                                    <li>Las técnicas modernas de radioterapia permiten limitar sustancialmente los daños a los tejidos sanos alrededor del tumor.</li>
                                </ul>
                                <li>Los estadios III y IV del cáncer de cabeza y cuello se denominan respectivamente cáncer localmente avanzado y cáncer metastásico, ya que su tamaño supera los 4 cm de diámetro, se han propagado a ganglios linfáticos o se
                                    han propagado a órganos distantes.</li>
                            </ul>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cabeza-y-cuello/tratamiento_cancer_cuello-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>La cuestión principal en estos estadios será evaluar si el tumor puede extirparse por completo por medio de cirugía sin comprometer demasiado la calidad de vida de la persona.</p>
                            <ul class="secciones-enumeradas">
                                <li>Si el tumor se considera resecable (operable), el tratamiento propuesto es la cirugía para extirpar el tumor, seguida de cirugía reconstructiva y radioterapia. El análisis del tumor extirpado durante la cirugía proporcionará
                                    información adicional útil para decidir si debería agregarse quimioterapia a la radioterapia tras la cirugía, en ciertos casos. La radioterapia es la alternativa cuando se desestima la cirugía como tratamiento, y el
                                    agregado de quimioterapia concomitante a la radioterapia es mas efectivo que la radioterapia sola.</li>
                                <li>En algunos casos, fármacos denominados de inmunoterapia y/o terapia dirigida, podrá usarse en combinación o sustitución de la quimioterapia.</li>
                            </ul>
                            <p>El seguimiento es importante para detectar efectos secundarios asociados con el tratamiento como secuelas en la deglución o respiración, así mismo la evaluación de la glándula tiroides es necesaria en pacientes que recibieron
                                radioterapia al cuello. El examen físico y la adquisición de imágenes médicas se realizarán de manera periódica principalmente para detectar recurrencia.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Puntos importantes del Tratamiento de Cáncer de Cabeza y Cuello</h2>
                            <ul class="secciones-enumeradas">
                                <li>Hay diferentes tipos de tratamiento para los pacientes de cáncer escamoso metastásico de cuello con tumor primario oculto.
                                </li>
                                <li>Se utilizan dos tipos de tratamiento estándar:</li>
                                <ul>
                                    <li>Cirugía</li>
                                    <li>Radioterapia</li>
                                </ul>
                                <li>Se están probando nuevos tipos de tratamiento en ensayos clínicos.</li>
                                <ul>
                                    <li>Quimioterapia</li>
                                    <li>Radioterapia hiperfraccionada</li>
                                </ul>
                                <li>A veces el tratamiento para el cáncer escamoso metastásico de cuello con tumor primario oculto causa efectos secundarios.</li>
                                <li>Los pacientes podrían considerar la participación en un ensayo clínico.</li>
                                <li>Los pacientes pueden ingresar en los ensayos clínicos antes, durante o después de comenzar su tratamiento para el cáncer.</li>
                                <li>A veces se necesitan pruebas de seguimiento.</li>
                            </ul>
