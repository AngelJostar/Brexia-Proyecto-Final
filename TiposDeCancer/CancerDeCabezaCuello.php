                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Cabeza y Cuello</h2>
                        <div class="font-16 fontW-400">
                            <p>Cabe sospechar la presencia de cáncer de cabeza y cuello ante la presencia de síntomas como un bulto en el cuello, úlceras en la lengua, zonas con sangrado, manchas blancas o rojas en la boca, dolor de garganta, deglución dolorosa,
                                ronquera persistente, nariz congestionada en un lado y/o secreción de sangre de la nariz, especialmente si persisten más de 3 semanas.</p>
                            <p>La observación directa y la palpación de las lesiones sospechosas se complementarán con la observación de boca, nariz, garganta y vías respiratorias superiores con un tubo flexible con luz llamado endoscopio.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cabeza-y-cuello/Diagnostico_cancer_cuello-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Las imágenes médicas se usan para conocer la forma y el tamaño del tumor y también pueden usarse para verificar si el cáncer se ha propagado a otras partes del cuerpo.</p>

                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-cervicouterino/Cancer_diagnostico-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Un diagnóstico completo y preciso del cáncer es el primer paso para desarrollar un plan de tratamiento para el cáncer de cabeza y cuello. En Brexia utilizaremos una variedad de pruebas y herramientas diseñadas para diagnosticar
                                el cáncer de cabeza y cuello, evaluar la enfermedad y planificar su tratamiento individualizado. A lo largo de su tratamiento, utilizaremos pruebas de imagen y de laboratorio para rastrear el tamaño de los tumores, monitorear
                                su respuesta al tratamiento y modificar su plan cuando sea necesario.</p>
                            <p>Los tipos de procedimientos que pueden usarse para diagnosticar el cáncer de cabeza y cuello incluyen:</p>
                            <p>Faringoscopia indirecta y laringoscopia: Estos procedimientos a menudo se usan para examinar la parte posterior de la garganta. Usamos faringoscopia y laringoscopia indirecta para verificar si hay signos de cáncer de cabeza
                                y cuello. Durante una faringoscopia y laringoscopia indirecta, colocamos pequeños espejos en la parte posterior de la boca para examinar claramente la garganta, la base de la lengua y parte de la laringe.</p>
                            <p>Endoscopia: Este procedimiento permite a los médicos examinar la cabeza y el cuello desde adentro. Se puede insertar un endoscopio en la boca o la nariz para que el médico pueda examinar áreas difíciles de ver de la cabeza
                                y el cuello, como la laringe y detrás de la nariz. Una endoscopia es mínimamente invasiva, generalmente toma menos tiempo en la sala de operaciones y requiere una incisión más pequeña que la cirugía tradicional, lo que
                                ayuda a reducir la pérdida de sangre.</p>
                            <p>Panendoscopia: Este es un procedimiento utilizado si su médico sospecha la presencia de cáncer de cabeza y cuello. La panendoscopia es una prueba de diagnóstico que se usa para examinar el sistema digestivo superior. Si se
                                encuentran tumores durante el procedimiento, el médico puede extraer muestras para una biopsia.</p>
                            <p>Biopsia: Una biopsia es la única prueba que puede respaldar un diagnóstico definitivo de cáncer de cabeza y cuello. Se requiere una muestra de tejido o células para una biopsia, que debe realizarse antes de comenzar el tratamiento.</p>
                            <p>Rayos X: Se pueden tomar imágenes del tórax para determinar si el cáncer se ha diseminado a los pulmones. El cáncer no estará presente en los pulmones a menos que haya avanzado. Una radiografía generalmente es un procedimiento
                                ambulatorio, lo que significa que no tiene que ser ingresado en un hospital para la prueba. Si el resultado es anormal, se realizarán más pruebas.</p>
                            <p>Esofagografía con bario: Estas pruebas pueden mostrar irregularidades en la laringe, faringe, boca y áreas circundantes, y a menudo se usan para detectar tumores pequeños, tempranos de cabeza y cuello. También conocida como
                                una "serie GI superior", un trago de bario implica beber una bebida calcárea hecha de una solución a base de bario, que permite que las células cancerosas se vean mejor durante una radiografía.</p>
                            <p>Pruebas de imagen: Las pruebas de imagen que se usan para diagnosticar el cáncer de cabeza y cuello incluyen: Tomografía computarizada (TC), Resonancia magnética (MRI) y Tomografía por emisión de positrones (PET).</p>
