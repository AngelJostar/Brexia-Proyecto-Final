                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cáncer de Cabeza y Cuello</h2>
                        <div class="font-16 fontW-400">
                            <p>Después de diagnosticarse el cáncer escamoso metastásico de cuello con tumor primario oculto, se realizan pruebas para determinar si las células cancerosas se diseminaron hasta otras partes del cuerpo.</p>
                            <p>El proceso que se utiliza para determinar si el cáncer se diseminó hasta otras partes del cuerpo se llama estadificación. Los resultados de las pruebas y procedimientos que se usan para detectar y diagnosticar el tumor primario
                                también se usan para determinar si el cáncer se diseminó a otras partes del cuerpo.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cabeza-y-cuello/estadificacion_cancer_cuello-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>No hay ningún sistema de estadificación estándar para el cáncer escamoso metastásico de cuello con tumor primario oculto. Los tumores se describen como recidivantes o que no recibieron tratamiento. El cáncer escamoso metastásico
                                de cuello con tumor primario oculto sin tratamiento es un cáncer recién diagnosticado y que no ha sido tratado, salvo para aliviar los signos o síntomas que causa esta enfermedad.</p>
                            <p>El cáncer se disemina en el cuerpo de tres maneras:</p>
                            <ol class="secciones-enumeradas">
                                <li>Tejido: El cáncer se disemina desde donde comenzó y se extiende hacia las áreas cercanas.</li>
                                <li>Sistema linfático: El cáncer se disemina desde donde comenzó y entra en el sistema linfático. El cáncer se desplaza a través de los vasos linfáticos a otras partes del cuerpo.</li>
                                <li>Sangre: El cáncer se disemina desde donde comenzó y entra en la sangre. El cáncer se desplaza a través de los vasos sanguíneos a otras partes del cuerpo.</li>
                            </ol>
                        </div>

