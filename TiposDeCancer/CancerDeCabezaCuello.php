                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Cáncer de Cabeza y Cuello</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer de cabeza y cuello es un grupo de cánceres que se forman en los tejidos del tracto aerodigestivo superior (labios, lengua, boca, garganta y laringe) o en los senos y la cavidad nasal. La mayoría del cáncer de cabeza
                                y cuello se origina en las células escamosas que forman estos tractos y cavidades y por ello son denominados carcinomas de células escamosas. El ochenta y cinco por ciento del cáncer de cabeza y cuello está relacionado
                                con el consumo de tabaco, y el 75 por ciento está asociado con una combinación de consumo de tabaco y alcohol.</p>
                            <p>Debido a su ubicación, el cáncer de cabeza y cuello y los efectos secundarios relacionados con el tratamiento pueden afectar la capacidad de los pacientes para comer, tragar y respirar. En Brexia, nuestro equipo de atención
                                multidisciplinaria de expertos en cáncer de cabeza y cuello trabaja con los pacientes para ayudarlos a tomar decisiones informadas sobre sus opciones de tratamiento, al mismo tiempo que ofrecemos terapias para controlar
                                los posibles efectos secundarios.</p>
