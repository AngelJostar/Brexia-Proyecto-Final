                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Cabeza y cuello</h2>
                        <div class="font-16 fontW-400">
                            <p>Las personas infectadas con el virus del papiloma humano (VPH) tienen un mayor riesgo de algunos cánceres de garganta y boca. El VPH ha ido en aumento en los últimos años, especialmente entre las personas de 40 y 50 años. Los
                                factores de riesgo para el cáncer de cabeza y cuello incluyen:</p>
                            <p>Tabaco: este es el factor de riesgo más grande para el cáncer de cabeza y cuello. Fumar presenta el mayor riesgo de desarrollar este tipo de cáncer, pero el humo de segunda mano también puede aumentar el riesgo. Masticar tabaco
                                se ha relacionado con el cáncer de cavidad oral.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cabeza-y-cuello/actores_riesgo_cancer_cuello-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Alcohol: el consumo excesivo de alcohol es el segundo factor de riesgo más grande para el cáncer de boca, faringe, laringe y esófago.</p>
                            <p>Género: los hombres tienen dos o tres veces más probabilidades que las mujeres de desarrollar cáncer de cabeza y cuello.</p>
                            <p>Edad: el cáncer de cabeza y cuello es más común en personas mayores de 50 años.</p>
                            <p>Ciertas enfermedades: el virus de Epstein-Barr (VEB), el VPH y dos síndromes genéticos hereditarios, la anemia de Fanconi y la disqueratosis congénita, se han relacionado con el cáncer de cabeza y cuello.</p>
                            <p>Exposición al sol: la exposición prolongada al sol puede aumentar el riesgo de cáncer de labio y oral.</p>
                            <p>Radioterapia: las altas dosis de radioterapia, particularmente administradas en la región de la cabeza y el cuello, pueden aumentar el riesgo de desarrollar este tipo de cáncer.</p>
                            <p>Nutrición: la deficiencia nutricional y las deficiencias de vitaminas pueden aumentar el riesgo de un paciente de desarrollar la enfermedad.</p>
