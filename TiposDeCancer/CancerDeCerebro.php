                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas del Cáncer de Cerebro (gliomas)</h2>
                        <div class="font-16 fontW-400">
                            <P>Se puede sospechar la existencia de un glioma ante la presencia de diversos síntomas. Sin embargo, es importante saber que estos síntomas dependen en gran medida del tipo de glioma, así como de su ubicación exacta en el sistema
                                nervioso central. En la ilustración están representados los diferentes lóbulos del hemisferio izquierdo del cerebro. El cerebro tiene dos hemisferios y cada lóbulo de cada hemisferio es responsable de una multitud de funciones.
                                Por lo tanto, la lista de los síntomas descritos en este documento no puede ser exhaustiva. Sin embargo, en un esfuerzo por generalizar, los signos y síntomas listados a continuación pueden estar comúnmente presentes ya
                                sea solos o combinados entre sí, tanto en el primer diagnóstico como posteriormente durante el curso de la enfermedad.</P>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cerebro/sintomas_cancer_cerebro-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Convulsiones: Se trata de uno de los síntomas más comunes y, a menudo, más dolorosos de los gliomas. Las convulsiones se presentan especialmente en pacientes con tumores de crecimiento lento, como los gliomas de bajo grado.
                                Una convulsión puede causar sacudidas o espasmos en una mano, brazo o pierna. Sin embargo, una convulsión también puede afectar a todo el cuerpo con movimientos bastantes violentos e incontrolados, posiblemente con pérdida
                                de conciencia.</p>
                            <p>Problemas neurológicos (conocidos como “déficits”): Estos dependen en gran medida del lóbulo o lóbulos del cerebro que están afectados. Pueden estar presentes problemas de visión si el lóbulo occipital (representado en azul)
                                está implicado. La dificultad para hablar o entender lo que se le dice a uno, así como la pérdida de función (fuerza) o sensibilidad (sensación) en una parte del cuerpo se produce cuando el plano frontal (en amarillo) o
                                el lóbulo parietal (en rojo) están afectados. Los cambios de personalidad y los cambios en el comportamiento tales como apatía o pérdida de iniciativa y la pérdida de control emocional/pérdida de la inhibición pueden producirse
                                si el lóbulo frontal (en amarillo) está comprometido. Por último, la pérdida de memoria se asocia a menudo con la implicación del lóbulo temporal (en verde), mientras que la coordinación deficiente o el movimiento incontrolado
                                de los ojos pueden estar presentes si la enfermedad afecta al cerebelo.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cerebro/sintmas_cancer_cerebro_2.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Síntomas resultantes del aumento de la presión en el cerebro: Estos síntomas son típicos de pacientes con gliomas de alto grado. Se producen porque el tumor crece rápidamente en el cerebro, que está contenido dentro del espacio
                                fijo del cráneo. Esto puede ocasionar dolor de cabeza, náuseas, vómitos, visión doble y somnolencia.</p>
                            <p>Eventos tromboembólicos: Los eventos tromboembólicos, esto es, la formación de coágulos en la sangre, a menudo están presentes en los pacientes de glioma. Hay varias razones posibles para ello. Los pacientes de glioma suelen
                                presentar factores de riesgo comúnmente asociados con el desarrollo de trombosis entre los que se incluyen los déficits motores y/o la inmovilidad y, tras el diagnóstico, el tratamiento con quimioterapia. Los síntomas asociados
                                con la trombosis difieren en gran medida según el lugar involucrado, y su lista queda fuera del alcance de esta guía para pacientes.</p>
                        </div>
