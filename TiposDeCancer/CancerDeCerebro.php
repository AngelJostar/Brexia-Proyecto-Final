                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Cerebro (gliomas)</h2>
                        <div class="font-16 fontW-400">
                            <p>La existencia del glioma se puede sospechar cuando está presente un cierto número de síntomas, tales como convulsiones, cambios en la personalidad y en el comportamiento, diversos tipos de problemas neurológicos (incluyendo
                                problemas de visión, dificultad en el habla, comprensión de lo que se dice, pérdida de fuerza o sensibilidad en una parte del cuerpo, o cambios en la forma de caminar), así como síntomas asociados con un aumento de la presión
                                en la cabeza (cefalea, náusea, vómito y somnolencia).</p>
                            <p>La imagen por resonancia magnética (IRM) del cerebro es la prueba radiológica de referencia para la detección de un glioma. También ayuda a definir la extensión de la enfermedad y además indica si el tumor puede ser extirpado
                                quirúrgicamente de forma segura.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cerebro/resonancia-magnetica-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Deberá obtenerse para su análisis en el laboratorio una porción pequeña del tumor (extraída tanto por resección quirúrgica como por biopsia estereotáctica/abierta en el caso de que la extirpación quirúrgica no sea posible)
                                para confirmar el diagnóstico y obtener más detalles acerca de las características moleculares del tumor. La caracterización molecular puede ayudar a la hora de definir el subtipo exacto de glioma, proporcionar información
                                sobre el resultado probable del diagnóstico (“pronóstico”) y ayudar a orientar las decisiones de tratamiento.</p>
                        </div>
                    </article>
                    <article id="tab5">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Cerebro (gliomas)</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar cáncer de cerebro (gliomas) son:</p>
                            <p>La cirugía es el tratamiento de primera elección para la mayoría de los gliomas recién diagnosticados; de hecho, la extirpación quirúrgica tan amplia como sea posible de forma segura está asociada con un mejor resultado independientemente
                                del subtipo de glioma. Después de la cirugía, el tratamiento difiere según el subtipo del glioma.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cerebro/Tratamiento_cancer_de_cerebro-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Glioma de bajo grado (Grados 1 y 2)</p>
                            <ul class="secciones-enumeradas">
                                <li>La radioterapia es el tratamiento estándar postoperatorio en aquellos pacientes cuyas características de la enfermedad sugieren una alta probabilidad de reaparición de la enfermedad (denominada recidiva).</li>
                                <li>La quimioterapia tiene un papel menos definido en el glioma de bajo grado. Sin embargo, puede ser utilizada en pacientes que no se consideren elegibles para la cirugía y/o la radioterapia o en tumores recurrentes después
                                    de la radioterapia. Los pacientes cuyo tumor muestra una característica molecular específica (denominada “pérdida genética en los cromosomas 1p/19q”) parecen ser especialmente sensibles a la quimioterapia, es decir,
                                    hay más posibilidades de beneficio quimioterapéutico en estos pacientes.</li>
                            </ul>
                            <p>Glioma anaplásico (Grado 3)</p>
                            <ul class="secciones-enumeradas">
                                <li>La radioterapia seguida de quimioterapia es un tratamiento postoperatorio estándar para el glioma anaplásico.</li>
                                <li>La radioterapia sola puede utilizarse en tumores oligodendrogliales anaplásicos sin pérdida genética en los cromosomas 1p/19q.</li>
                                <li>Existen estudios que han demostrado que la quimioterapia sola después de la cirugía, y la administración de radioterapia sólo en el momento de progresión de la enfermedad, consigue los mismos resultados que la radioterapia
                                    aplicada después de la cirugía y la quimioterapia administrada en el momento de progresión de la enfermedad.</li>
                            </ul>
                            <p>Glioblastoma (Grado 4)</p>
                            <ul class="secciones-enumeradas">
                                <li>El uso de quimioterapia y radioterapia en combinación (“simultáneamente” o “en concomitancia”) es el tratamiento estándar después de la cirugía en los pacientes de glioblastoma menores de 70 años, así como en pacientes
                                    mayores con buen estado de salud cuyo tumor resulte positivo en una determinada característica molecular (presencia de metilación del gen MGMT).</li>
                                <li>La radioterapia sola se prefiere en los pacientes de >70 años que no tengan un estado de salud apto para recibir quimiorradioterapia concurrente y/o cuyo tumor resulte negativo en la presencia de metilación del gen MGMT.</li>
                                <li>La quimioterapia sola es la opción de tratamiento preferida en pacientes >70 años que no presenten un buen estado de salud y cuyo tumor dé positivo en la metilación del gen MGMT.</li>
                            </ul>
