                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Cerebro (gliomas)</h2>
                        <div class="font-16 fontW-400">
                            <p>Antes de abordar los factores de riesgo que pueden predisponer al desarrollo del glioma, es importante saber que un factor de riesgo aumenta el riesgo de que aparezca un cáncer, pero no es ni necesario ni suficiente por sí
                                solo para provocar cáncer. Un factor de riesgo no es una causa en sí mismo. Por lo tanto, algunas personas con uno o más factores de riesgo nunca desarrollarán un glioma y algunas personas sin ninguno de estos factores
                                de riesgo pueden, sin embargo, desarrollar un glioma.
                            </p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-cerebro/factores_de_riesgo_cancer_cerebro-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>No obstante, en la actualidad, no está claro por qué se produce el glioma, y se han identificado muy pocos factores de riesgo. En general, los gliomas son ligeramente más comunes en hombres que en mujeres y se presentan más
                                en individuos de raza blanca que en poblaciones de raza negra. Los factores de riesgo reconocidos de los gliomas son:</p>
                            <p>Radiaciones ionizantes: Este es un factor de riesgo ambiental establecido, tal y como queda documentado por la observación de que los individuos expuestos a las bombas atómicas y a los ensayos de armas nucleares tienen un mayor
                                riesgo de desarrollar glioma. Los individuos que han recibido irradiación craneal para la terapia del cáncer durante su infancia también tienen un mayor riesgo de desarrollar glioma años o incluso décadas después.</p>
                            <p>Antecedentes familiares: Unos antecedentes familiares de glioma (lo que significa la existencia de uno o más casos de glioma en la misma familia) están asociados con un incremento de 2 veces en el riesgo de desarrollar un glioma.</p>
                            <p>Síndromes genéticos: Existe una serie de síndromes hereditarios poco comunes que se asocian con un mayor riesgo de desarrollo de cánceres en general, principalmente como resultado de la presencia de una o varias alteraciones
                                genéticas. Cabe señalar que algunos de estos síndromes hereditarios pueden conllevar también un mayor riesgo de desarrollo de glioma, tales como el síndrome de Cowden, el síndrome de Turcot, el síndrome de Lynch, el síndrome
                                de LiFraumeni y la neurofibromatosis de tipo I.</p>
                            <p>Se ha sospechado que otros factores pueden estar asociados con un mayor riesgo de glioma, pero las pruebas a este respecto son inconsistentes. Es el caso de la exposición al teléfono móvil, para la que los estudios epidemiológicos
                                (investigación de los patrones, las causas y los efectos en poblaciones específicas sanas y enfermas) no han podido demostrar hasta ahora de forma concluyente una asociación con un aumento en el riesgo de desarrollo de
                                glioma. Las pruebas son igualmente inconsistentes para otros factores que un tiempo eran sospechados de poder aumentar el riesgo de desarrollo de glioma, tales como las lesiones en la cabeza, el aspartamo, o la exposición
                                a plaguicidas.</p>
                        </div>
                    </article>
