                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento de los Tumores Neuroendocrinos</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de ​tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en ​cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar los ​tumores neuroendocrinos ​son.</p>
                            <p>Cirugía: ​Se utiliza principalmente para extirpar el tumor que aún no se ha extendido a otros órganos, es decir, el que se encuentra confinado en el lugar de origen. Si ya ha alcanzado los ganglios linfáticos de la zona, la
                                cirugía no siempre es posible, y cuando afecta a otros órganos a distancia es una opción que se contempla solo en casos muy seleccionados. También se puede optar por la cirugía para paliar síntomas locales, o en situaciones
                                de liberación hormonal exacerbada con presencia de una lesión predominante que se cree que es la principal responsable de la producción hormonal. En estos casos, normalmente, tras la extirpación del tumor local no es necesario
                                administrar radioterapia ni quimioterapia.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/tumores-neuroendocrinos/Contenido-Tumores_0000_Capa-5-768x512.jpg" alt="Cancer de prostata">
                            </div>
                            <p>Terapias locorregionales dirigidas al hígado: ​El hígado suele ser el órgano más afectado por las metástasis. Ciertas técnicas que permiten la embolización de las metástasis de mayor tamaño o la ablación por radiofrecuencia
                                pueden permitir el control a nivel local de determinadas lesiones.</p>
                            <p>Análogos de la somatostatina: ​La mayoría de estos tumores expresan en su membrana unos receptores para la somatostatina. Algunos fármacos se dirigen contra estos receptores y han demostrado ser capaces de reducir la producción
                                hormonal, así como de ralentizar el crecimiento del tumor.</p>
                            <p>Quimioterapia: ​Se ha empleado tradicionalmente con distinta eficacia. Los tratamientos quimioterápicos basados en combinaciones con platino son el estándar para aquellos tumores con una alta agresividad y grado de proliferación
                                elevado. La quimioterapia también puede jugar un papel importante en los tumores neuroendocrinos de origen pancreático.</p>
                            <p>Terapias dirigidas: ​En estas terapias se emplean fármacos dirigidos a alteraciones moleculares concretas que puede haber en las células del tumor o en las del entorno de este.</p>
                            <p>Radionúclidos: ​Son fármacos que combinan análogos de la somatostatina, a los que se une un átomo radiactivo. Como la mayoría de los tumores neuroendocrinos expresan en su membrana receptores para la somatostatina, encontramos
                                en el cuerpo estas células que captan a los análogos y las eliminan de manera selectiva con el átomo cargado radiactivamente.</p>
                            <p>Inhibidores de la síntesis de serotonina: ​Son fármacos que impiden la formación de la serotonina ayudando al control del síndrome carcinoide en aquellos pacientes en los que no se logra su adecuado control con los análogos
                                de la somatostatina.</p>
                            <p>El empleo de una u otra técnica o fármaco exige experiencia, diálogo y seguimiento multidisciplinar y también requiere contemplar todas las opciones disponibles.</p>
