                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico de los Tumores Neuroendocrinos</h2>
                        <div class="font-16 fontW-400">
                            <p>Ante la sospecha de un ​tumor neuroendocrino ​se podrán realizar.</p>
                            <ul class="secciones-enumeradas">
                                <li>Analítica general ​- ​Estudio hormonal inicial en plasma y/u orina: ​La hormona estudiada dependerá del tumor neuroendocrino que se sospeche clínicamente.</li>
                                <li>Pruebas de imagen - ​​Medicina nuclear:​Las pruebas de medicina nuclear tienen especial utilidad en el diagnóstico de tumores neuroendocrinos. Las más habituales son la gammagrafía con octreotido, el PET-Galio68 y el PET-FDG
                                    (glucosa). El objetivo es detectar la expresión de receptores de somatostatina (gammagrafía, PET-Galio68) o la actividad tumoral a través del consumo de glucosa (PET-FDG). De esta manera se puede determinar la agresividad
                                    del tumor y la sensibilidad a tratamientos dirigidos a los receptores de la somatostatina.
                                    <div class="imagen-80 centrar-imagen .">
                                        <img src="/img/tumores-neuroendocrinos/Contenido-Tumores_0002_Capa-3-768x512.webp" alt="Cancer de prostata">
                                    </div>
                                </li>
                                <li>Gammagrafía con MIBG 131:​Es la técnica más sensible y específica para el diagnóstico de feocromocitomas y paragangliomas.</li>
                                <li>TAC de alta definición:​Puede ser útil como prueba principal en los casos con gammagrafías negativas, y como estudio complementario en los casos con gammagrafías positivas al proporcionar imágenes mucho más precisas anatómicamente.
                                    El TAC es esencial para el seguimiento de la enfermedad metastásica y para seguir la respuesta a los tratamientos. Debido a las características especiales de los TNE-GEP el TAC debe ser trifásico para obtener las mejores
                                    imágenes posibles.</li>
                                <li>Resonancia magnética nuclear (RMN):​Presenta una gran sensibilidad para la detección de metástasis hepáticas. También es muy útil en la detección de otros tumores neuroendocrinos como el de tiroides o los de hipófisis.</li>
                                <li>Estudio anatomopatológico:​Debe realizarse una biopsia tumoral en todos los casos. Un estudio anatomopatológico reglado es esencial para realizar un correcto diagnóstico del tumor y una correcta clasificación.</li>

                            </ul>
