                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué son los Tumores Neuroendocrinos?</h2>
                        <div class="font-16 fontW-400">
                            <p>El sistema ​endocrino se encarga de producir y liberar hormonas al torrente circulatorio y a los tejidos circundantes, y regula así la función y actividad de muchos órganos y sistemas de nuestro cuerpo.</p>
                            <p>Se compone principalmente de glándulas, como puede ser el tiroides, la hipófisis, las gónadas o las suprarrenales, y también de las células del sistema neuroendocrino, compuesto por millones de células que constituyen, cada
                                una de ellas, una glándula en sí misma, puesto que son capaces, de manera autónoma, de producir y liberar hormonas activas tanto a nivel local como a distancia.</p>
                            <p>Las células neuroendocrinas pueden encontrarse en cualquier órgano del cuerpo, aunque principalmente a lo largo de las paredes del tubo digestivo y del tracto respiratorio (o tracto bronquial). Además, en el páncreas constituyen
                                acúmulos denominados islotes pancreáticos o de Langerhans. Y también las tenemos en la piel o en el tiroides.</p>
                            <p>Un ​tumor neuroendocrino se forma cuando la célula pierde la capacidad de controlar su propia proliferación. Puede originarse en cualquiera de las células descritas anteriormente, con mayor frecuencia en el tubo digestivo,
                                en el páncreas y el tracto respiratorio Si se origina en las de la piel, se denomina carcinoma de células de Merkel, y si ocurre en la tiroides o en las glándulas adrenales, carcinoma medular de tiroides y feocromocitoma,
                                respectivamente, siendo estos últimos menos incidentes.</p>
