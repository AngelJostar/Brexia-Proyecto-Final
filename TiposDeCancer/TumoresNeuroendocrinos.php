                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas de los Tumores Neuroendocrinos</h2>
                        <div class="font-16 fontW-400">
                            <p>Los diferentes tipos de ​tumores neuroendocrinos causan diferentes ​síntomas​, dependiendo de la ubicación del ​tumor y de si el ​tumor neuroendocrino es funcional o no funcional. Los ​tumores neuroendocrinos funcionales se
                                definen en función de la presencia de ​síntomas clínicos debido al exceso de secreción hormonal por el ​tumor. ​Los ​tumores neuroendocrinos no funcionales no secretan hormonas. Pueden producir ​síntomas causados ​​por
                                el crecimiento del ​tumor.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/tumores-neuroendocrinos/Contenido-Tumores_0001_Capa-4-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Los síntomas comunes de los ​tumores neuroendocrinos ​incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Enrojecimiento (enrojecimiento, calor) en la cara o el cuello sin sudar.</li>
                                <li>Diarrea, incluso por la noche.</li>
                                <li>Falta de aliento, latidos cardíacos rápidos / palpitaciones.</li>
                                <li>Alta presión sanguínea Fatiga, debilidad.</li>
                                <li>Dolor abdominal, calambres, sensación de saciedad.</li>
                                <li>Aumento o pérdida de peso inexplicable.</li>
                                <li>Sibilancias, tos.</li>
                                <li>Hinchazón en los pies y tobillos.</li>
                                <li>Lesiones cutáneas, parches decolorados de la piel, piel delgada.</li>
                                <li>Niveles altos de glucosa en la sangre (micción frecuente, aumento de la sed, aumento del hambre).</li>
                                <li>Niveles bajos de glucosa en sangre (temblores, mareos, sudoración, desmayos).</li>
                            </ul>
                            <p>Los ​tumores neuroendocrinos a menudo no causan ​síntomas temprano en el proceso de la enfermedad. Cuando los ​síntomas están presentes, pueden ser similares a los causados ​​por afecciones más comunes. Como resultado, los
                                ​tumores neuroendocrinos a veces se diagnostican erróneamente como síndrome del intestino irritable (SII). Sin embargo, con el SII, las molestias abdominales generalmente se alivian al ir al baño.</p>
                            <p>Debido a que la mayoría de los pacientes con ​tumores neuroendocrinos son diagnosticados en una etapa avanzada, es importante que informe a su ​médico si nota síntomas​ o cambios nuevos o persistentes en su cuerpo.</p>
