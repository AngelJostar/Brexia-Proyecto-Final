                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Cáncer incidental de vesícula Biliares</h2>
                            <p>El ​cáncer​ de ​vesícula biliar​se descubre cada vez más por casualidad cuando los pacientes se someten a procedimientos por otras afecciones de la ​vesícula biliar​(por ejemplo, cirugía de vesícula biliar para cálculos biliares).
                                En estos casos, el ​cáncer​es diagnosticado mediante pruebas de patología en el tejido extirpado. Como el ​cáncer​de ​vesícula biliar​a menudo no causa ​síntomas​en sus primeras etapas, el ​diagnóstico​incidental brinda
                                la oportunidad de un ​diagnóstico​y ​tratamiento​más tempranos, que pueden incluir una operación adicional para asegurar que se elimine todo el ​cáncer​,así como ​quimioterapia adyuvante.</p>
