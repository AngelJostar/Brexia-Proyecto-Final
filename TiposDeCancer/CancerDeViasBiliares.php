                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Vías Biliares</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de ​tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión de la enfermedad. Nuestros expertos en ​cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Las opciones más comunes para tratar ​cáncer de vías biliares​ son:</p>
                            <p>Cirugía: ​La cirugía para extirpar el tumor (resección) es el único tratamiento potencialmente curativo para el cáncer del tracto biliar. El objetivo de la resección es eliminar el cáncer junto con un margen saludable de tejido
                                para ayudar a evitar la recurrencia. La cirugía curativa generalmente solo se ofrece a los pacientes con enfermedad en etapa temprana (localizada), cuando hay una buena posibilidad de resección completa. El tipo de cirugía
                                dependerá del subtipo de cáncer del tracto biliar.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/cancer-de-vias-biliares/cancer_vias_biliares-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <p>Quimioterapia​: La quimioterapia destruye las células cancerosas y se usa ampliamente en el tratamiento del cáncer del tracto biliar. Existen múltiples opciones dependiendo del subtipo de cáncer del tracto biliar.</p>
                            <p>Radioterapia: ​La radioterapia utiliza radiación ionizante para dañar el ácido nucleico desoxirribosa (ADN) de las células cancerosas, haciéndolos morir. La radioterapia no se usa comúnmente en el tratamiento del cáncer del
                                tracto biliar, pero puede ser considerada para algunos pacientes.</p>
