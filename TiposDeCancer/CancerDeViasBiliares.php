                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Vías Biliares</h2>
                        <div class="font-16 fontW-400">
                            <p>El ​diagnóstico​de ​cáncer de las vías biliares​se basa en los resultados de los siguientes exámenes y pruebas:</p>
                            <ol class="secciones-enumeradas">
                                <li>Examen clínico. Si tiene síntomas de cáncer del tracto biliar, su médico puede realizar un examen clínico general para sentir cualquier área de su abdomen que esté hinchada o dolorosa.</li>
                                <li>Análisis de sangre de biomarcadores. Su médico puede recomendarle que tenga sangre prueba para verificar los niveles de biomarcadores. Sin embargo, es importante entender que algunas personas con cáncer del tracto biliar
                                    no tienen niveles elevados de biomarcadores, los niveles elevados también pueden ocurrir en otras condiciones (incluyendo condiciones no cancerosas). Por estas razones, una sangre la prueba sola no puede proporcionar
                                    un diagnóstico.</li>
                                <div class="imagen-80 centrar-imagen .">
                                    <img src="/img/cancer-de-vias-biliares/Diagnostico_vias_biliares-768x512.webp" alt="Cancer de prostata">
                                </div>
                                <li>Imagen.
                                    <ul class="secciones-enumeradas">
                                        <li>Su médico puede recomendarle que se haga un ultrasonido para observar los conductos biliares, la vesícula biliar y los órganos circundantes para detectar signos de cáncer.</li>
                                        <li>Las imágenes por resonancia magnética (IRM) se usan ampliamente para diagnosticar el cáncer del tracto biliar. La resonancia magnética utiliza campos magnéticos y ondas de radio para producir imágenes detalladas
                                            del interior del cuerpo. Un tipo particular de la resonancia magnética llamada colangiopancreatografía por resonancia magnética (MRCP) se puede usar para Imagen detallada de los conductos biliares, la vesícula
                                            biliar, el páncreas y cualquier tumor. Una resonancia magnética también se puede utilizar para mira el hígado con más detalle.</li>
                                        <li>La tomografía computarizada (TC) es un tipo de técnica de rayos X que permite a los médicos ver sus órganos internos en sección transversal. Las tomografías computarizadas se pueden usar en el diagnóstico de cáncer
                                            de tracto biliar, pero se usan con mayor frecuencia para evaluar extensión del cáncer en otras partes del cuerpo</li>
                                    </ul>
                                </li>
                                <li>Biopsias.
                                    <ul class="secciones-enumeradas">
                                        <li>Colangiopancreatografía retrógrada endoscópica. Durante una CPRE, un tubo largo y flexible con una pequeña cámara y luz al final (endoscopio) se pasa por la garganta hasta llegar a una posición donde permita inyectar
                                            contraste para para tomar radiografías de los conductos biliares, vesícula biliar y páncreas. Esto le permite al médico ver la ubicación y el tamaño del tumor y tomar las biopsias.</li>
                                        <li>Ultrasonido endoscópico (EUS). Si las biopsias guiadas por CPRE no son concluyentes, entonces un procedimiento llamado ultrasonido endoscópico (EUS) se puede utilizar para guiar una aguja fina y realizar biopsia
                                            por aspiración para obtener pequeñas muestras. Esto implica el uso de un endoscopio con una sonda de ultrasonido en el extremo, que crea imágenes de los conductos biliares, la vesícula biliar y el páncreas desde
                                            el interior del cuerpo.</li>
                                    </ul>
