                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>Una vez que se detecta el cáncer de riñón, su médico realizará pruebas para saber si el cáncer se ha diseminado dentro del riñón o a otras partes del cuerpo. Este proceso se denomina clasificación. Es importante conocer el
                                estadio antes de diseñar un plan de tratamiento. Cuanto más alto es el estadio, más grave es el cáncer.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-riñon/estadificacion_cancer_de_rinon-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>
                                Se utilizan los siguientes estadios para el cáncer de células renales:
                            </p>
                            <p>Estadio 1: El tumor es de 7 centímetros (cm), o más pequeño y se encuentra solo en el riñón.</p>
                            <p>Estadio 2: El tumor tiene más de 7 centímetros (cm) y se encuentra solo en el riñón.</p>
                            <p>Estadio 3: El cáncer se encuentra en los principales vasos sanguíneos del riñón o en la capa de tejido graso que envuelve al riñón. O bien el tumor es de cualquier tamaño y el cáncer se encuentra solo en el riñón y en uno o
                                más nódulos linfáticos cercanos.</p>
                            <p>Estadio 4: El cáncer se ha propagado fuera de la capa de tejido graso que envuelve al riñón y puede encontrarse en la glándula suprarrenal encima del riñón con cáncer o en los nódulos linfáticos cercanos; o se ha propagado
                                a otros órganos como los pulmones, el hígado, los huesos o el cerebro, y podría haberse propagado también a los nódulos linfáticos.</p>
                            <p>Cáncer de células renales recidivante: El cáncer de células renales recidivante es cáncer que recidivó (volvió) después de haber sido tratado. El cáncer puede volver al riñón o a otras partes del cuerpo muchos años después
                                del tratamiento inicial.</p>
                        </div>
                    </article>
                    <article id="tab7">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>Nadie sabe si el cáncer de riñón se desarrollará o cuándo, pero comprender los factores de riesgo para el cáncer de riñón puede ayudarlo a tomar medidas preventivas para reducir la probabilidad de desarrollar la enfermedad.
                            </p>
                            <p>El cáncer de riñón se produce cuando el ADN en las células de uno o ambos riñones mutan, lo que puede conducir a una división y crecimiento celular descontrolado. Si bien es posible que no se conozca la causa exacta del cáncer
                                de riñón, ciertos factores de riesgo están fuertemente vinculados a la enfermedad, como fumar tabaco y la obesidad. Además, las personas con ciertos síndromes de cáncer hereditario o antecedentes familiares de cáncer de
                                riñón tienen un alto riesgo de desarrollar la enfermedad.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-riñon/factores_cancer_de_rinon-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Pasos importantes para reducir el riesgo de Cáncer de Riñón</h2>
                            <ul class="secciones-enumeradas">
                                <li>No fume</li>
                                <li>Mantenga un peso saludable.</li>
                                <li>Averigüe si estuvo expuesto a ciertas toxinas en el trabajo o en el hogar. Algunas toxinas que pueden aumentar el riesgo de cáncer de riñón incluyen cadmio, asbesto y tricloroetileno.</li>
                                <li>Cuide sus riñones. Las personas con insuficiencia renal pueden tener mayor riesgo de cáncer de riñón.</li>
                                <li>Evite el uso prolongado de medicamentos antiinflamatorios no esteroides (AINE) como ibuprofeno y naproxeno.</li>
                                <li>Controle la presión arterial alta.</li>
                                <li>Controle su nivel de azúcar en la sangre si tiene diabetes.</li>
                            </ul>
                            <p>Tenga en cuenta ciertos factores de riesgo que no se pueden modificar, pero sobre los cuales se debe realizar un seguimiento como antecedentes familiares de cáncer de riñón y ciertas enfermedades congénitas, como la enfermedad
                                de von Hippel-Lindau.</p>

                        </div>

