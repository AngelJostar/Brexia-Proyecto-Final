                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>El tratamiento depende del estadio del cáncer de riñón, el estado general de salud, la edad y otros factores. Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la
                                progresión de la enfermedad. En Brexia, nuestros expertos en cáncer están capacitados y tienen experiencia en los tratamientos disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes.
                                Las opciones más comunes para tratar cáncer de riñón son:</p>
                        </div>
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Cirugía de Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>La cirugía es el tratamiento más común para el cáncer de riñón; la mayoría de las personas con cáncer de riñón en estadio temprano (estadios 1, 2 y 3) se puede curar con cirugía. Los tipos más comunes de cirugía en cáncer de
                                riñón son:</p>
                            <ul class="secciones-enumeradas">
                                <li>Nefrectomía parcial: solo se extirpa el tumor o parte del riñón con el tumor para dejar la mayor cantidad de riñón posible.</li>
                                <li>Nefrectomía radical: se extirpa todo el riñón. También se pueden extirpar, de ser necesario, los tejidos que rodean al riñón y los nódulos linfáticos.</li>
                            </ul>
                            <p>
                                Su médico le orientará sobre la mejor técnica quirúrgica para su caso:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li>Abierta (cirugía tradicional con incisión larga).</li>
                                <li>Laparoscópica (cirugía con una videocámara e instrumentos delgados para incisiones más pequeñas).</li>
                                <li>Robótica (cirugía laparoscópica con ayuda de un robot).</li>
                            </ul>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-riñon/tratamiento_cancer_de_rinon-768x512.webp" alt="cp">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Ablación térmica de Cáncer de Riñón</h2>
                            <p>La ablación térmica destruye el tumor al quemarlo o congelarlo. Se utiliza con mayor frecuencia para tumores pequeños en personas que no son buenas candidatas para la cirugía de nefrectomía.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Vigilancia Activa en Cáncer de Riñón</h2>
                            <p>La vigilancia activa se usa si es un tumor pequeño. La mayoría de los tumores pequeños crece lentamente y es posible que no sean cancerosos. Necesitará pruebas y monitoreo frecuentes.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Terapia Sistemica en Cáncer de Riñón</h2>
                            <p>El tratamiento con medicamentos se usa con frecuencia para el cáncer avanzado de riñón que se propagó a otras partes del cuerpo o donde no se pueda realizar cirugía. Y son comprende las siguientes.</p>
                            <ul class="secciones-enumeradas">
                                <li>La inmunoterapia utiliza el sistema de defensa del cuerpo (sistema inmunitario) para detener o aminorar el crecimiento de las células cancerosas.</li>
                                <li>Los anticuerpos monoclonales atacan una parte específica de las células cancerosas.</li>
                                <li>Los inhibidores de puntos de control ayudan al sistema inmunitario a reconocer y atacar las células cancerosas.</li>
                                <li>Las vacunas y otra inmunoterapia dan un refuerzo general al sistema inmunitario.</li>
                                <li>Las terapias antiangiogénicas reducen el suministro de sangre al tumor para aminorar o detener su crecimiento.
                                </li>
                                <li>Las terapias dirigidas inhiben directamente el crecimiento del cáncer.</li>
                            </ul>
                            <p>La quimioterapia y radiación utilizadas para otros tipos de cáncer no han demostrado ser efectivas para tratar la mayoría de las formas de cáncer de riñón.</p>
                        </div>
