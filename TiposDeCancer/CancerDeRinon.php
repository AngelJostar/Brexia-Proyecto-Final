                            </div>
                            <p>Por otra parte, alrededor de un tercio de los sobrevivientes de cáncer de riñón tienen o desarrollarán insuficiencia renal. Algunas de las razones son:</p>
                            <ul class="secciones-enumeradas">
                                <li>Cirugía para extirpar un riñón entero (nefrectomía radical). A veces hay que extraer todo el riñón porque el tumor es tan grande que destruyó la mayor parte del riñón. Su riesgo de desarrollar insuficiencia renal es mayor
                                    si se debe extirpar la totalidad (no solo una parte) del riñón a causa del cáncer. Sin embargo, con frecuencia, extirpar todo el riñón es mejor para su supervivencia si el tumor es grande o está localizado en el centro.
                                    Si el tumor es pequeño, es mejor someterse a una operación para extirpar solo el tumor y no todo el riñón (nefrectomía parcial). Este enfoque disminuye la probabilidad de desarrollar insuficiencia renal crónica y problemas
                                    asociados con enfermedades cardíacas y de los vasos sanguíneos.</li>
                                <li>Medicamentos para enlentecer o detener el crecimiento del cáncer. En ocasiones, se utilizan medicamentos que se propagan por todo el cuerpo para tratar las células cancerosas, dondequiera que estén, para tratar el cáncer
                                    de riñón avanzado. Todos los medicamentos para el cáncer tienen algunos efectos secundarios, pero algunos pueden ser tóxicos para el riñón (llamados nefrotóxicos). La palabra “nefrotóxico” significa que puede dañar
                                    su función renal</li>
                            </ul>
                            <p>Recuerde: No todas las personas con cáncer de riñón padecerán insuficiencia renal. De la misma manera, no todas las personas que sufren insuficiencia renal o reciben un trasplante tendrán cáncer de riñón. Pregúntele a su médico
                                qué puede hacer para disminuir el riesgo.</p>
