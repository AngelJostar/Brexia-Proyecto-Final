                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas del Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>En los estadios tempranos del cáncer de riñón, la mayoría de las personas no tiene signos ni síntomas. Habitualmente el cáncer de riñón se detecta durante un examen de diagnóstico por imágenes del abdomen, debido a otras afecciones.
                                Sin embargo, a medida que crece el tumor, podemos encontrar:</p>
                            <ul class="secciones-enumeradas">
                                <li>Sangre en la orina.</li>
                                <li>Dolor en la parte inferior de la espalda.</li>
                                <li>Un bulto en la parte inferior de la espalda o costado de la cintura.</li>
                                <li>Pérdida de peso Sudoración nocturna o Fiebre.</li>
                                <li>Fatiga sin explicación.</li>
                                <li>Anemia (recuento bajo de glóbulos rojos).</li>
                                <li>Hinchazón de tobillos y piernas.</li>
                                <li>En los hombres, un grupo de venas agrandadas alrededor de un testículo, típicamente, el testículo derecho.
                                </li>
                            </ul>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-riñon/sintomas_cancer_de_rinon-768x512.webp" alt="cp">
                            </div>
                            <p>Aunque estos síntomas pueden indicar un tumor renal, también pueden ser causados ​​por otros problemas de salud menos graves. Algunos pacientes con cáncer de riñón no experimentan ninguno de estos sintomas, y otros experimentan
                                síntomas completamente diferentes.</p>
                        </div>
                    </article>
                    <article id="tab5">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tipos de Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>El carcinoma de células renales (CCR) es el tipo más común de cáncer de riñón. Las células cancerosas generalmente se desarrollan en el revestimiento de tubos muy pequeños en el riñón, llamados túbulos. Con el tiempo, estas
                                células pueden convertirse en una masa y causar una obstrucción. El cáncer puede formarse en uno o ambos riñones.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-riñon/tipos_cancer_de_rinon-768x512.webp" alt="cp">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Subtipos de RCC</h2>
                            <ul class="secciones-enumeradas">
                                <li>El CCR de células claras: representa aproximadamente el 80 por ciento de todos los carcinomas de células renales. Las células tumorales aparecen muy pálidas o claras cuando se observan con un microscopio
                                </li>
                                <li>El CCR papilar: es el segundo tipo más común de CCR y representa del 10 al 15 por ciento de los cánceres de riñón. Las células cancerosas desarrollan proyecciones similares a dedos en el tumor.</li>
                                <li>El CCR cromófobo: como el CCR de células claras, tiene células tumorales que aparecen pálidas o claras bajo un microscopio. Sin embargo, las células relacionadas con este tipo de cáncer tienden a ser de mayor tamaño</li>
                                <li>El CCR del conducto colector: es un tipo de CCR raro y agresivo, que representa menos del uno por ciento de los cánceres de riñón. Las células cancerosas forman tubos irregulares dentro del tumor.</li>
                                <li>El CCR sin clasificar: es otro tipo raro de cáncer de riñón. Estas células no pueden clasificarse según su apariencia bajo un microscopio. Alternativamente, los cánceres que tienen más de un subtipo pueden agruparse en
                                    esta categoría.</li>
                            </ul>
                            <p>Otros tipos de tumores renales incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>El carcinoma de células transicionales: se desarrolla en la región donde se unen el riñón y los uréteres. Los uréteres son tubos que transportan la orina desde los riñones hasta la vejiga. Las células tumorales pueden comenzar
                                    a parecerse más a las células de cáncer de vejiga que a las células de cáncer de riñón cuando se observan con un microscopio. Este tipo de cáncer también puede denominarse cáncer urotelial o carcinoma de pelvis renal.</li>
                                <li>El sarcoma renal: es un tipo raro de cáncer de riñón que se desarrolla en el tejido conectivo del riñón.</li>
                            </ul>
                        </div>
