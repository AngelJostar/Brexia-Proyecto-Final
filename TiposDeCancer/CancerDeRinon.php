                    <article id="tab8">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Insuficiencia Renal y Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>Los estudios demuestran que hay una relación entre el cáncer de riñón y la insuficiencia renal. Algunos estudios demuestran que las personas que padecen insuficiencia renal pueden tener un riesgo más alto de padecer cáncer
                                de riñón. Algunas de las razones son:
                            </p>
                            <ul class="secciones-enumeradas">
                                <li>Diálisis a largo plazo. Algunos estudios demuestran que las personas sometidas a diálisis a largo plazo tienen un riesgo 5 veces mayor de padecer cáncer de riñón. Los expertos creen que este riesgo se debe a la insuficiencia
                                    renal y no a la diálisis.</li>
                                <li>Medicamentos inmunosupresores. Algunos medicamentos antirrechazo que deben tomar quienes reciben un trasplante de riñón pueden aumentar el riesgo de padecer cáncer de riñón. Sin embargo, es importante que tome su medicamento
                                    inmunosupresor si ha sido sometido a un trasplante. Sin él, el cuerpo rechazará el nuevo riñón.</li>
                            </ul>
