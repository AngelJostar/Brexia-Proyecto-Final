                                <li class="">Entre los signos de cáncer de riñón se incluyen tener sangre en la orina o una masa en el abdomen.</li>
                                <li class="">Para diagnosticar el cáncer de riñón se utilizan pruebas que examinan el abdomen y los riñones.</li>
                                <li class="">Ciertos factores afectan el pronóstico (probabilidad de recuperación) y las opciones de tratamiento.</li>
