                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Riñón</h2>
                        <div class="font-16 fontW-400">
                            <p>Su médico diagnosticará cáncer de riñón al revisar su historia clínica y al hacer un examen físico, junto con análisis de sangre y orina.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-riñon/diagnostico_cancer_rinon-768x512.webp" alt="cp">
                            </div>
                            <p>También hará uno o alguno de los siguientes exámenes de diagnóstico por imágenes:</p>
                            <ul class="secciones-enumeradas">
                                <li class="">La tomografía computarizada (TC) usa rayos X para tomar una imagen completa de los riñones y el abdomen (vientre). Se puede realizar con o sin contraste. Se utilizan pequeñas cantidades de radiación. Con frecuencia, una
                                    TC muestra si un tumor parece canceroso o si se ha propagado fuera del riñón.</li>
                                <li>La imagen por resonancia magnética (IRM) realiza una imagen completa de los riñones y el abdomen, pero sin radiación. Se puede hacer con o sin un material de contraste denominado gadolinio, que se deberá evitar en personas
                                    con diálisis o con muy baja función renal. Una IRM puede costar más que una TC, puede tardar más tiempo en realizarse y las imágenes tal vez no sean tan claras.</li>
                                <li>El ultrasonido utiliza ondas de sonido para obtener una imagen completa de los riñones y el abdomen sin radiación. Puede ser útil para ayudar a determinar si la masa en sus riñones es un quiste lleno de líquido o si se
                                    trata de un tumor sólido. Esta prueba se realiza sin contraste.
                                </li>
                                <li>Biopsia: extracción de células o tejidos para que un patólogo los observe al microscopio y determine si hay signos de cáncer. Para realizar una biopsia del cáncer de células renales, se inserta una aguja delgada en el tumor
                                    y se extrae una muestra de tejido.</li>
                                <li>Examen físico y antecedentes de salud: examen del cuerpo para revisar el estado general de salud e identificar cualquier signo de enfermedad, como masas o cualquier otra cosa que parezca anormal. También se toman datos
                                    sobre los hábitos de salud, los antecedentes de enfermedades y los tratamientos anteriores.</li>
                                <li>Análisis de orina: prueba para analizar el color de la orina y su contenido; por ejemplo, azúcar, proteínas, glóbulos rojos y glóbulos blancos.</li>
                            </ul>
                        </div>
