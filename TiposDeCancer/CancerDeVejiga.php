                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Diagnóstico del Cáncer de Vejiga</h2>
                        <div class="font-16 fontW-400">
                            <p>Existen pruebas específicas que ayudan a realizar el diagnóstico y la evaluación de la diseminación del cáncer de vejiga. El diagnóstico del cáncer de vejiga puede confirmarse solamente a través de un examen histopatológico
                                en el que las muestras del tejido del tumor son sometidas a examen en el laboratorio. Dicho examen revela las características específicas del tumor para determinar el tipo de cáncer de vejiga. La cistoscopia es un examen
                                que permite la inspección del interior de la vejiga y la uretra para detectar la presencia de tumores.</p>
                            <p>Nuestros expertos en cáncer de vejiga utilizan una variedad de pruebas y herramientas para diagnosticar el cáncer de vejiga, evaluar la enfermedad y desarrollar una estrategia de tratamiento personalizada para cada paciente.
                                Las herramientas comunes utilizadas para diagnosticar el cáncer de vejiga incluyen:</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Citoscopia en Cáncer de Vejiga</h2>
                            <p>Si su médico ve células anormales, se realizará una biopsia para extraer tejidos para un examen más detallado. Para obtener una muestra de tejido del interior de la vejiga, se inserta un cistoscopio, un instrumento delgado,
                                en la vejiga a través de la uretra.</p>
                            <p>A veces, se realiza una citoscopia de fluorescencia con una citoscopia estándar. Durante esta prueba de cáncer de vejiga, se insertan medicamentos conocidos como porfirinas en la vejiga. Estos medicamentos son fácilmente absorbidos
                                por las células cancerosas, lo que hace que las células brillen o se fluoreszcan bajo una luz azul. Este proceso le permite al médico ver áreas de la vejiga donde las células cancerosas pueden haberse perdido durante la
                                citoscopia de rutina.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/cancer-de-vejiga/diagnostico_cancer_de_vejiga-1-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Pruebas de laboratorio en Cáncer de Vejiga</h2>
                            <p>Las pruebas realizadas en el laboratorio para diagnosticar el cáncer de vejiga incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Citología de la orina: se examina una muestra de orina con un microscopio para ver si hay células cancerosas o precancerosas.</li>
                                <li>Cultivo de orina: esta prueba puede ayudar a determinar si los síntomas urinarios se deben a una infección, en lugar de cáncer.</li>
                                <li>Pruebas de marcadores tumorales en orina: otras pruebas para el cáncer de vejiga miden la presencia de varias sustancias en la orina que pueden indicar la presencia de cáncer. Las pruebas para NMP22 y BTA, la prueba de
                                    inmunocitos y la prueba de UroVysion son ejemplos de formas de verificar marcadores o indicadores de cáncer en la orina.</li>
                                <li>Pruebas genómicas avanzadas: las pruebas genómicas examinan el ADN de un tumor para encontrar alteraciones que pueden estar impulsando el crecimiento del cáncer. Al identificar las mutaciones (cambios) que ocurren en el
                                    genoma de una célula cancerosa, los médicos pueden comprender mejor qué causó el tumor y adaptar el tratamiento según estos hallazgos.</li>
                                <li>Panel de nutrición: los pacientes son evaluados para detectar deficiencias de nutrientes, como la vitamina D y el hierro. La prueba ayuda a identificar los nutrientes que deben reemplazarse o aumentarse para respaldar la
                                    calidad de vida del paciente.</li>
                            </ul>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Biopsia en Cáncer de Vejiga</h2>
                            <p>Con el cáncer de vejiga, las biopsias generalmente se realizan durante una citoscopia. Se inserta un instrumento delgado a través del tubo de citoscopia y se recoge una pequeña muestra de tejido que parece posiblemente canceroso
                                desde el interior de la vejiga.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Pielograma en Cáncer de Vejiga</h2>
                            <p>Un pielograma retrógrado es una prueba de imagen para el cáncer de vejiga que usa un tinte para mejorar la vista del área de la vejiga. Con este enfoque, el tinte se inyecta a través de un catéter colocado a través de la uretra
                                y dentro de la vejiga o el uréter. Esta prueba es útil para personas alérgicas al tinte utilizado para la prueba de rayos X.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Radiografía en Cáncer de Vejiga</h2>
                            <p>Se puede tomar una radiografía de tórax para determinar si el cáncer se ha diseminado a los pulmones. El cáncer no estará presente en los pulmones a menos que esté en una etapa avanzada.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Tomografía computarizada en Cáncer de Vejiga</h2>
                            <p>Una tomografía computarizada (TC) de la vejiga, los uréteres y los riñones se llama urograma por tomografía computarizada. A veces, esta prueba de imagen se puede realizar en lugar de un pielograma intravenoso para observar
                                específicamente la parte superior del sistema urinario. Una urografía por TC también puede ayudar a localizar ganglios linfáticos agrandados y ofrecer información útil sobre las regiones abdominal y pélvica.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Resonancia magnética en Cáncer de Vejiga</h2>
                            <p>Con el cáncer de vejiga, una resonancia magnética (IRM) se usa principalmente para buscar signos de que el cáncer se haya diseminado más allá de la vejiga hacia los tejidos cercanos o los ganglios linfáticos. A veces se puede
                                realizar una urografía por resonancia magnética en lugar de una pielografía intravenosa para examinar la parte superior del sistema urinario.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Ultrasonido en Cáncer de Vejiga</h2>
                            <p>Esta prueba de imagen puede ayudar a determinar el tamaño del cáncer de vejiga y si se ha diseminado a órganos o tejidos cercanos.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Exploración Ósea en Cáncer de Vejiga</h2>
                            <p>El cáncer de vejiga a veces hace metástasis (se extiende) a los huesos. Si un paciente experimenta dolor óseo o los análisis de sangre revelan niveles elevados de calcio, el oncólogo radiólogo puede realizar una exploración
                                ósea para detectar si el cáncer de vejiga se ha diseminado al hueso.</p>
