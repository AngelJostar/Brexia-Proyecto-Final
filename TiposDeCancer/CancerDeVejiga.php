                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tipos de Cáncer de Vejiga</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer de vejiga se clasifica por varios tipos, dependiendo de dónde se forma exactamente, junto con otros factores. El tipo más común de cáncer de vejiga es el carcinoma de células de transición (urotelial) (TCC). Este
                                tipo representa aproximadamente el 95 por ciento de los cánceres de vejiga. Las células cancerosas de este tipo se parecen a las células uroteliales que recubren el interior de la vejiga. Hay dos subtipos de TCC.</p>
                            <ul class="secciones-enumeradas">
                                <li>Carcinoma papilar: crece desde la superficie interna de la vejiga hacia el centro hueco en proyecciones en forma de dedo. A menudo, estos tumores se denominan cánceres papilares no invasivos, lo que significa que no crecen
                                    en las capas más profundas de la pared de la vejiga. Cuando el CCT papilar es de grado muy bajo, puede denominarse neoplasia papilar de bajo potencial maligno, y los tratamientos suelen tener resultados positivos.</li>
                                <li>Carcinomas planos: este tipo de TCC no crece fuera del urotelio hacia el centro de la vejiga. Por el contrario, los carcinomas planos permanecen en la superficie de la pared de la vejiga. Si un carcinoma plano se limita
                                    al urotelio, se llama carcinoma plano no invasivo o carcinoma plano in situ.</li>
                            </ul>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-vejiga/tipos_cancer_de_vejiga-1-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Las formas más raras de cáncer de vejiga incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>El carcinoma de células escamosas representa aproximadamente del 1 al 2 por ciento de los cánceres de vejiga diagnosticados en los Estados Unidos. Las células escamosas se parecen a las células planas en la superficie de
                                    la piel. Casi todos los carcinomas de células escamosas de la vejiga son invasivos.</li>
                                <li>El adenocarcinoma de vejiga se parece mucho a las células formadoras de glándulas que se ven en los cánceres de colon y representa aproximadamente el 1 por ciento de los cánceres de vejiga en los Estados Unidos.</li>
                                <li>El carcinoma de células pequeñas es extremadamente raro, representa menos del 1 por ciento de todos los cánceres de vejiga diagnosticados en los Estados Unidos. Este tipo de cáncer de vejiga comienza en las células neuroendocrinas,
                                    que son similares a los nervios.</li>
                                <li>El sarcoma es otro tipo muy raro de cáncer de vejiga que comienza en la capa muscular de la pared de la vejiga.</li>
                            </ul>
                        </div>
                    </article>
                    <article id="tab6">
                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Estadificación del Cáncer de Vejiga</h2>
                        <div class="font-16 fontW-400">
                            <p>Al igual que otros tipos de cáncer, el cáncer de vejiga se evalúa con el sistema TNM para evaluar tres factores principales a la hora de tratar el cáncer.</p>
                            <ul class="secciones-enumeradas">
                                <li>T (tumor): describe el tamaño del tumor original.</li>
                                <li>N (nodo): indica si el cáncer está presente en los ganglios linfáticos.</li>
                                <li>M (metástasis): se refiere a si el cáncer se ha diseminado a otras partes del cuerpo.</li>
                            </ul>
                            <p>Se asigna un número (0-4) o la letra X a cada factor. Un número mayor indica una gravedad creciente. Por ejemplo, una puntuación T1 indica un tumor más pequeño que una puntuación T2. La letra X significa que la información
                                no pudo ser evaluada.</p>
                            <p>Una vez que se han asignado los puntajes T, N y M, se asigna una etapa general de cáncer de vejiga.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-vejiga/estadificacion_cancer_de_vejiga-1.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Categorías T para el cáncer de vejiga: (Estas medidas se refieren al tumor primario)</p>
                            <ul class="secciones-enumeradas">
                                <li>TX significa que el tumor primario no se puede evaluar o que no se conoce información.</li>
                                <li>TO significa que no hay evidencia de tumor primario.</li>
                                <li>Ta indica carcinoma papilar no invasivo.</li>
                                <li>T1 significa que el tumor ha crecido desde la capa de células que recubre la vejiga hacia el tejido conectivo que se encuentra debajo. No ha crecido en la capa muscular de la vejiga.</li>
                                <li>T2 significa que el tumor ha crecido en la capa muscular. T2 tiene dos subcategorías:
                                    <ul class="secciones-enumeradas">
                                        <li>T2a significa que el tumor está en la mitad interna de la capa muscular.</li>
                                        <li>T2b significa que el tumor está en la mitad externa de la capa muscular.</li>
                                    </ul>
                                </li>
                                <li>T3 significa que el tumor ha crecido a través de la capa muscular y hacia el tejido graso circundante. T3 tiene dos subcategorías:
                                    <ul class="secciones-enumeradas">
                                        <li>T3a significa que esta extensión al tejido graso solo se puede ver con un microscopio.</li>
                                        <li>T3b significa que esta diseminación al tejido graso es lo suficientemente grande como para ser vista en una prueba de imagen o para ser vista / sentida por el cirujano.</li>
                                    </ul>
                                </li>
                                <li>T4 significa que el tumor se ha diseminado a órganos o estructuras cercanas. Puede estar creciendo en el estroma (tejido principal) de la próstata, las vesículas seminales, el útero, la vagina, la pared pélvica o la pared
                                    abdominal.
                                </li>
                            </ul>
                            <p>Categorías N para el cáncer de vejiga:</p>
                            <ul class="secciones-enumeradas">
                                <li>NX significa que no se pueden evaluar los ganglios linfáticos cercanos o se desconoce la información.</li>
                                <li>N0 significa que el cáncer no se ha diseminado a ningún ganglio linfático cercano.</li>
                                <li>N1 significa que el cáncer se ha diseminado a un ganglio linfático en la pelvis verdadera.</li>
                                <li>N2 significa que el cáncer se ha diseminado a dos o más ganglios linfáticos en la pelvis verdadera.</li>
                                <li>N3 significa que el cáncer se ha diseminado a los ganglios linfáticos que se encuentran a lo largo de la arteria ilíaca común.</li>
                            </ul>
                            <p>Categorías M para la cavidad de la vejiga:</p>
                            <ul class="secciones-enumeradas">
                                <li>M0: sin propagación distante.</li>
                                <li>M1: el cáncer se ha diseminado a sitios distantes fuera de la región de la vejiga (por ejemplo, pulmones, hígado o huesos).</li>
                            </ul>
                            <p>Una vez que se han asignado las categorías, el cáncer se clasifica de una de las siguientes maneras:</p>
                            <p>La etapa 0 tiene dos subcategorías:</p>
                            <ul class="secciones-enumeradas">
                                <li>Etapa 0a: el cáncer de vejiga es un carcinoma papilar no invasivo. El cáncer ha crecido hacia el centro hueco de la vejiga pero no hacia el tejido conectivo o la capa muscular. No hay células malignas en los ganglios linfáticos
                                    o sitios distantes.</li>
                                <li>Etapa 0is: carcinoma plano no invasivo o carcinoma in situ. En esta etapa del cáncer de vejiga, el tumor está creciendo solo en la capa interna de la vejiga. No ha crecido hacia la parte hueca de la vejiga y no ha invadido
                                    el tejido conectivo o el músculo de la pared de la vejiga. No se han encontrado células malignas en los ganglios linfáticos o sitios distantes.</li>
                            </ul>
                            <p>Etapa I (cáncer de vejiga en etapa 1): el cáncer se ha convertido en la capa de tejido conectivo debajo de la capa de revestimiento de la pared de la vejiga. La enfermedad no se ha propagado a los ganglios linfáticos o sitios
                                distantes.
                            </p>
                            <p>Etapa II (cáncer de vejiga en etapa 2): el cáncer ha crecido hacia la capa muscular de la pared de la vejiga. No ha crecido a través de la capa muscular hasta el tejido graso que rodea la vejiga. En esta etapa del cáncer de
                                vejiga, la enfermedad no se ha propagado a los ganglios linfáticos ni a sitios distantes.</p>
                            <p>Etapa III (cáncer de vejiga en etapa 3): el cáncer ha crecido a través de la pared de la vejiga hacia el tejido adiposo circundante. También puede estar en la próstata, el útero o la vagina. No está presente en la pared pélvica
                                o abdominal y no se ha propagado a los ganglios linfáticos o sitios distantes.</p>
                            <p>Etapa IV (cáncer de vejiga en etapa 4): en el cáncer de vejiga en etapa IV, se aplica uno de los siguientes:</p>
                            <ul class="secciones-enumeradas">
                                <li>El cáncer ha crecido a través de la pared de la vejiga y hacia la pared pélvica o abdominal, pero no se ha diseminado a los ganglios linfáticos o sitios distantes.</li>
                                <li>El cáncer se ha diseminado a los ganglios linfáticos cercanos pero no a sitios distantes.</li>
                                <li>El cáncer se ha diseminado a sitios distantes, como huesos, hígado o pulmones.</li>
                            </ul>
                        </div>
