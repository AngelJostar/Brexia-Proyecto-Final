                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas del Cáncer de Vejiga</h2>
                        <div class="font-16 fontW-400">
                            <p>Debido a que la vejiga es responsable de retener la orina después de que es producida por los riñones, muchos síntomas del cáncer de vejiga pueden estar relacionados con anomalías al orinar.</p>
                            <p>La sangre en la orina (hematuria) suele ser el primer síntoma de cáncer de vejiga. La sangre puede hacer que el color de la orina cambie a naranja, rosa o, raramente, rojo oscuro. La sangre puede estar presente de forma regular
                                o desaparecer y reaparecer en el transcurso de días o semanas.</p>
                            <p>El cáncer de vejiga en etapa temprana generalmente no causa dolor u otros síntomas además del sangrado. Pero la sangre en la orina no siempre significa que hay un tumor en la vejiga. Es más probable que sea causado por una
                                afección menos grave, como una infección.</p>
                            <p>Los cambios en la orina pueden ser otro signo temprano de cáncer de vejiga. La micción frecuente, el dolor al orinar, la incapacidad para orinar y una sensación de urgencia al orinar, incluso cuando la vejiga no está llena,
                                pueden ser síntomas de cáncer de vejiga, pero estos cambios son más comúnmente un signo de una afección menos grave, como un tumor benigno o una infección.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-vejiga/Sintomas_cancer_de_vejiga-1-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Síntomas de Cáncer de Vejiga avanzado</h2>
                            <p>Cuando los tumores de vejiga se agrandan o las células cancerosas se propagan a otras áreas del cuerpo, pueden causar síntomas que incluyen:</p>
                            <ul class="secciones-enumeradas">
                                <li>Dolor lumbar, generalmente enfocado en un lado.</li>
                                <li>Dolor de huesos.</li>
                                <li>Hinchazón de pies</li>
                            </ul>
                            <p>Si su médico sospecha de cáncer de vejiga, él o ella ordenará pruebas para determinar la causa de sus síntomas.</p>
