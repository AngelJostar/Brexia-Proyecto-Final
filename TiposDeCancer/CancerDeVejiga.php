                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Factores de Riesgo del Cáncer de Vejiga</h2>
                        <div class="font-16 fontW-400">
                            <p>El cáncer de vejiga se forma cuando el ADN en las células de la vejiga muta o cambia, deshabilitando las funciones que controlan el crecimiento celular. En muchos casos, estas células mutadas mueren o son atacadas por el sistema
                                inmune. Pero algunas células mutadas pueden escapar del sistema inmune y crecer sin control, formando un tumor en la vejiga. Si bien no se conoce la causa exacta del cáncer de vejiga, ciertos factores de riesgo están relacionados
                                con la enfermedad, incluido el tabaquismo y la exposición a ciertos productos químicos y gases. Además, las personas con antecedentes familiares de cáncer de vejiga tienen un alto riesgo de desarrollar la enfermedad.</p>
                            <div class="imagen-80 centrar-imagen ">
                                <img src="/img/cancer-de-vejiga/factores_riesgo_cancer_de_vejiga-1-768x512.webp" alt="Brexia_especialistas">
                            </div>
                            <p>Los factores de riesgo conocidos para el cáncer de vejiga incluyen:</p>
                            <ol class="secciones-enumeradas">
                                <li>Género: los hombres tienen un mayor riesgo que las mujeres de contraer cáncer de vejiga. Los hombres tienen una probabilidad de aproximadamente 1 en 26 de desarrollar cáncer de vejiga en su vida. Para las mujeres, esta
                                    posibilidad es de aproximadamente 1 de cada 86.</li>
                                <li>Edad: la edad promedio al momento del diagnóstico es de 73 años, y el 90 por ciento de los pacientes tienen más de 55 años.</li>
                                <li>Raza: el cáncer de vejiga es dos veces más común entre los caucásicos que los afroamericanos. Esta enfermedad es menos común entre hispanos, asiáticos y nativos americanos</li>
                                <li>Antecedentes familiares: las personas con un familiar que tiene o ha tenido cáncer de vejiga tienen un mayor riesgo de desarrollar esta enfermedad.</li>
                                <li>Otros síndromes genéticos hereditarios también se consideran factores de riesgo de cáncer de vejiga, como:
                                    <ol class="secciones-enumeradas">
                                        <li>Rb1: una forma alterada de Rb1, el gen del retinoblastoma, puede aumentar los riesgos de cáncer de vejiga.</li>
                                        <li>Enfermedad de Cowden: este síndrome, relacionado con una forma anormal del gen PTEN, puede desencadenar cánceres de mama y tiroides, y aumenta el riesgo de cáncer de vejiga.</li>
                                        <li>Síndrome de Lynch: esta afección genética, también puede aumentar el riesgo de cáncer de vejiga y cáncer de uréter.</li>
                                    </ol>
                                </li>
                                <li>Fumar: fumar cigarrillos es el factor de riesgo más grande para el cáncer de vejiga. Los fumadores tienen más del doble de probabilidades de contraer cáncer de vejiga en comparación con los no fumadores.</li>
                                <li>Exposición en el lugar de trabajo: algunos productos químicos utilizados en la industria del tinte, como la bencidina y la beta-naftilamina, se han asociado con el cáncer de vejiga. Otras industrias en las que se usan productos
                                    químicos que pueden causar cáncer de vejiga incluyen el caucho, el cuero, los textiles, la fabricación de pintura y la impresión.</li>
                                <li>Arsénico: el agua potable que contiene arsénico se ha relacionado con el cáncer de vejiga. La exposición depende de dónde vive una persona y la fuente de agua.</li>
                                <li>Bajo consumo de líquidos: beber muchos líquidos diariamente ayuda a reducir los riesgos de cáncer de vejiga. Del mismo modo, no beber lo suficiente puede aumentar este riesgo porque los químicos se quedan en la vejiga por
                                    más tiempo.</li>
                            </ol>
                        </div>
