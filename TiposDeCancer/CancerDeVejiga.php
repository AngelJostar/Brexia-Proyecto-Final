                        <h2 class="centrar-texto brexia-color encabezados-brexia ">¿Qué es el Cáncer de Vejiga?</h2>
                        <div class="font-16 fontW-400">
                            <p>EI cáncer de vejiga se forma en los tejidos de la vejiga, el órgano donde se amacena la orina. EI tipo más frecuente de cáncer de vejiga (90%) es el carcinoma de células de transición o de células transicionales. Este tipo
                                de cáncer comienza en las células que normalmente forman el revestimiento interior de la vejiga, también llamado epitelio transicional, epitelio de transición o urotelio. Otros tipos incluyen el carcinoma escamocelular,
                                un cáncer de vejiga que comienza en las células planas del revestimiento de la vejiga, y el adenocarcinoma, un cáncer que comienza en las células del revestimiento de la vejiga que producen moco, y otras formas poco comunes.
                            </p>
                            <P>
                                El cáncer de vejiga se forma cuando el ADN en las células de la vejiga muta o cambia, deshabilitando las funciones que controlan el crecimiento celular. En muchos casos, estas células mutadas mueren o son atacadas por el sistema inmune. Pero algunas células
                                mutadas pueden escapar del sistema inmune y crecer sin control, formando un tumor en la vejiga. Si bien no se conoce la causa exacta del cáncer de vejiga, ciertos factores de riesgo están relacionados con la enfermedad,
                                incluido el tabaquismo y la exposición a ciertos productos químicos y gases. Además, las personas con antecedentes familiares de cáncer de vejiga tienen un alto riesgo de desarrollar la enfermedad.
                            </P>
