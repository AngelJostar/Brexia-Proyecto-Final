                        <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento de Cáncer de Vejiga</h2>
                        <div class="font-16 fontW-400">
                            <p>Las opciones de tratamiento varían ampliamente dependiendo de varios factores, incluyendo la etapa, el tipo y la progresión del cáncer de vejiga. Nuestros expertos en cáncer están capacitados y tienen experiencia en los tratamientos
                                disponibles, y se mantienen actualizados sobre las opciones y tecnologías nuevas y emergentes. Utilizando diversas técnicas, que incluyen inmunoterapia, cirugía, radioterapia y quimioterapia. Los planes de tratamiento del
                                cáncer de vejiga se desarrollan en función de diferentes factores, como la etapa de la enfermedad y la salud general del paciente.</p>
                            <p>Se utilizan cuatro tipos de tratamiento estándar:</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Cirugía en Cáncer de Vejiga</h2>
                            <p>Es posible realizar uno de los siguientes tipos de cirugía:</p>
                            <ul class="secciones-enumeradas">
                                <li>Resección transuretral (RTU) con fulguración: cirugía para la que se introduce un cistoscopio (un tubo delgado con luz) en la vejiga a través de la uretra. Se utiliza un instrumento con un pequeño bucle de alambre en uno
                                    de los extremos para extirpar el cáncer o quemar el tumor con electricidad de alta energía.</li>
                                <li>Cistectomía radical: cirugía para extirpar la vejiga y cualquiera de los ganglios linfáticos y órganos cercanos que tienen cáncer. Esta operación se puede realizar cuando el cáncer de vejiga invade la pared muscular, o
                                    cuando un cáncer superficial afecta una parte grande de la vejiga.</li>
                                <li>Cistectomía parcial: cirugía para extirpar parte de la vejiga. Esta operación se emplea para pacientes con un tumor de grado bajo que invade la pared de la vejiga, pero que se limita a un área. Como solo se extrae una parte
                                    de la vejiga, los pacientes pueden orinar normalmente después de recuperarse de esta cirugía. Este procedimiento también se llama cistectomía segmentaria.</li>
                            </ul>
                            <p>Una vez que el médico extirpa todo el cáncer visible durante la cirugía, es posible que algunos pacientes reciban quimioterapia o radioterapia después de la cirugía para destruir cualquier célula cancerosa que quede. El tratamiento
                                que se administra después de la cirugía para disminuir el riesgo de que el cáncer vuelva se llama terapia adyuvante.</p>
                            <div class="imagen-80 centrar-imagen .">
                                <img src="/img/cancer-de-vejiga/Tratamiento_cancer_de_vejiga-1-768x512.webp" alt="Cancer de prostata">
                            </div>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Radioterapia en Cáncer de Vejiga</h2>
                            <p>La radioterapia es un tratamiento del cáncer para el que se usan rayos X de alta energía u otros tipos de radiación para destruir células cancerosas o impedir que crezcan. Hay dos tipos de radioterapia:</p>
                            <ul class="secciones-enumeradas">
                                <li>Radioterapia externa: tipo de radioterapia para la que se usa una máquina que envía la radiación hacia el cáncer desde el exterior del cuerpo.</li>
                                <li>Radioterapia interna: tipo de radioterapia para la que se usa una sustancia radiactiva sellada en agujas, semillas, alambres o catéteres que se colocan directamente en el cáncer o cerca de este.</li>
                            </ul>
                            <p>La forma de administración de la radioterapia depende del tipo y el estadio del cáncer de vejiga que se esté tratando. La radioterapia externa se usa para tratar el cáncer de vejiga.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Quimioterapia en Cáncer de Vejiga</h2>
                            <p>Para el cáncer de vejiga, la quimioterapia regional puede ser intravesical (administrada en la vejiga a través de un tubo que se introduce en la uretra). La forma de administración de la quimioterapia depende del tipo y el
                                estadio del cáncer que se esté tratando. La quimioterapia combinada es un tratamiento para el que se utilizan uno o más medicamentos contra el cáncer.</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Inmunoterapia en Cáncer de Vejiga</h2>
                            <p>La inmunoterapia es un tratamiento en el que se usa el sistema inmunitario del paciente para combatir el cáncer. Se usan sustancias elaboradas por el cuerpo o en el laboratorio para impulsar, dirigir o restaurar las defensas
                                naturales del cuerpo contra el cáncer. Este tipo de tratamiento para el cáncer también se llama bioterapia o terapia biológica</p>
                            <h2 class="centrar-texto brexia-color encabezados-brexia ">Tratamiento del Cáncer de Vejiga en función de la extensión de la enfermedad (clasificada en estadios)</h2>
                            <ul class="secciones-enumeradas">
                                <li>Enfermedad no músculo−invasiva (estadio 0a, estadio 0is, estadio I) Implica la existencia de un tumor confinado en la mucosa (capa superficial de tejido del revestimiento de la vejiga).
                                    <ul class="secciones-enumeradas">
                                        <li>Después de la cistoscopia todos los pacientes se someten a una resección transuretral (RTU)* del tumor de vejiga, procedimiento que podría resultar curativo en ciertos casos, puesto que podría extirparse todo el
                                            tumor.
                                        </li>
                                        <li>En ocasiones, para evitar la recidiva de la enfermedad, podría administrarse una terapia adyuvante, como quimioterapia o inmunoterapia instiladas directamente en la vejiga.</li>
                                        <li>Si estos tratamientos fracasaran, la extirpación de la vejiga (cistectomía) sería una opción.</li>
                                    </ul>
                                </li>
                                <li>Cáncer de vejiga músculo−invasivo (estadio II, estadio III) implica la existencia de un tumor que ha invadido la capa de músculo de la vejiga o que se ha extendido a los tejidos que la circulan
                                    <ul class="secciones-enumeradas">
                                        <li>El tratamiento recomendado es la extirpación radical de la vejiga, incluyendo algunos órganos circundantes completa o parcialmente. Este proceso podría modificarse ligeramente para preservar en la mayor medida posible
                                            los órganos circundantes.</li>
                                        <li>La quimioterapia o la radioterapia se recomiendan antes de la cirugía para mejorar el resultado. Si un paciente rechaza la cirugía o si no está en una condición física suficientemente buena para someterse a ella,
                                            la RTU o la RTU combinada con radioterapia y/o quimioterapia son opciones posibles.</li>
                                    </ul>
                                </li>
                                <li>La enfermedad avanzada y metastásica (estadio IV) implica la presencia de un tumor que ha crecido a través de la vejiga hasta la pared de la pelvis o el abdomen, o más allá de estos hasta órganos distantes.
                                    <ul class="secciones-enumeradas">
                                        <li>En este estadio es preferible la administración de quimioterapia, inmunoterapia o terapia dirigida ya que es improbable que la cirugía logre ser curativa. Para determinados pacientes la cirugía y la administración
                                            de radioterapia tras la quimioterapia podrían ser beneficiosas.</li>
                                        <li>La radioterapia también podría ser útil para aliviar dolor o sangrado.</li>
                                    </ul>
                                </li>
